    "# 2. CompletableFuture (비동기 결과 조합)",
    "",
    "## 개념",
    "",
    "`CompletableFuture`는 Java 8에서 도입된 **비동기 작업의 결과**를 다루는 클래스입니다. 여러 비동기 작업을 **병렬 실행**하고, 결과를 **조합/변환/체이닝**할 수 있습니다.",
    "",
    "```mermaid",
    "flowchart TD",
    "    A[\"시작\"] --> B[\"프로필 조회\"]",
    "    A --> C[\"주문 내역 조회\"]",
    "    A --> D[\"포인트 조회\"]",
    "    B --> E[\"allOf - 모두 완료 대기\"]",
    "    C --> E",
    "    D --> E",
    "    E --> F[\"결과 조합하여 응답\"]",
    "```",
    "",
    "### 동기 vs 비동기 실행 시간 비교",
    "",
    "| 방식 | 프로필(2초) | 주문(3초) | 포인트(1초) | **총 시간** |",
    "|------|-----------|----------|-----------|------------|",
    "| 동기(순차) | → | → | → | **6초** |",
    "| 비동기(병렬) | ↓ | ↓ | ↓ | **3초** (가장 긴 작업 기준) |",
    "",
    "---",
    "",
    "## 실전 예제: 여러 API를 병렬 호출 후 조합",
    "",
    "```java",
    "@RestController",
    "@RequestMapping(\"/api/dashboard\")",
    "@RequiredArgsConstructor",
    "public class DashboardController {",
    "",
    "    private final ExternalApiService apiService;",
    "",
    "    @GetMapping(\"/{userId}\")",
    "    public ResponseEntity<DashboardResponse> getDashboard(",
    "            @PathVariable String userId) throws Exception {",
    "",
    "        long start = System.currentTimeMillis();",
    "",
    "        // 3개의 비동기 작업을 동시에 시작",
    "        CompletableFuture<UserProfile> profileFuture =",
    "                apiService.fetchUserProfile(userId);",
    "        CompletableFuture<List<Order>> ordersFuture =",
    "                apiService.fetchOrderHistory(userId);",
    "        CompletableFuture<Integer> pointFuture =",
    "                apiService.fetchPoint(userId);",
    "",
    "        // 모든 작업이 완료될 때까지 대기",
    "        CompletableFuture.allOf(profileFuture, ordersFuture, pointFuture).join();",
    "",
    "        // 결과 조합",
    "        DashboardResponse response = DashboardResponse.builder()",
    "                .profile(profileFuture.get())",
    "                .orders(ordersFuture.get())",
    "                .point(pointFuture.get())",
    "                .build();",
    "",
    "        long elapsed = System.currentTimeMillis() - start;",
    "        log.info(\"대시보드 조회 완료: {}ms (동기였다면 6초)\", elapsed);",
    "",
    "        return ResponseEntity.ok(response);",
    "    }",
    "}",
    "```",
    "",
    "---",
    "",
    "## CompletableFuture 핵심 메서드",
    "",
    "### 생성",
    "",
    "```java",
    "// 즉시 완료된 Future",
    "CompletableFuture.completedFuture(\"결과\");",
    "",
    "// 별도 스레드에서 실행 (Executor 지정 가능)",
    "CompletableFuture.supplyAsync(() -> {",
    "    return fetchData();  // 값 반환",
    "});",
    "",
    "CompletableFuture.runAsync(() -> {",
    "    sendEmail();  // 반환값 없음",
    "});",
    "```",
    "",
    "### 변환 / 체이닝",
    "",
    "```java",
    "CompletableFuture<String> future = CompletableFuture",
    "        .supplyAsync(() -> getUserName(userId))",
    "        .thenApply(name -> name.toUpperCase())             // 결과 변환",
    "        .thenApply(name -> \"Hello, \" + name + \"!\");       // 추가 변환",
    "",
    "// 비동기 체이닝 (다음 작업도 비동기)",
    "CompletableFuture<OrderResponse> future = CompletableFuture",
    "        .supplyAsync(() -> getUser(userId))",
    "        .thenCompose(user -> getOrders(user.getId()));     // 비동기 체이닝",
    "```",
    "",
    "### 조합",
    "",
    "```java",
    "// 두 작업 결과를 조합",
    "CompletableFuture<String> combined = profileFuture",
    "        .thenCombine(pointFuture, (profile, point) ->",
    "                profile.getName() + \"님의 포인트: \" + point);",
    "",
    "// 모든 작업 완료 대기",
    "CompletableFuture.allOf(future1, future2, future3).join();",
    "",
    "// 가장 빨리 끝나는 작업의 결과 사용",
    "CompletableFuture.anyOf(future1, future2, future3);",
    "```",
    "",
    "### 에러 처리",
    "",
    "```java",
    "CompletableFuture<UserProfile> future = CompletableFuture",
    "        .supplyAsync(() -> fetchUserProfile(userId))",
    "        .exceptionally(ex -> {",
    "            log.error(\"프로필 조회 실패\", ex);",
    "            return UserProfile.defaultProfile(); // 기본값 반환",
    "        });",
    "",
    "// 성공/실패 모두 처리",
    "CompletableFuture<UserProfile> future = CompletableFuture",
    "        .supplyAsync(() -> fetchUserProfile(userId))",
    "        .handle((result, ex) -> {",
    "            if (ex != null) {",
    "                log.error(\"실패\", ex);",
    "                return UserProfile.defaultProfile();",
    "            }",
    "            return result;",
    "        });",
    "```",
    "",
    "### 타임아웃",
    "",
    "```java",
    "// Java 9+: 5초 내에 완료되지 않으면 기본값",
    "CompletableFuture<UserProfile> future = CompletableFuture",
    "        .supplyAsync(() -> fetchUserProfile(userId))",
    "        .completeOnTimeout(UserProfile.defaultProfile(), 5, TimeUnit.SECONDS);",
    "",
    "// Java 9+: 5초 내에 완료되지 않으면 예외",
    "CompletableFuture<UserProfile> future = CompletableFuture",
    "        .supplyAsync(() -> fetchUserProfile(userId))",
    "        .orTimeout(5, TimeUnit.SECONDS);",
    "```",
    "",
    "### 메서드 정리표",
    "",
    "| 메서드 | 역할 | 반환 |",
    "|--------|------|------|",
    "| `supplyAsync(Supplier)` | 비동기 실행 (값 반환) | `CompletableFuture<T>` |",
    "| `runAsync(Runnable)` | 비동기 실행 (반환 없음) | `CompletableFuture<Void>` |",
    "| `thenApply(Function)` | 결과 변환 | `CompletableFuture<U>` |",
    "| `thenCompose(Function)` | 비동기 체이닝 | `CompletableFuture<U>` |",
    "| `thenCombine(Future, BiFunction)` | 두 결과 조합 | `CompletableFuture<V>` |",
    "| `allOf(Future...)` | 모든 작업 완료 대기 | `CompletableFuture<Void>` |",
    "| `anyOf(Future...)` | 가장 빠른 작업 결과 | `CompletableFuture<Object>` |",
    "| `exceptionally(Function)` | 에러 시 기본값 | `CompletableFuture<T>` |",
    "| `handle(BiFunction)` | 성공/실패 모두 처리 | `CompletableFuture<U>` |",
    "| `completeOnTimeout(T, long, TimeUnit)` | 타임아웃 시 기본값 | `CompletableFuture<T>` |",
    "| `orTimeout(long, TimeUnit)` | 타임아웃 시 예외 | `CompletableFuture<T>` |",
    "| `join()` | 결과 대기 (unchecked 예외) | `T` |",
    "| `get()` | 결과 대기 (checked 예외) | `T` |",
    "",
    "---",
    "",
    "## @Async vs CompletableFuture.supplyAsync",
    "",
    "| 비교 | `@Async` | `CompletableFuture.supplyAsync()` |",
    "|------|---------|----------------------------------|",
    "| 스레드 관리 | Spring이 관리 (Bean 설정) | 직접 Executor 전달 |",
    "| 트랜잭션 | 별도 트랜잭션 | 호출자 트랜잭션 상속 불가 |",
    "| AOP | Spring 프록시 기반 | 일반 Java 코드 |",
    "| 유연성 | 단순 비동기 | **체이닝, 조합, 에러 처리** 유연 |",
    "| 추천 | Service 메서드 비동기 실행 | 복잡한 비동기 로직 조합 |",
