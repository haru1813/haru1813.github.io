    "# 3. @Scheduled (스케줄링)",
    "",
    "## 개념",
    "",
    "`@Scheduled`는 메서드를 **정해진 시간/주기**에 자동으로 실행하는 어노테이션입니다. 배치 작업, 캐시 갱신, 상태 모니터링 등에 사용합니다.",
