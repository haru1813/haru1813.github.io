    "## `EventHandler<TEventArgs>` 표준 패턴",
    "",
    ".NET에서 이벤트를 선언할 때는 `EventHandler<TEventArgs>`를 사용하는 것이 표준 패턴입니다.",
    "",
    "```csharp",
    "// 1. 커스텀 EventArgs 정의",
    "class TemperatureChangedEventArgs : EventArgs",
    "{",
    "    public double OldTemp { get; }",
    "    public double NewTemp { get; }",
    "",
    "    public TemperatureChangedEventArgs(double oldTemp, double newTemp)",
    "    {",
    "        OldTemp = oldTemp;",
    "        NewTemp = newTemp;",
    "    }",
    "}",
    "",
    "// 2. 이벤트 게시자 (Publisher)",
    "class Thermostat",
    "{",
    "    private double temperature;",
    "",
    "    public event EventHandler<TemperatureChangedEventArgs> TemperatureChanged;",
    "",
    "    public double Temperature",
    "    {",
    "        get => temperature;",
    "        set",
    "        {",
    "            double old = temperature;",
    "            temperature = value;",
    "            TemperatureChanged?.Invoke(this,",
    "                new TemperatureChangedEventArgs(old, value));",
    "        }",
    "    }",
    "}",
    "",
    "// 3. 이벤트 구독자 (Subscriber)",
    "class Display",
    "{",
    "    public void OnTemperatureChanged(object sender, TemperatureChangedEventArgs e)",
    "    {",
    "        Console.WriteLine($\"온도 변경: {e.OldTemp}°C → {e.NewTemp}°C\");",
    "    }",
    "}",
    "",
    "// 4. 사용",
    "var thermostat = new Thermostat();",
    "var display = new Display();",
    "",
    "thermostat.TemperatureChanged += display.OnTemperatureChanged;",
    "thermostat.Temperature = 22.5;  // 온도 변경: 0°C → 22.5°C",
    "thermostat.Temperature = 25.0;  // 온도 변경: 22.5°C → 25°C",
    "```",
