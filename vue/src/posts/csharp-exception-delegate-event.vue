    "publisher.SomeEvent += subscriber.Handler;",
    "subscriber = null; // subscriber는 여전히 GC 불가!",
    "",
    "// 해결 1: 반드시 구독 해제",
    "publisher.SomeEvent -= subscriber.Handler;",
    "",
    "// 해결 2: WeakEventManager (WPF)",
    "// WeakEventManager<Publisher, EventArgs>.AddHandler(",
    "//     publisher, \"SomeEvent\", handler);",
    "```",
