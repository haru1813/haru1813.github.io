    "## 이벤트 (event)",
    "",
    "델리게이트에 `event` 키워드를 추가하면, **클래스 외부에서 직접 호출할 수 없고** `+=`/`-=`로만 구독/해제가 가능합니다.",
    "",
    "```csharp",
    "class MyTimer",
    "{",
    "    public delegate void Tick(DateTime now);",
    "    public event Tick Ticked; // event 키워드 추가",
    "    private int interval;",
    "",
    "    public MyTimer(int interval)",
    "    {",
    "        this.interval = interval;",
    "    }",
    "",
    "    public void Start()",
    "    {",
    "        DateTime before = DateTime.Now;",
    "        while (true)",
    "        {",
    "            DateTime current = DateTime.Now;",
    "            if (before.AddSeconds(interval) < current)",
    "            {",
    "                Ticked?.Invoke(current);",
    "                before = current;",
    "            }",
    "        }",
    "    }",
    "}",
    "",
