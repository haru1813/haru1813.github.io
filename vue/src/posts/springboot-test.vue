    "",
    "| 어노테이션/API | 역할 |",
    "|--------------|------|",
    "| `@ExtendWith(MockitoExtension.class)` | Mockito를 JUnit 5에서 사용하도록 설정 |",
    "| `@Mock` | 가짜(Mock) 객체 생성 |",
    "| `@InjectMocks` | Mock 객체를 주입받는 테스트 대상 생성 |",
