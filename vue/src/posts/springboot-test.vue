    "### @WebMvcTest vs @SpringBootTest",
    "",
    "| 항목 | @WebMvcTest | @SpringBootTest |",
    "|------|------------|----------------|",
    "| 로드 범위 | Controller + 관련 설정만 | **전체** 애플리케이션 컨텍스트 |",
    "| Service/Repository | `@MockBean`으로 Mock | **실제 빈** 사용 |",
    "| 속도 | **빠름** | 느림 |",
    "| 용도 | Controller 로직, 요청/응답 검증 | 전체 통합 흐름 검증 |",
    "",
    "### @AutoConfigureMockMvc란?",
    "",
    "`@SpringBootTest`는 기본적으로 `MockMvc`를 구성하지 않습니다. `@AutoConfigureMockMvc`를 추가하면 **MockMvc 빈이 자동으로 생성**되어 `@Autowired`로 주입받을 수 있습니다.",
    "",
    "```java",
    "@SpringBootTest",
    "@AutoConfigureMockMvc",
    "class UserIntegrationMvcTest {",
    "",
    "    @Autowired",
    "    private MockMvc mockMvc;",
    "",
    "    @Test",
    "    @DisplayName(\"실제 Service/Repository를 사용한 통합 테스트\")",
    "    void getUserIntegration() throws Exception {",
    "        mockMvc.perform(get(\"/api/users/1\"))",
    "                .andExpect(status().isOk())",
    "                .andExpect(jsonPath(\"$.name\").exists());",
    "    }",
    "}",
    "```",
    "",
    "| 조합 | MockMvc | 로드 범위 | 용도 |",
    "|------|---------|----------|------|",
    "| `@WebMvcTest` | **자동 포함** | Controller 계층만 | 슬라이스 테스트 (Service는 Mock) |",
    "| `@SpringBootTest` | **없음** | 전체 | `TestRestTemplate` 등으로 통합 테스트 |",
    "| `@SpringBootTest` + `@AutoConfigureMockMvc` | **자동 포함** | 전체 | **실제 빈으로 MockMvc 통합 테스트** |",
    "",
    "> `@WebMvcTest`는 Controller만 로드하므로 Service/Repository를 `@MockBean`으로 Mock해야 합니다. 반면 `@SpringBootTest` + `@AutoConfigureMockMvc`는 **실제 Service, Repository가 모두 동작**하는 상태에서 MockMvc로 요청을 보내므로, DB까지 포함한 **통합 테스트**에 적합합니다.",
