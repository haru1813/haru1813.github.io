    "### UserService.java",
    "",
    "```java",
    "package com.example.demo.service;",
    "",
    "import com.example.demo.dto.CreateUserRequest;",
    "import com.example.demo.dto.UserResponse;",
    "import com.example.demo.exception.DuplicateEmailException;",
    "import com.example.demo.exception.UserNotFoundException;",
    "import lombok.extern.slf4j.Slf4j;",
    "import org.springframework.stereotype.Service;",
    "",
    "import java.util.ArrayList;",
    "import java.util.List;",
    "import java.util.Map;",
    "import java.util.concurrent.ConcurrentHashMap;",
    "import java.util.concurrent.atomic.AtomicLong;",
    "",
    "@Slf4j",
    "@Service",
    "public class UserService {",
    "",
    "    private final Map<Long, UserResponse> users = new ConcurrentHashMap<>();",
    "    private final AtomicLong idGenerator = new AtomicLong(1);",
    "",
    "    public UserResponse create(CreateUserRequest request) {",
    "        // 이메일 중복 체크",
    "        boolean emailExists = users.values().stream()",
    "                .anyMatch(u -> u.getEmail().equals(request.getEmail()));",
    "        if (emailExists) {",
    "            throw new DuplicateEmailException(request.getEmail());",
    "        }",
    "",
    "        Long id = idGenerator.getAndIncrement();",
    "        UserResponse user = new UserResponse(id, request.getName(), request.getEmail(), request.getAge());",
    "        users.put(id, user);",
    "        log.info(\"사용자 생성: {} ({})\", request.getName(), request.getEmail());",
    "        return user;",
    "    }",
    "",
    "    public UserResponse findById(Long id) {",
    "        UserResponse user = users.get(id);",
    "        if (user == null) {",
    "            throw new UserNotFoundException(id);",
    "        }",
    "        return user;",
    "    }",
