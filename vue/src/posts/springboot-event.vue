    "# 4. @TransactionalEventListener (트랜잭션 연동)",
    "",
    "## 문제 상황",
    "",
    "기본 `@EventListener`는 **이벤트 발행 즉시** 리스너가 실행됩니다. 만약 트랜잭션이 롤백되면, 이미 발송된 이메일은 취소할 수 없습니다.",
    "",
