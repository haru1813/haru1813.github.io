    "",
    "`@TransactionalEventListener`는 **트랜잭션이 커밋된 후**에 리스너를 실행합니다.",
    "",
    "```mermaid",
    "sequenceDiagram",
    "    participant S as MemberService",
    "    participant DB as DB",
    "    participant L as EmailListener",
    "",
    "    Note over S,L: @TransactionalEventListener (안전)",
    "    S->>DB: 회원 저장",
    "    S->>S: 이벤트 발행 (대기 중...)",
