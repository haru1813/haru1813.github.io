    "## 주의사항",
    "",
    "### 1. @EventListener는 기본 동기",
    "리스너가 예외를 던지면 발행자에게 전파됩니다. 부가 로직의 실패가 핵심 로직에 영향을 주지 않으려면 `@Async`를 사용하세요.",
    "",
    "### 2. @TransactionalEventListener는 트랜잭션 없으면 무시",
    "`publishEvent()`가 `@Transactional` 메서드 밖에서 호출되면 `@TransactionalEventListener`는 **실행되지 않습니다**.",
    "",
    "```java",
    "// ❌ 트랜잭션 없음 → @TransactionalEventListener 동작 안 함",
    "public void someMethod() {",
    "    eventPublisher.publishEvent(new SomeEvent());",
    "}",
    "",
    "// ✅ 트랜잭션 있음 → 정상 동작",
    "@Transactional",
    "public void someMethod() {",
    "    eventPublisher.publishEvent(new SomeEvent());",
    "}",
    "```",
    "",
    "> `fallbackExecution = true`를 설정하면 트랜잭션 없이도 실행됩니다.",
    "",
    "```java",
    "@TransactionalEventListener(fallbackExecution = true)",
    "public void handle(SomeEvent event) { ... }",
    "```",
