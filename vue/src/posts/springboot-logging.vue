    "## 비동기 로깅 (AsyncAppender)",
    "",
    "로그 출력은 I/O 작업이므로 **동기 방식**에서는 성능에 영향을 줄 수 있습니다. **AsyncAppender**를 사용하면 로그를 **큐에 저장**하고 별도 스레드에서 출력합니다.",
    "",
    "```mermaid",
    "flowchart LR",
    "    A[\"애플리케이션 스레드\"] -->|\"log.info()\"| B[\"AsyncAppender<br/>(큐: BlockingQueue)\"]",
    "    B -->|\"별도 스레드\"| C[\"FileAppender<br/>(파일 I/O)\"]",
    "",
    "    style B fill:#FF9800,color:#fff",
    "```",
    "",
    "### AsyncAppender 설정",
    "",
    "```xml",
    "<configuration>",
    "",
    "    <!-- 기존 파일 Appender -->",
    "    <appender name=\"FILE\" class=\"ch.qos.logback.core.rolling.RollingFileAppender\">",
    "        <file>logs/application.log</file>",
    "        <encoder>",
    "            <pattern>%d{HH:mm:ss.SSS} %-5level [%thread] %logger{36} - %msg%n</pattern>",
    "        </encoder>",
    "        <rollingPolicy class=\"ch.qos.logback.core.rolling.TimeBasedRollingPolicy\">",
    "            <fileNamePattern>logs/application.%d{yyyy-MM-dd}.log</fileNamePattern>",
    "            <maxHistory>30</maxHistory>",
    "        </rollingPolicy>",
    "    </appender>",
    "",
    "    <!-- 비동기 Appender -->",
    "    <appender name=\"ASYNC_FILE\" class=\"ch.qos.logback.classic.AsyncAppender\">",
    "        <queueSize>512</queueSize>",
    "        <discardingThreshold>0</discardingThreshold>",
    "        <includeCallerData>false</includeCallerData>",
    "        <neverBlock>true</neverBlock>",
    "        <appender-ref ref=\"FILE\" />",
    "    </appender>",
    "",
    "    <root level=\"INFO\">",
    "        <appender-ref ref=\"CONSOLE\" />",
    "        <appender-ref ref=\"ASYNC_FILE\" />",
    "    </root>",
    "",
    "</configuration>",
    "```",
    "",
    "| 설정 | 설명 | 기본값 |",
    "|------|------|-------|",
    "| `queueSize` | 큐 크기 (로그 메시지 버퍼) | 256 |",
    "| `discardingThreshold` | 큐가 이 비율 이하로 남으면 TRACE/DEBUG 폐기 | 20% |",
    "| `includeCallerData` | 호출자 정보 포함 (성능 저하) | false |",
    "| `neverBlock` | 큐 가득 차도 블로킹하지 않음 | false |",
    "",
    "### 동기 vs 비동기 성능 비교",
    "",
    "| 항목 | 동기 (FileAppender) | 비동기 (AsyncAppender) |",
    "|------|--------------------|-----------------------|",
    "| 로그 출력 | 즉시 파일 쓰기 | 큐에 저장 후 별도 스레드에서 쓰기 |",
    "| 응답 시간 영향 | 있음 (I/O 대기) | 거의 없음 |",
    "| 로그 유실 가능성 | 없음 | 큐 초과 시 가능 |",
    "| 적합한 환경 | 개발, 소규모 | 운영, 대규모 트래픽 |",
