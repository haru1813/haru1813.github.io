    "## MDC (Mapped Diagnostic Context)",
    "",
    "**MDC**는 요청별 **고유 식별자(traceId)**를 로그에 자동으로 포함시키는 기능입니다. 분산 환경에서 하나의 요청 흐름을 추적할 때 필수적입니다.",
    "",
    "```mermaid",
    "sequenceDiagram",
    "    participant C as 클라이언트",
    "    participant F as MDC Filter",
    "    participant Ctrl as Controller",
    "    participant Svc as Service",
    "    participant Log as 로그 파일",
    "",
    "    C->>F: HTTP 요청",
    "    F->>F: traceId 생성 (UUID)",
    "    F->>F: MDC.put(\"traceId\", uuid)",
    "    F->>Ctrl: 요청 전달",
    "    Ctrl->>Svc: 비즈니스 로직",
    "    Svc->>Log: log.info(\"처리 완료\")",
    "    Note over Log: [abc-123] 처리 완료",
    "    F->>F: MDC.clear()",
    "```",
    "",
    "### MDC Filter 구현",
    "",
    "```java",
    "import org.slf4j.MDC;",
    "import jakarta.servlet.*;",
    "import jakarta.servlet.http.HttpServletRequest;",
    "import org.springframework.stereotype.Component;",
    "import java.io.IOException;",
    "import java.util.UUID;",
    "",
    "@Component",
    "public class MdcFilter implements Filter {",
    "",
    "    @Override",
    "    public void doFilter(ServletRequest request, ServletResponse response,",
    "                         FilterChain chain) throws IOException, ServletException {",
    "        try {",
    "            String traceId = UUID.randomUUID().toString().substring(0, 8);",
    "            MDC.put(\"traceId\", traceId);",
    "",
    "            HttpServletRequest httpRequest = (HttpServletRequest) request;",
    "            MDC.put(\"requestURI\", httpRequest.getRequestURI());",
    "            MDC.put(\"method\", httpRequest.getMethod());",
    "",
    "            chain.doFilter(request, response);",
    "        } finally {",
    "            MDC.clear();",
    "        }",
    "    }",
    "}",
    "```",
    "",
    "### 로그 패턴에 MDC 값 추가",
    "",
    "```xml",
    "<!-- logback-spring.xml 패턴에 %X{traceId} 추가 -->",
    "<property name=\"FILE_PATTERN\"",
    "          value=\"%d{HH:mm:ss.SSS} %-5level [%thread] [%X{traceId}] %logger{36} - %msg%n\" />",
