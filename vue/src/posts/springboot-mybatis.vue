    "# 5. ResultMap (복잡한 매핑)",
    "",
    "컬럼명과 필드명이 다르거나 **연관 관계(1:1, 1:N)**를 매핑할 때 사용합니다.",
    "",
    "## 기본 ResultMap",
    "",
    "```xml",
    "<resultMap id=\"userResultMap\" type=\"User\">",
    "    <id property=\"id\" column=\"user_id\" />",
    "    <result property=\"name\" column=\"user_name\" />",
    "    <result property=\"email\" column=\"user_email\" />",
    "</resultMap>",
    "",
    "<select id=\"findAll\" resultMap=\"userResultMap\">",
    "    SELECT user_id, user_name, user_email FROM users",
    "</select>",
    "```",
    "",
    "## 1:1 관계 (association)",
    "",
    "```xml",
    "<resultMap id=\"userWithDeptMap\" type=\"User\">",
    "    <id property=\"id\" column=\"id\" />",
    "    <result property=\"name\" column=\"name\" />",
    "    <association property=\"department\" javaType=\"Department\">",
    "        <id property=\"id\" column=\"dept_id\" />",
    "        <result property=\"deptName\" column=\"dept_name\" />",
    "    </association>",
    "</resultMap>",
    "",
    "<select id=\"findUserWithDepartment\" resultMap=\"userWithDeptMap\">",
    "    SELECT u.id, u.name, d.id AS dept_id, d.dept_name",
    "    FROM users u",
    "    JOIN departments d ON u.department_id = d.id",
    "    WHERE u.id = #{id}",
    "</select>",
    "```",
    "",
    "## 1:N 관계 (collection)",
    "",
    "```xml",
    "<resultMap id=\"userWithOrdersMap\" type=\"User\">",
    "    <id property=\"id\" column=\"id\" />",
    "    <result property=\"name\" column=\"name\" />",
    "    <collection property=\"orders\" ofType=\"Order\">",
    "        <id property=\"id\" column=\"order_id\" />",
    "        <result property=\"productName\" column=\"product_name\" />",
    "        <result property=\"price\" column=\"price\" />",
    "    </collection>",
    "</resultMap>",
    "",
    "<select id=\"findUserWithOrders\" resultMap=\"userWithOrdersMap\">",
    "    SELECT u.id, u.name, o.id AS order_id, o.product_name, o.price",
    "    FROM users u",
    "    LEFT JOIN orders o ON u.id = o.user_id",
    "    WHERE u.id = #{id}",
    "</select>",
    "```",
