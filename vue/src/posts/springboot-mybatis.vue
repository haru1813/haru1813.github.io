    "# 7. TypeHandler (커스텀 타입 변환)",
    "",
    "Java 타입과 DB 타입 간 변환 규칙을 정의합니다.",
    "",
    "```java",
    "@MappedTypes(YesNo.class)",
    "public class YesNoTypeHandler extends BaseTypeHandler<Boolean> {",
    "",
    "    @Override",
    "    public void setNonNullParameter(PreparedStatement ps, int i,",
    "                                     Boolean parameter, JdbcType jdbcType) throws SQLException {",
    "        ps.setString(i, parameter ? \"Y\" : \"N\");",
    "    }",
    "",
    "    @Override",
    "    public Boolean getNullableResult(ResultSet rs, String columnName) throws SQLException {",
    "        return \"Y\".equals(rs.getString(columnName));",
    "    }",
    "",
    "    @Override",
    "    public Boolean getNullableResult(ResultSet rs, int columnIndex) throws SQLException {",
    "        return \"Y\".equals(rs.getString(columnIndex));",
    "    }",
    "",
    "    @Override",
    "    public Boolean getNullableResult(CallableStatement cs, int columnIndex) throws SQLException {",
    "        return \"Y\".equals(cs.getString(columnIndex));",
    "    }",
    "}",
    "```",
    "",
    "```yaml",
    "mybatis:",
    "  type-handlers-package: com.example.demo.typehandler",
    "```",
    "",
