    "# 3. Service & Controller",
    "",
    "## Service",
    "",
    "```java",
    "@Service",
    "@RequiredArgsConstructor",
    "public class UserService {",
    "",
    "    private final UserMapper userMapper;",
    "",
    "    public List<UserResponse> findAll() {",
    "        return userMapper.findAll().stream()",
    "                .map(UserResponse::from)",
    "                .toList();",
    "    }",
    "",
    "    public UserResponse findById(Long id) {",
    "        User user = userMapper.findById(id)",
    "                .orElseThrow(() -> new RuntimeException(\"사용자를 찾을 수 없습니다: \" + id));",
    "        return UserResponse.from(user);",
    "    }",
    "",
    "    @Transactional",
    "    public UserResponse create(CreateUserRequest request) {",
    "        userMapper.findByEmail(request.getEmail())",
    "                .ifPresent(u -> {",
    "                    throw new RuntimeException(\"이미 존재하는 이메일: \" + request.getEmail());",
    "                });",
    "",
    "        User user = User.builder()",
    "                .name(request.getName())",
    "                .email(request.getEmail())",
    "                .age(request.getAge())",
    "                .department(request.getDepartment())",
    "                .build();",
    "",
    "        userMapper.insert(user);  // user.getId()에 자동 생성된 ID가 세팅됨",
    "        return UserResponse.from(user);",
    "    }",
    "",
    "    @Transactional",
    "    public UserResponse update(Long id, CreateUserRequest request) {",
    "        User user = userMapper.findById(id)",
    "                .orElseThrow(() -> new RuntimeException(\"사용자를 찾을 수 없습니다\"));",
    "",
    "        user.setName(request.getName());",
    "        user.setEmail(request.getEmail());",
    "        user.setAge(request.getAge());",
    "        user.setDepartment(request.getDepartment());",
    "",
    "        userMapper.update(user);",
    "        return UserResponse.from(user);",
    "    }",
    "",
    "    @Transactional",
    "    public void delete(Long id) {",
    "        int deleted = userMapper.deleteById(id);",
    "        if (deleted == 0) {",
    "            throw new RuntimeException(\"삭제할 사용자가 없습니다: \" + id);",
    "        }",
    "    }",
    "}",
