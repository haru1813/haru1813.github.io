    "",
    "import java.util.List;",
    "",
    "@Service",
    "@RequiredArgsConstructor",
    "public class MemberService {",
    "",
    "    private final MemberMapper memberMapper;",
    "",
    "    public List<MemberResponse> findAll() {",
    "        return memberMapper.findAll().stream()",
    "                .map(MemberResponse::from)",
    "                .toList();",
    "    }",
    "",
    "    public MemberResponse findById(Long id) {",
    "        Member member = memberMapper.findById(id)",
    "                .orElseThrow(() -> new RuntimeException(\"회원을 찾을 수 없습니다: \" + id));",
    "        return MemberResponse.from(member);",
    "    }",
    "",
    "    @Transactional",
    "    public MemberResponse create(MemberCreateRequest request) {",
    "        memberMapper.findByEmail(request.getEmail())",
    "                .ifPresent(m -> {",
    "                    throw new RuntimeException(\"이미 존재하는 이메일: \" + request.getEmail());",
    "                });",
    "",
    "        Member member = Member.builder()",
    "                .name(request.getName())",
    "                .email(request.getEmail())",
    "                .age(request.getAge())",
    "                .build();",
    "",
    "        memberMapper.insert(member);",
    "        return MemberResponse.from(member);",
    "    }",
    "",
    "    @Transactional",
    "    public MemberResponse update(Long id, MemberUpdateRequest request) {",
    "        Member member = memberMapper.findById(id)",
    "                .orElseThrow(() -> new RuntimeException(\"회원을 찾을 수 없습니다\"));",
    "",
    "        member.setName(request.getName());",
    "        member.setEmail(request.getEmail());",
    "        member.setAge(request.getAge());",
    "",
    "        memberMapper.update(member);",
    "        return MemberResponse.from(member);",
    "    }",
    "",
    "    @Transactional",
    "    public void delete(Long id) {",
    "        int deleted = memberMapper.deleteById(id);",
    "        if (deleted == 0) {",
    "            throw new RuntimeException(\"삭제할 회원이 없습니다: \" + id);",
    "        }",
    "    }",
