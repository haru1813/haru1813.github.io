    "# 4. 동적 SQL",
    "",
    "MyBatis의 가장 강력한 기능입니다. 조건에 따라 SQL을 동적으로 조합합니다.",
    "",
    "## `<if>` (조건부 절)",
    "",
    "```xml",
    "<select id=\"findByCondition\" resultType=\"User\">",
    "    SELECT * FROM users",
    "    WHERE 1=1",
    "    <if test=\"name != null and name != ''\">",
    "        AND name LIKE CONCAT('%', #{name}, '%')",
    "    </if>",
    "    <if test=\"department != null\">",
    "        AND department = #{department}",
    "    </if>",
    "    <if test=\"minAge != null\">",
    "        AND age >= #{minAge}",
    "    </if>",
    "    ORDER BY id DESC",
    "</select>",
    "```",
    "",
    "## `<where>` (WHERE 자동 생성)",
    "",
    "`WHERE 1=1` 대신 `<where>` 태그를 사용하면 첫 번째 조건의 `AND`를 자동으로 제거합니다.",
    "",
    "```xml",
    "<select id=\"findByCondition\" resultType=\"User\">",
    "    SELECT * FROM users",
    "    <where>",
    "        <if test=\"name != null and name != ''\">",
    "            AND name LIKE CONCAT('%', #{name}, '%')",
    "        </if>",
    "        <if test=\"department != null\">",
    "            AND department = #{department}",
    "        </if>",
    "        <if test=\"minAge != null\">",
    "            AND age >= #{minAge}",
    "        </if>",
    "    </where>",
    "    ORDER BY id DESC",
    "</select>",
    "```",
    "",
    "## `<choose>` / `<when>` / `<otherwise>` (switch-case)",
    "",
    "```xml",
    "<select id=\"findBySort\" resultType=\"User\">",
    "    SELECT * FROM users",
    "    ORDER BY",
    "    <choose>",
    "        <when test=\"sortBy == 'name'\">name</when>",
    "        <when test=\"sortBy == 'age'\">age DESC</when>",
    "        <otherwise>id DESC</otherwise>",
    "    </choose>",
    "</select>",
    "```",
    "",
    "## `<set>` (동적 UPDATE)",
    "",
    "```xml",
    "<update id=\"updateSelective\" parameterType=\"User\">",
    "    UPDATE users",
    "    <set>",
    "        <if test=\"name != null\">name = #{name},</if>",
    "        <if test=\"email != null\">email = #{email},</if>",
    "        <if test=\"age != null\">age = #{age},</if>",
    "        <if test=\"department != null\">department = #{department},</if>",
    "        updated_at = CURRENT_TIMESTAMP,",
    "    </set>",
    "    WHERE id = #{id}",
    "</update>",
    "```",
    "",
    "> `<set>` 태그는 마지막 쉼표를 자동으로 제거합니다.",
    "",
    "## `<foreach>` (IN 절, 배치 INSERT)",
    "",
    "```xml",
    "<!-- IN 절 -->",
    "<select id=\"findByIds\" resultType=\"User\">",
    "    SELECT * FROM users",
    "    WHERE id IN",
    "    <foreach collection=\"ids\" item=\"id\" open=\"(\" separator=\",\" close=\")\">",
    "        #{id}",
    "    </foreach>",
    "</select>",
    "",
    "<!-- 배치 INSERT -->",
    "<insert id=\"insertBatch\" parameterType=\"list\">",
    "    INSERT INTO users (name, email, age, department)",
    "    VALUES",
    "    <foreach collection=\"list\" item=\"user\" separator=\",\">",
    "        (#{user.name}, #{user.email}, #{user.age}, #{user.department})",
    "    </foreach>",
    "</insert>",
    "```",
    "",
    "### `<foreach>` 속성",
    "",
    "| 속성 | 설명 | 예시 |",
    "|------|------|------|",
    "| `collection` | 파라미터명 | `\"ids\"`, `\"list\"` |",
    "| `item` | 반복 변수명 | `\"id\"`, `\"user\"` |",
    "| `open` | 시작 문자 | `\"(\"` |",
    "| `close` | 종료 문자 | `\")\"` |",
    "| `separator` | 구분자 | `\",\"` |",
    "",
    "## `<sql>` / `<include>` (재사용 SQL 조각)",
    "",
    "```xml",
    "<sql id=\"userColumns\">",
    "    id, name, email, age, department, created_at, updated_at",
    "</sql>",
    "",
    "<select id=\"findAll\" resultType=\"User\">",
    "    SELECT <include refid=\"userColumns\" />",
    "    FROM users",
    "    ORDER BY id DESC",
    "</select>",
    "",
    "<select id=\"findById\" resultType=\"User\">",
    "    SELECT <include refid=\"userColumns\" />",
    "    FROM users",
    "    WHERE id = #{id}",
    "</select>",
    "```",
    "",
    "### 동적 SQL 태그 정리",
    "",
    "| 태그 | 역할 | 예시 |",
    "|------|------|------|",
    "| `<if>` | 조건부 포함 | `test=\"name != null\"` |",
    "| `<where>` | WHERE + 불필요한 AND/OR 제거 | `<where><if>...</if></where>` |",
    "| `<set>` | SET + 불필요한 쉼표 제거 | 동적 UPDATE |",
    "| `<choose>` | switch-case | `<when>`, `<otherwise>` |",
    "| `<foreach>` | 반복 (IN, 배치) | `collection`, `item`, `separator` |",
    "| `<sql>` / `<include>` | SQL 조각 재사용 | 공통 컬럼 목록 |",
    "| `<trim>` | prefix/suffix 커스텀 | WHERE/SET의 일반화 |",
