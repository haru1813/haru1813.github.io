    "",
    "import com.example.springboot_mybatis_example.domain.Member;",
    "import com.example.springboot_mybatis_example.dto.MemberCreateRequest;",
    "import com.example.springboot_mybatis_example.dto.MemberResponse;",
    "import com.example.springboot_mybatis_example.dto.MemberUpdateRequest;",
    "import com.example.springboot_mybatis_example.mapper.MemberMapper;",
