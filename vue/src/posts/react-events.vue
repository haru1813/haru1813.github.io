    "  };",
    "",
    "  return (",
    "    <div>",
    "      <input",
    "        value={input}",
    "        onChange={(e) => setInput(e.target.value)}",
    "        onKeyDown={handleKeyDown}",
    "        placeholder=\"Enter: 제출, Esc: 초기화, Ctrl+S: 저장\"",
    "      />",
    "      <p>최근 키: {pressedKeys.join(' → ')}</p>",
    "    </div>",
    "  );",
    "}",
