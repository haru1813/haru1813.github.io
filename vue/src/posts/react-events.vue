    "## 마우스 이벤트",
    "",
    "### 이벤트 종류",
    "",
    "| 이벤트 | 발생 시점 |",
    "|--------|----------|",
    "| `onClick` | 클릭 |",
    "| `onDoubleClick` | 더블 클릭 |",
    "| `onMouseDown` / `onMouseUp` | 버튼 누름 / 뗌 |",
    "| `onMouseEnter` / `onMouseLeave` | 요소 진입 / 이탈 (버블링 없음) |",
    "| `onMouseOver` / `onMouseOut` | 요소 위 / 밖 (버블링 있음) |",
    "| `onMouseMove` | 요소 위에서 이동 |",
    "| `onContextMenu` | 우클릭 (컨텍스트 메뉴) |",
    "",
    "### 마우스 위치 추적",
    "",
    "**JavaScript**",
    "```jsx",
    "import { useState } from 'react';",
    "",
    "function MouseTracker() {",
    "  const [pos, setPos] = useState({ x: 0, y: 0 });",
    "  const [isOver, setIsOver] = useState(false);",
    "",
    "  return (",
    "    <div",
    "      onMouseMove={(e) => setPos({ x: e.clientX, y: e.clientY })}",
    "      onMouseEnter={() => setIsOver(true)}",
    "      onMouseLeave={() => setIsOver(false)}",
    "      style={{",
    "        width: '100%', height: '200px',",
    "        background: isOver ? '#e8f4fd' : '#f5f5f5',",
    "        display: 'flex', alignItems: 'center', justifyContent: 'center',",
    "        border: '2px dashed #3498db', borderRadius: '8px',",
    "        cursor: 'crosshair',",
    "      }}",
    "    >",
    "      <p>{isOver ? `마우스 위치: (${pos.x}, ${pos.y})` : '마우스를 올려보세요'}</p>",
    "    </div>",
    "  );",
    "}",
    "```",
    "",
    "**TypeScript**",
    "```tsx",
