    "## 2. v-bind — 속성 바인딩",
    "",
    "HTML 속성에 동적 값을 바인딩합니다. 단축 문법은 `:`입니다.",
    "",
    "```vue",
    "\x3cscript setup lang=\"ts\">",
    "import { ref } from 'vue'",
    "",
    "const imgUrl = ref('https://example.com/img.jpg')",
    "const isDisabled = ref(true)",
    "const dynamicClass = ref('active')",
    "\x3c/script>",
    "",
    "\x3ctemplate>",
    "  <!-- 전체 문법 -->",
    "  <img v-bind:src=\"imgUrl\" />",
    "",
    "  <!-- 단축 문법 (권장) -->",
    "  <img :src=\"imgUrl\" />",
    "  <button :disabled=\"isDisabled\">버튼</button>",
    "  <div :class=\"dynamicClass\">동적 클래스</div>",
    "\x3c/template>",
    "```",
    "",
