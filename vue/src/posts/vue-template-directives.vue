    "",
    "## 1. 텍스트 보간법",
    "",
    "```vue",
    "\x3cscript setup lang=\"ts\">",
    "import { ref } from 'vue'",
    "",
    "const message = ref('안녕하세요')",
    "const count = ref(42)",
    "\x3c/script>",
    "",
    "\x3ctemplate>",
