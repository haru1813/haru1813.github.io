    "## 3. v-on — 이벤트 핸들링",
    "",
    "DOM 이벤트를 처리합니다. 단축 문법은 `@`입니다.",
    "",
    "```vue",
    "\x3cscript setup lang=\"ts\">",
    "import { ref } from 'vue'",
    "",
    "const count = ref(0)",
    "",
    "const handleClick = (event: MouseEvent): void => {",
    "  console.log('클릭!', event.target)",
    "}",
    "\x3c/script>",
    "",
    "\x3ctemplate>",
    "  <!-- 전체 문법 -->",
    "  <button v-on:click=\"count++\">{{ count }}</button>",
    "",
    "  <!-- 단축 문법 (권장) -->",
    "  <button @click=\"count++\">인라인</button>",
    "  <button @click=\"handleClick\">메서드</button>",
    "  <button @click=\"() => count = 0\">초기화</button>",
    "\x3c/template>",
    "```",
    "",
