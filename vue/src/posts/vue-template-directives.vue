    "",
    "```vue",
    "\x3cscript setup lang=\"ts\">",
    "import { ref } from 'vue'",
    "",
    "const isActive = ref(true)",
    "const hasError = ref(false)",
    "\x3c/script>",
    "",
    "\x3ctemplate>",
    "  <!-- 객체 문법 -->",
    "  <div :class=\"{ active: isActive, error: hasError }\">객체</div>",
    "",
    "  <!-- 배열 문법 -->",
    "  <div :class=\"[isActive ? 'active' : '', 'base']\">배열</div>",
