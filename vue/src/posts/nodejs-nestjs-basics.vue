    "## 핵심 구조: Module / Controller / Service",
    "",
    "```",
    "Module (모듈)  ←  애플리케이션의 기능 단위",
    "  ├── Controller  ←  HTTP 요청/응답 처리",
    "  └── Service     ←  비즈니스 로직",
