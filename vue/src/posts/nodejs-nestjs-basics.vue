    "",
    "@Injectable()",
    "export class BlogFileRepository implements BlogRepository {",
    "  FILE_NAME = './src/blog.data.json';",
    "",
    "  async getAllPost(): Promise<PostDto[]> {",
    "    const datas = await readFile(this.FILE_NAME, 'utf8');",
