    "    {",
    "        yield return numbers[0]; // 1",
    "        yield return numbers[1]; // 2",
    "        yield return numbers[2]; // 3",
    "        yield break;             // 여기서 종료",
    "        yield return numbers[3]; // 실행되지 않음",
    "    }",
    "}",
    "",
    "var obj = new MyEnumerator();",
    "foreach (int i in obj)",
    "    Console.Write($\"{i} \"); // 1 2 3",
