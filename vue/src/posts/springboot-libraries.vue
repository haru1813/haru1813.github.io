    "# 2. ModelMapper (객체 변환)",
    "",
    "## ModelMapper란?",
    "",
    "**ModelMapper**는 Java 객체 간의 **필드를 자동으로 복사**해주는 라이브러리입니다. Entity ↔ DTO 변환에서 반복적인 getter/setter 코드를 줄여줍니다.",
    "",
    "```mermaid",
    "flowchart LR",
    "    A[\"User Entity<br/>{id, name, email, password, createdAt}\"] -->|ModelMapper| B[\"UserResponse DTO<br/>{id, name, email}\"]",
    "    C[\"CreateUserRequest DTO<br/>{name, email, age}\"] -->|ModelMapper| D[\"User Entity<br/>{name, email, age}\"]",
    "```",
    "",
    "## 의존성",
    "",
    "```groovy",
    "dependencies {",
    "    implementation 'org.modelmapper:modelmapper:3.2.0'",
    "}",
    "```",
    "",
    "## Bean 등록",
    "",
    "```java",
    "package com.example.demo.config;",
    "",
    "import org.modelmapper.ModelMapper;",
    "import org.modelmapper.convention.MatchingStrategies;",
    "import org.springframework.context.annotation.Bean;",
    "import org.springframework.context.annotation.Configuration;",
    "",
    "@Configuration",
    "public class AppConfig {",
    "",
    "    @Bean",
    "    public ModelMapper modelMapper() {",
    "        ModelMapper mapper = new ModelMapper();",
    "        mapper.getConfiguration()",
    "                .setMatchingStrategy(MatchingStrategies.STRICT)",
    "                .setFieldMatchingEnabled(true)",
    "                .setFieldAccessLevel(org.modelmapper.config.Configuration.AccessLevel.PRIVATE);",
    "        return mapper;",
    "    }",
    "}",
    "```",
    "",
    "### 매칭 전략",
    "",
    "| 전략 | 설명 | 추천 |",
    "|------|------|------|",
    "| `STANDARD` | 기본값. 소스와 대상 필드명을 지능적으로 매칭 | 일반적인 경우 |",
    "| `STRICT` | 필드명이 정확히 일치해야 매핑 | **권장** (예상치 못한 매핑 방지) |",
