    "## Gson vs Jackson 비교",
    "",
    "| 비교 | Gson | Jackson |",
    "|------|------|---------|",
    "| 개발사 | Google | FasterXML |",
    "| Spring Boot 기본 | X | **O** (자동 포함) |",
    "| API 스타일 | `toJson()` / `fromJson()` | `writeValueAsString()` / `readValue()` |",
    "| 설정 복잡도 | 단순 | 다양한 설정 가능 |",
    "| 성능 | 중간 | **빠름** (스트리밍 지원) |",
    "| 어노테이션 | `@SerializedName`, `@Expose` | `@JsonProperty`, `@JsonIgnore` |",
    "| 용량 | 가벼움 (약 250KB) | 상대적으로 큼 |",
    "| 추천 | 외부 JSON 파싱, 단순 변환 | Spring Boot 기본, REST API |",
