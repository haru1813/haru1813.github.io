    "## 고급 사용법",
    "",
    "### 필드명이 다른 경우 (커스텀 매핑)",
    "",
    "```java",
    "// Entity",
    "@Getter @Setter",
    "public class Order {",
    "    private Long id;",
    "    private String productName;",
    "    private int totalPrice;",
    "    private User buyer;",
    "}",
    "",
    "// DTO (필드명이 다름)",
    "@Getter @Setter",
    "public class OrderResponse {",
    "    private Long orderId;       // id → orderId",
    "    private String itemName;    // productName → itemName",
    "    private int price;          // totalPrice → price",
    "    private String buyerName;   // buyer.name → buyerName",
    "}",
    "```",
    "",
    "```java",
    "@Bean",
    "public ModelMapper modelMapper() {",
    "    ModelMapper mapper = new ModelMapper();",
    "",
    "    // Order → OrderResponse 커스텀 매핑 규칙",
    "    mapper.createTypeMap(Order.class, OrderResponse.class)",
    "            .addMapping(Order::getId, OrderResponse::setOrderId)",
    "            .addMapping(Order::getProductName, OrderResponse::setItemName)",
    "            .addMapping(Order::getTotalPrice, OrderResponse::setPrice)",
    "            .addMapping(",
    "                src -> src.getBuyer().getName(),",
    "                OrderResponse::setBuyerName",
    "            );",
    "",
    "    return mapper;",
    "}",
    "```",
    "",
    "### 특정 필드 제외 (skip)",
    "",
    "```java",
    "mapper.createTypeMap(User.class, UserResponse.class)",
    "        .addMappings(m -> m.skip(UserResponse::setEmail));",
    "```",
    "",
    "### 변환 후 추가 처리 (Converter)",
    "",
    "```java",
    "mapper.createTypeMap(User.class, UserResponse.class)",
    "        .setPostConverter(context -> {",
    "            UserResponse dest = context.getDestination();",
    "            // 이름을 마스킹",
    "            String name = dest.getName();",
    "            if (name != null && name.length() > 1) {",
    "                dest.setName(name.charAt(0) + \"*\".repeat(name.length() - 1));",
    "            }",
    "            return dest;",
    "        });",
    "```",
