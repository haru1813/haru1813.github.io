    "### Dead Letter Queue (DLQ)",
    "",
    "메시지 처리에 실패하면 **Dead Letter Queue**로 보내서 나중에 분석하거나 재처리할 수 있습니다.",
    "",
    "```mermaid",
    "flowchart LR",
    "    P[\"Producer\"] --> E[\"Exchange\"]",
    "    E --> Q[\"email-queue\"]",
    "    Q --> C[\"Consumer\"]",
    "    C -->|처리 실패| DLX[\"DL Exchange\"]",
    "    DLX --> DLQ[\"Dead Letter Queue\"]",
    "    DLQ --> R[\"재처리 / 알림\"]",
