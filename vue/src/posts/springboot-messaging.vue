    "            if (ex == null) {",
    "                log.info(\"[Kafka Producer] 발행 성공: topic={}, partition={}, offset={}\",",
    "                        result.getRecordMetadata().topic(),",
    "                        result.getRecordMetadata().partition(),",
    "                        result.getRecordMetadata().offset());",
    "            } else {",
    "                log.error(\"[Kafka Producer] 발행 실패: {}\", ex.getMessage());",
    "            }",
    "        });",
    "    }",
    "}",
