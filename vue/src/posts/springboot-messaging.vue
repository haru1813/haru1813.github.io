    "# 3. Kafka 연동 (분산 이벤트 스트리밍)",
    "",
    "## 개념",
    "",
    "**Apache Kafka**는 **분산 로그 기반 스트리밍 플랫폼**입니다. RabbitMQ와 달리 메시지를 **삭제하지 않고 로그에 보관**하며, 여러 Consumer가 자신의 속도로 읽을 수 있습니다.",
    "",
    "```mermaid",
    "flowchart LR",
    "    P1[\"Producer 1\"] --> T[\"Topic: order-events\"]",
    "    P2[\"Producer 2\"] --> T",
    "    T --> Pa0[\"Partition 0\"]",
    "    T --> Pa1[\"Partition 1\"]",
    "    T --> Pa2[\"Partition 2\"]",
    "    Pa0 --> CG1[\"Consumer Group A\"]",
    "    Pa1 --> CG1",
    "    Pa2 --> CG1",
    "    Pa0 --> CG2[\"Consumer Group B\"]",
    "    Pa1 --> CG2",
    "    Pa2 --> CG2",
    "```",
    "",
    "### Kafka 핵심 용어",
    "",
    "| 용어 | 설명 |",
    "|------|------|",
    "| **Topic** | 메시지 카테고리 (DB의 테이블과 유사) |",
    "| **Partition** | Topic을 분할한 단위, 병렬 처리의 기본 |",
    "| **Producer** | 메시지를 발행하는 주체 |",
    "| **Consumer** | 메시지를 소비하는 주체 |",
    "| **Consumer Group** | 같은 토픽을 나눠서 처리하는 Consumer 묶음 |",
    "| **Offset** | Partition 내 메시지 위치 (읽은 곳 북마크) |",
    "| **Broker** | Kafka 서버 인스턴스 |",
    "",
    "### RabbitMQ vs Kafka",
    "",
    "| 비교 | RabbitMQ | Kafka |",
    "|------|---------|-------|",
    "| 메시지 모델 | **큐** (소비 후 삭제) | **로그** (보관, 재읽기 가능) |",
    "| 처리량 | 수만 msg/sec | **수백만 msg/sec** |",
    "| 순서 보장 | 큐 단위 | **파티션 단위** |",
    "| 메시지 재처리 | 불가 (삭제됨) | **가능** (offset 이동) |",
    "| 라우팅 | Exchange + Routing Key | Topic + Partition |",
    "| 적합 | 작업 큐, 요청-응답 | **이벤트 소싱, 로그, 분석** |",
    "",
