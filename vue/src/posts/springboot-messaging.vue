    "# 2. RabbitMQ 연동 (메시지 큐)",
    "",
    "## 개념",
    "",
    "**RabbitMQ**는 AMQP(Advanced Message Queuing Protocol) 기반의 **메시지 브로커**입니다. Producer가 메시지를 보내면 Exchange가 라우팅 규칙에 따라 Queue에 전달하고, Consumer가 꺼내서 처리합니다.",
    "",
    "```mermaid",
    "flowchart LR",
    "    P[\"Producer<br/>(메시지 발행)\"] --> E[\"Exchange<br/>(라우팅)\"]",
    "    E -->|routing key| Q1[\"Queue 1<br/>(이메일)\"]",
    "    E -->|routing key| Q2[\"Queue 2<br/>(알림)\"]",
    "    E -->|routing key| Q3[\"Queue 3<br/>(로그)\"]",
    "    Q1 --> C1[\"Consumer 1\"]",
    "    Q2 --> C2[\"Consumer 2\"]",
    "    Q3 --> C3[\"Consumer 3\"]",
    "```",
    "",
    "### Exchange 유형",
    "",
    "| Exchange | 라우팅 방식 | 사용 예 |",
    "|----------|-----------|--------|",
    "| **Direct** | 정확한 routing key 일치 | 특정 큐에 전달 |",
    "| **Topic** | 패턴 매칭 (`order.*`, `#.error`) | 주제 기반 구독 |",
    "| **Fanout** | 모든 바인딩 큐에 복사 | 브로드캐스트 |",
    "| **Headers** | 헤더 값 기반 매칭 | 복잡한 조건 |",
    "",
