    "        // 전달된 인자 개수 확인",
    "        System.out.println(\"인자 개수: \" + args.length);",
    "",
    "        // 모든 인자 출력",
    "        for (int i = 0; i < args.length; i++) {",
    "            System.out.println(\"args[\" + i + \"] = \" + args[i]);",
    "        }",
    "",
    "        // 활용 예: 계산기",
