    "",
    "### 각 키워드의 의미",
    "",
    "| 키워드 | 의미 | 왜 필요한가? |",
    "|--------|------|-------------|",
    "| `public` | 접근 제한자 — 어디서든 접근 가능 | JVM이 외부에서 호출해야 하므로 |",
    "| `static` | 정적 메서드 — 객체 생성 없이 호출 | JVM이 인스턴스 없이 직접 호출하므로 |",
    "| `void` | 반환값 없음 | 프로그램 종료 시 반환할 값이 없으므로 |",
    "| `main` | 메서드 이름 (고정) | JVM이 이 이름을 약속된 진입점으로 인식 |",
    "| `String[] args` | 명령행 인자 배열 | 실행 시 외부에서 값을 전달받기 위해 |",
