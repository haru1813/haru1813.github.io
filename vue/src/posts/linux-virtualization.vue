    "",
    "### KVM이란?",
    "리눅스 커널에 내장된 **Type 1 하이퍼바이저**입니다. CPU의 하드웨어 가상화(Intel VT-x / AMD-V)를 활용하여 **거의 네이티브 성능**으로 가상머신을 실행합니다.",
    "",
    "```",
    "KVM 구조:",
    "┌──────┐ ┌──────┐",
    "│ VM 1 │ │ VM 2 │      ← QEMU (하드웨어 에뮬레이션)",
    "└──┬───┘ └──┬───┘",
    "   └────┬───┘",
    "     libvirt                ← VM 관리 API",
    "     KVM 모듈 (커널 내장)    ← 하이퍼바이저",
    "     리눅스 커널",
    "     하드웨어 (VT-x / AMD-V)",
    "```",
    "",
    "### 가상화 지원 확인",
    "```bash",
    "# CPU가 하드웨어 가상화를 지원하는지 확인",
    "egrep -c '(vmx|svm)' /proc/cpuinfo",
    "# 1 이상이면 지원",
    "",
    "# vmx: Intel VT-x",
    "# svm: AMD-V",
    "```",
    "",
