    "    public LocaleResolver localeResolver() {",
    "        CookieLocaleResolver resolver = new CookieLocaleResolver(\"lang\");",
    "        resolver.setDefaultLocale(Locale.KOREA);",
    "        resolver.setCookieMaxAge(3600 * 24 * 30); // 30일",
    "        return resolver;",
    "    }",
    "",
    "    @Bean",
    "    public LocaleChangeInterceptor localeChangeInterceptor() {",
    "        LocaleChangeInterceptor interceptor = new LocaleChangeInterceptor();",
    "        interceptor.setParamName(\"lang\"); // ?lang=en 으로 언어 변경",
    "        return interceptor;",
    "    }",
    "",
    "    @Override",
    "    public void addInterceptors(InterceptorRegistry registry) {",
    "        registry.addInterceptor(localeChangeInterceptor());",
    "    }",
    "}",
    "```",
    "",
    "### LocaleResolver 종류",
    "",
    "| Resolver | 저장 위치 | 특징 |",
    "|----------|----------|------|",
    "| `AcceptHeaderLocaleResolver` | HTTP 헤더 | 브라우저 설정 기반 (기본값), 변경 불가 |",
    "| **`CookieLocaleResolver`** | **쿠키** | **사용자가 선택한 언어를 쿠키에 저장 (권장)** |",
    "| `SessionLocaleResolver` | 세션 | 세션에 저장 (로그인 기반 서비스에 적합) |",
    "| `FixedLocaleResolver` | 고정 | 언어 변경 불가 (단일 언어 서비스) |",
    "",
