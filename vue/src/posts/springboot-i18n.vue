    "| `LocaleContextHolder` | 현재 Locale 조회 | `getLocale()` |",
    "",
    "| 상황 | 방법 |",
    "|------|------|",
    "| Thymeleaf에서 메시지 표시 | `th:text=\"#{key}\"` |",
    "| 파라미터가 있는 메시지 | `#{key(${param})}` |",
