    "    ...",
    "</form>",
    "",
    "<!-- 수동으로 CSRF 토큰 포함 -->",
    "<form action=\"/login\" method=\"post\">",
    "    <input type=\"hidden\" th:name=\"${_csrf.parameterName}\"",
    "           th:value=\"${_csrf.token}\">",
    "    ...",
    "</form>",
    "```",
