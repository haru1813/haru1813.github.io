    "### 1. 정적 자원이 로그인 페이지에서 안 보임",
    "",
    "CSS, JS, 이미지 등의 정적 자원도 Security 필터를 거치므로 허용이 필요합니다.",
    "",
    "```java",
    ".requestMatchers(\"/css/**\", \"/js/**\", \"/images/**\").permitAll()",
    "```",
    "",
    "### 2. 로그아웃은 반드시 POST로",
    "",
    "CSRF가 활성화된 상태에서 로그아웃은 POST 요청이어야 합니다.",
    "",
    "```html",
    "<!-- O: POST 방식 -->",
    "<form th:action=\"@{/logout}\" method=\"post\">",
    "    <button type=\"submit\">로그아웃</button>",
    "</form>",
    "",
    "<!-- X: GET 방식 (CSRF 때문에 403 에러) -->",
    "<a href=\"/logout\">로그아웃</a>",
    "```",
    "",
    "### 3. hasRole vs hasAuthority",
    "",
    "```java",
    "// hasRole(\"ADMIN\") → 내부적으로 \"ROLE_ADMIN\" 확인",
    ".requestMatchers(\"/admin\").hasRole(\"ADMIN\")",
    "",
    "// hasAuthority(\"ROLE_ADMIN\") → 정확한 문자열 비교",
    ".requestMatchers(\"/admin\").hasAuthority(\"ROLE_ADMIN\")",
    "```",
    "",
    "### 4. 순서 주의: 구체적인 URL을 먼저",
    "",
    "```java",
    "// O: 구체적 → 일반적",
    ".requestMatchers(\"/admin/**\").hasRole(\"ADMIN\")",
    ".requestMatchers(\"/api/**\").authenticated()",
    ".anyRequest().permitAll()",
    "",
    "// X: 일반적 규칙이 먼저 → admin도 누구나 접근 가능",
    ".anyRequest().permitAll()",
    ".requestMatchers(\"/admin/**\").hasRole(\"ADMIN\")  // 무시됨",
    "```",
    "",
