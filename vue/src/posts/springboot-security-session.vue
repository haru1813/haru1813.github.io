    "## CSRF (Cross-Site Request Forgery) 보호",
    "",
    "Spring Security는 **CSRF 보호가 기본 활성화**됩니다. 모든 POST/PUT/DELETE 요청에 CSRF 토큰이 필요합니다.",
    "",
    "```mermaid",
    "sequenceDiagram",
    "    participant B as 브라우저",
    "    participant S as 서버",
    "    B->>S: GET /login",
    "    S-->>B: 로그인 페이지 + CSRF 토큰 (hidden input)",
    "    B->>S: POST /login + _csrf=토큰값",
    "    S->>S: CSRF 토큰 검증",
    "    S-->>B: 인증 처리",
    "```",
    "",
    "### Thymeleaf에서 CSRF",
    "",
    "`th:action`을 사용하면 **CSRF 토큰이 자동 포함**됩니다.",
    "",
