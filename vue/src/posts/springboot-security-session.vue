    "## @Secured, @PreAuthorize 메서드 보안",
    "",
    "Controller나 Service의 **메서드 단위**로 권한을 체크할 수 있습니다.",
    "",
    "### 활성화",
    "",
    "```java",
    "@Configuration",
    "@EnableMethodSecurity(securedEnabled = true)",
    "public class SecurityConfig { ... }",
    "```",
    "",
    "### 사용 예시",
    "",
    "```java",
    "@Service",
    "public class AdminService {",
    "",
    "    // @Secured: 단순 권한 체크",
    "    @Secured(\"ROLE_ADMIN\")",
    "    public void deleteUser(Long userId) { ... }",
    "",
    "    // @PreAuthorize: SpEL 표현식으로 복잡한 조건",
    "    @PreAuthorize(\"hasRole('ADMIN') or #userId == authentication.principal.member.id\")",
    "    public UserResponse getUser(Long userId) { ... }",
    "",
    "    // @PreAuthorize: 여러 권한 중 하나",
    "    @PreAuthorize(\"hasAnyRole('ADMIN', 'MANAGER')\")",
    "    public List<UserResponse> getAllUsers() { ... }",
    "}",
    "```",
    "",
    "| 어노테이션 | 특징 |",
    "|------------|------|",
    "| `@Secured(\"ROLE_ADMIN\")` | 단순 역할 체크, `ROLE_` 접두사 필요 |",
    "| `@PreAuthorize(\"hasRole('ADMIN')\")` | SpEL 사용 가능, `ROLE_` 자동 처리, **권장** |",
    "| `@PostAuthorize(\"returnObject.username == ...\")` | 메서드 실행 후 결과로 검증 |",
