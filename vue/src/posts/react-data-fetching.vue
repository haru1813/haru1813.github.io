    "### 낙관적 업데이트 (Optimistic Update)",
    "",
    "서버 응답을 기다리지 않고 UI를 먼저 업데이트합니다. 실패하면 롤백합니다.",
    "",
    "```tsx",
    "interface Todo {",
    "  id: number;",
    "  text: string;",
    "  done: boolean;",
    "}",
    "",
    "const toggleMutation = useMutation<Todo, Error, number>({",
    "  mutationFn: async (todoId) => {",
    "    const { data } = await axios.patch<Todo>(`/api/todos/${todoId}/toggle`);",
    "    return data;",
    "  },",
    "  onMutate: async (todoId) => {",
    "    // 진행 중인 쿼리 취소 (낙관적 업데이트와 충돌 방지)",
    "    await queryClient.cancelQueries({ queryKey: ['todos'] });",
    "",
    "    // 이전 데이터 스냅샷 (롤백용)",
    "    const previous = queryClient.getQueryData<Todo[]>(['todos']);",
    "",
    "    // 낙관적 업데이트",
    "    queryClient.setQueryData<Todo[]>(['todos'], (old) =>",
    "      old?.map((t) => (t.id === todoId ? { ...t, done: !t.done } : t))",
    "    );",
    "",
    "    return { previous };",
    "  },",
    "  onError: (_err, _todoId, context) => {",
    "    // 실패 시 롤백",
    "    if (context?.previous) {",
    "      queryClient.setQueryData(['todos'], context.previous);",
    "    }",
    "  },",
    "  onSettled: () => {",
    "    queryClient.invalidateQueries({ queryKey: ['todos'] });",
    "  },",
    "});",
    "```",
