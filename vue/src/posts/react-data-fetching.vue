    "  defaultOptions: {",
    "    queries: {",
    "      staleTime: 1000 * 60 * 5,  // 5분간 신선 (리페치 안 함)",
    "      retry: 2,",
    "    },",
    "  },",
    "});",
    "",
    "createRoot(document.getElementById('root')!).render(",
    "  <QueryClientProvider client={queryClient}>",
    "    <App />",
    "    <ReactQueryDevtools initialIsOpen={false} />",
    "  </QueryClientProvider>",
    ");",
    "```",
