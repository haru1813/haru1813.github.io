    "## 3. React Query (TanStack Query)",
    "",
    "React Query는 **서버 상태를 자동 관리**하는 라이브러리입니다. fetch나 Axios는 \"어떻게 요청하는가\"에 집중하고, React Query는 \"요청한 데이터를 어떻게 관리하는가\"에 집중합니다.",
    "",
    "### 설치",
    "```bash",
    "npm install @tanstack/react-query",
    "```",
    "",
    "### React Query가 해결하는 문제",
    "",
    "| 직접 구현 시 | React Query 사용 시 |",
    "|-------------|-------------------|",
    "| `loading`, `error` 상태를 `useState`로 관리 | `isLoading`, `isError` 자동 제공 |",
    "| 중복 요청 방지 로직 직접 작성 | 같은 `queryKey`면 자동 중복 제거 |",
    "| 데이터 캐싱 직접 구현 | `staleTime`, `gcTime`으로 자동 캐싱 |",
    "| 포커스 시 리페치 직접 구현 | `refetchOnWindowFocus` 자동 |",
    "| 낙관적 업데이트 복잡 | `onMutate`로 간단 구현 |",
    "| 페이지네이션 상태 관리 | `keepPreviousData`로 깜빡임 방지 |",
    "",
    "### 초기 설정",
    "",
    "**TypeScript**",
