    "### AOP 용어",
    "",
    "| 용어 | 설명 | 예시 |",
    "|------|------|------|",
    "| **Aspect** | 횡단 관심사를 모듈화한 것 | 로깅 Aspect, 트랜잭션 Aspect |",
    "| **Advice** | 실제 실행되는 로직 | `@Before`, `@After`, `@Around` |",
    "| **JoinPoint** | Advice가 적용될 수 있는 지점 | 메서드 실행 시점 |",
    "| **Pointcut** | 어떤 JoinPoint에 Advice를 적용할지 결정 | `execution(* com.example.service.*.*(..))` |",
    "",
    "### AOP 예시: 실행 시간 측정",
    "",
    "```java",
    "@Aspect",
    "@Component",
    "public class ExecutionTimeAspect {",
    "",
    "    @Around(\"execution(* com.example.service.*.*(..))\")",
    "    public Object measureTime(ProceedingJoinPoint joinPoint) throws Throwable {",
    "        long start = System.currentTimeMillis();",
    "",
    "        Object result = joinPoint.proceed();",
    "",
    "        long elapsed = System.currentTimeMillis() - start;",
    "        System.out.println(joinPoint.getSignature() + \" 실행 시간: \" + elapsed + \"ms\");",
    "",
    "        return result;",
    "    }",
    "}",
    "```",
