    "## Iterator와 for-each의 관계",
    "",
    "향상된 for문은 내부적으로 **Iterator 패턴**을 사용합니다.",
    "`Iterable` 인터페이스를 구현한 모든 객체에 사용할 수 있습니다.",
    "",
    "```java",
    "import java.util.ArrayList;",
    "import java.util.Iterator;",
    "import java.util.List;",
    "",
    "List<String> names = new ArrayList<>();",
    "names.add(\"홍길동\");",
    "names.add(\"김철수\");",
    "names.add(\"이영희\");",
    "",
    "// for-each (간결, 권장)",
    "for (String name : names) {",
    "    System.out.println(name);",
    "}",
    "",
    "// 위와 동일한 동작 (Iterator 사용)",
    "Iterator<String> it = names.iterator();",
    "while (it.hasNext()) {",
    "    String name = it.next();",
    "    System.out.println(name);",
    "}",
    "```",
    "",
    "> **💡 팁**: 순회 중 요소를 삭제해야 할 때는 for-each 대신 Iterator의 `remove()` 메서드를 사용해야 합니다.",
    "> for-each에서 컬렉션을 수정하면 `ConcurrentModificationException`이 발생합니다.",
    "",
    "```java",
    "// ❌ for-each에서 삭제 시 에러",
    "// for (String name : names) {",
    "//     if (name.equals(\"김철수\")) names.remove(name);  // 예외 발생!",
    "// }",
    "",
    "// ✅ Iterator로 안전하게 삭제",
    "Iterator<String> iter = names.iterator();",
    "while (iter.hasNext()) {",
    "    if (iter.next().equals(\"김철수\")) {",
    "        iter.remove();  // 안전한 삭제",
    "    }",
    "}",
    "```",
