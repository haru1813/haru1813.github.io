    "",
    "**ConstraintLayout**은 Android에서 가장 권장되는 레이아웃입니다. 각 뷰에 **제약 조건(Constraint)**을 설정하여 위치를 결정합니다. 복잡한 레이아웃도 **단일 계층(flat hierarchy)**으로 구현할 수 있어 성능이 우수합니다.",
    "",
