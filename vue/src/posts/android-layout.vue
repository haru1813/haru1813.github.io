    "        app:layout_constraintTop_toBottomOf=\"@id/etInput\"",
    "        app:layout_constraintEnd_toEndOf=\"parent\"",
    "        android:layout_marginTop=\"8dp\" />",
    "",
    "</androidx.constraintlayout.widget.ConstraintLayout>",
    "```",
    "",
    "- 각 뷰는 **수평, 수직** 방향 모두 최소 하나 이상의 제약이 필요합니다.",
