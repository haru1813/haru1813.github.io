    "",
    "    <EditText",
    "        android:id=\"@+id/etInput\"",
    "        android:layout_width=\"0dp\"",
    "        android:layout_height=\"wrap_content\"",
    "        android:hint=\"입력하세요\"",
    "        app:layout_constraintTop_toBottomOf=\"@id/tvTitle\"",
