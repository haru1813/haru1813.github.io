    "        app:layout_constraintEnd_toEndOf=\"parent\"",
    "        android:layout_marginTop=\"16dp\" />",
    "",
    "    <Button",
    "        android:id=\"@+id/btnSubmit\"",
    "        android:layout_width=\"wrap_content\"",
    "        android:layout_height=\"wrap_content\"",
