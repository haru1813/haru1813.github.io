    "    android:orientation=\"vertical\"",
    "    app:layout_constraintGuide_percent=\"0.3\" />",
    "",
    "<TextView",
    "    android:layout_width=\"0dp\"",
    "    android:layout_height=\"wrap_content\"",
    "    android:text=\"가이드라인 왼쪽 30% 영역\"",
