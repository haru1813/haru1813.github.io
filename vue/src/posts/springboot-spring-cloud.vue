    "## Resilience4j (Circuit Breaker)",
    "",
    "**Resilience4j**는 MSA에서 **장애 전파를 방지**하기 위한 라이브러리입니다. 핵심 패턴은 **Circuit Breaker(서킷 브레이커)**로, 특정 서비스 호출이 반복 실패하면 **회로를 열어(Open)** 더 이상 요청을 보내지 않고 빠르게 실패(Fail-Fast)합니다.",
    "",
    "### Circuit Breaker 상태 전환",
    "",
    "```mermaid",
    "stateDiagram-v2",
    "    [*] --> CLOSED",
    "    CLOSED --> OPEN: 실패율 >= threshold\<br>(예: 50% 이상 실패)",
    "    OPEN --> HALF_OPEN: waitDurationInOpenState\<br>(예: 60초 후)",
    "    HALF_OPEN --> CLOSED: 시험 호출 성공",
    "    HALF_OPEN --> OPEN: 시험 호출 실패",
    "",
    "    CLOSED: 정상 상태\<br>모든 요청 통과",
    "    OPEN: 차단 상태\<br>fallback 즉시 반환",
    "    HALF_OPEN: 반열림 상태\<br>일부 요청만 통과하여 테스트",
    "```",
    "",
    "| 상태 | 설명 |",
    "|------|------|",
    "| **CLOSED** | 정상 상태. 모든 요청이 대상 서비스로 전달됩니다 |",
    "| **OPEN** | 차단 상태. 요청을 보내지 않고 즉시 fallback을 반환합니다 |",
    "| **HALF_OPEN** | 시험 상태. 일부 요청만 보내서 서비스 복구 여부를 확인합니다 |",
    "",
    "### 의존성 및 설정",
    "",
    "```java",
    "// build.gradle",
    "dependencies {",
    "    implementation 'org.springframework.cloud:spring-cloud-starter-circuitbreaker-resilience4j'",
    "}",
    "```",
    "",
    "```yaml",
    "# application.yml",
    "resilience4j:",
    "  circuitbreaker:",
    "    instances:",
    "      productService:",
    "        sliding-window-size: 10             # 최근 10개 호출 기준",
    "        failure-rate-threshold: 50           # 실패율 50% 이상이면 OPEN",
    "        wait-duration-in-open-state: 60s     # OPEN 상태 유지 시간",
    "        permitted-number-of-calls-in-half-open-state: 3  # HALF_OPEN 시 시험 호출 수",
    "        minimum-number-of-calls: 5           # 최소 5번 호출 후 판단",
    "",
    "  retry:",
    "    instances:",
    "      productService:",
    "        max-attempts: 3",
    "        wait-duration: 1s",
    "        retry-exceptions:",
    "          - java.io.IOException",
    "          - java.net.SocketTimeoutException",
    "",
    "  ratelimiter:",
    "    instances:",
    "      productService:",
    "        limit-for-period: 100               # 기간당 허용 요청 수",
    "        limit-refresh-period: 1s             # 기간",
    "        timeout-duration: 0s                 # 대기 시간 (0 = 즉시 거부)",
    "```",
    "",
    "### @CircuitBreaker 사용",
    "",
    "```java",
    "import io.github.resilience4j.circuitbreaker.annotation.CircuitBreaker;",
    "import io.github.resilience4j.retry.annotation.Retry;",
    "import lombok.RequiredArgsConstructor;",
    "import lombok.extern.slf4j.Slf4j;",
    "import org.springframework.stereotype.Service;",
    "",
    "@Slf4j",
    "@Service",
    "@RequiredArgsConstructor",
    "public class OrderService {",
    "",
    "    private final ProductClient productClient;",
    "",
    "    @CircuitBreaker(name = \"productService\",",
    "                    fallbackMethod = \"getProductFallback\")",
    "    @Retry(name = \"productService\")",
    "    public ProductResponse getProduct(Long productId) {",
    "        return productClient.getProduct(productId);",
    "    }",
    "",
    "    // fallback 메서드 (파라미터 + Throwable)",
    "    private ProductResponse getProductFallback(",
    "            Long productId, Throwable t) {",
    "        log.warn(\"Product 서비스 호출 실패, fallback 실행: {}\",",
    "                 t.getMessage());",
    "        return ProductResponse.builder()",
    "                .id(productId)",
    "                .name(\"[상품 정보를 불러올 수 없습니다]\")",
    "                .price(0)",
    "                .build();",
    "    }",
    "}",
    "```",
    "",
    "### Resilience4j 핵심 패턴 비교",
    "",
    "| 패턴 | 목적 | 설정 예시 |",
    "|------|------|----------|",
    "| **CircuitBreaker** | 연속 실패 시 요청 차단 | `failure-rate-threshold: 50` |",
    "| **Retry** | 일시적 장애 시 재시도 | `max-attempts: 3, wait-duration: 1s` |",
    "| **RateLimiter** | 초당 요청 수 제한 | `limit-for-period: 100` |",
    "| **Bulkhead** | 동시 요청 수 제한 | `max-concurrent-calls: 25` |",
    "| **TimeLimiter** | 타임아웃 설정 | `timeout-duration: 3s` |",
    "",
