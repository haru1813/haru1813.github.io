    "### RestTemplate vs WebClient vs Feign 비교",
    "",
    "| 항목 | RestTemplate | WebClient | OpenFeign |",
    "|------|-------------|-----------|-----------|",
    "| 방식 | 명령형(Imperative) | 반응형(Reactive) | **선언적(Declarative)** |",
