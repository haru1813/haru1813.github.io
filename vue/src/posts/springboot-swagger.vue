    "    }",
    "",
    "    @Operation(summary = \"상품 수정\", description = \"기존 상품 정보를 수정합니다\")",
    "    @ApiResponses({",
    "        @ApiResponse(responseCode = \"200\", description = \"수정 성공\"),",
