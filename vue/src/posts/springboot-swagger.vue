    "| `@Parameter` | 파라미터 | 쿼리/패스 파라미터 설명 |",
    "| `@Hidden` | 클래스/메서드 | Swagger 문서에서 숨김 |",
    "",
    "---",
    "",
    "## DTO 어노테이션 - @Schema",
    "",
    "요청/응답 DTO에 `@Schema`를 사용하면 **필드 설명, 예시 값, 필수 여부**를 문서화할 수 있습니다.",
    "",
    "### 요청 DTO",
    "",
    "```java",
    "import io.swagger.v3.oas.annotations.media.Schema;",
    "import jakarta.validation.constraints.*;",
    "",
    "@Schema(description = \"사용자 생성 요청\")",
    "public record UserCreateRequest(",
    "",
    "    @Schema(description = \"사용자 이름\", example = \"홍길동\", requiredMode = Schema.RequiredMode.REQUIRED)",
    "    @NotBlank",
    "    String name,",
    "",
    "    @Schema(description = \"이메일 주소\", example = \"hong@example.com\", requiredMode = Schema.RequiredMode.REQUIRED)",
    "    @Email",
    "    String email,",
    "",
    "    @Schema(description = \"나이\", example = \"25\", minimum = \"1\", maximum = \"200\")",
    "    @Min(1) @Max(200)",
    "    Integer age,",
    "",
    "    @Schema(description = \"역할\", example = \"USER\", allowableValues = {\"USER\", \"ADMIN\"}, defaultValue = \"USER\")",
    "    String role",
    ") {}",
    "```",
    "",
    "### 응답 DTO",
    "",
    "```java",
    "@Schema(description = \"사용자 응답\")",
    "public record UserResponse(",
    "",
    "    @Schema(description = \"사용자 ID\", example = \"1\")",
    "    Long id,",
    "",
    "    @Schema(description = \"사용자 이름\", example = \"홍길동\")",
    "    String name,",
    "",
    "    @Schema(description = \"이메일\", example = \"hong@example.com\")",
    "    String email,",
    "",
    "    @Schema(description = \"역할\", example = \"USER\")",
    "    String role,",
    "",
    "    @Schema(description = \"가입일\", example = \"2026-01-15T10:30:00\")",
    "    LocalDateTime createdAt",
    ") {}",
    "```",
    "",
    "### @Schema 주요 속성",
    "",
    "| 속성 | 설명 | 예시 |",
    "|------|------|------|",
    "| `description` | 필드 설명 | `\"사용자 이름\"` |",
    "| `example` | 예시 값 | `\"홍길동\"` |",
    "| `requiredMode` | 필수 여부 | `REQUIRED`, `NOT_REQUIRED` |",
    "| `defaultValue` | 기본값 | `\"USER\"` |",
    "| `allowableValues` | 허용 값 목록 | `{\"USER\", \"ADMIN\"}` |",
    "| `minimum` / `maximum` | 최소/최대 값 | `\"1\"`, `\"200\"` |",
    "| `hidden` | 문서에서 숨김 | `true` |",
