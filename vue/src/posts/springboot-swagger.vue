    "## Swagger vs Postman 비교",
    "",
    "| 비교 항목 | Swagger UI | Postman |",
    "|----------|-----------|---------|",
    "| **문서화** | ✅ 코드 기반 자동 생성 | ❌ 수동 작성 |",
    "| **테스트** | ✅ 브라우저에서 즉시 | ✅ 상세한 테스트 |",
    "| **팀 공유** | ✅ URL만 공유 | Collection 공유 필요 |",
    "| **환경 변수** | ❌ 제한적 | ✅ 다양한 환경 |",
    "| **자동화 테스트** | ❌ 불가 | ✅ Runner, Newman |",
    "| **Mock 서버** | ❌ 불가 | ✅ 가능 |",
    "| **코드 동기화** | ✅ 항상 최신 | ❌ 수동 업데이트 |",
    "| **용도** | API 문서 + 간단한 테스트 | 상세 테스트 + 자동화 |",
    "",
