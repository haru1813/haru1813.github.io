    "## 실전 Controller 전체 예제",
    "",
    "User CRUD API에 Swagger 어노테이션을 완전히 적용한 예제입니다.",
    "",
    "```java",
    "import io.swagger.v3.oas.annotations.Operation;",
    "import io.swagger.v3.oas.annotations.Parameter;",
    "import io.swagger.v3.oas.annotations.responses.ApiResponse;",
    "import io.swagger.v3.oas.annotations.responses.ApiResponses;",
    "import io.swagger.v3.oas.annotations.security.SecurityRequirement;",
    "import io.swagger.v3.oas.annotations.tags.Tag;",
    "import jakarta.validation.Valid;",
    "import lombok.RequiredArgsConstructor;",
    "import org.springframework.data.domain.Page;",
    "import org.springframework.http.HttpStatus;",
    "import org.springframework.http.ResponseEntity;",
    "import org.springframework.web.bind.annotation.*;",
    "",
    "@Tag(name = \"User\", description = \"사용자 관리 API\")",
    "@SecurityRequirement(name = \"bearerAuth\")",
    "@RestController",
    "@RequestMapping(\"/api/users\")",
    "@RequiredArgsConstructor",
    "public class UserController {",
    "",
    "    private final UserService userService;",
    "",
    "    @Operation(summary = \"사용자 목록 조회\", description = \"페이징, 검색, 정렬을 지원하는 사용자 목록 조회 API\")",
    "    @ApiResponse(responseCode = \"200\", description = \"조회 성공\")",
    "    @GetMapping",
    "    public ResponseEntity<Page<UserResponse>> getUsers(",
    "            @Parameter(description = \"페이지 번호\", example = \"0\")",
    "            @RequestParam(defaultValue = \"0\") int page,",
    "            @Parameter(description = \"페이지 크기\", example = \"10\")",
    "            @RequestParam(defaultValue = \"10\") int size",
    "    ) {",
    "        return ResponseEntity.ok(userService.findAll(page, size));",
    "    }",
    "",
    "    @Operation(summary = \"사용자 단건 조회\", description = \"ID로 사용자 정보를 조회합니다\")",
    "    @ApiResponses({",
    "        @ApiResponse(responseCode = \"200\", description = \"조회 성공\"),",
    "        @ApiResponse(responseCode = \"404\", description = \"사용자를 찾을 수 없음\")",
    "    })",
    "    @GetMapping(\"/{id}\")",
    "    public ResponseEntity<UserResponse> getUser(",
    "            @Parameter(description = \"사용자 ID\", required = true, example = \"1\")",
    "            @PathVariable Long id",
    "    ) {",
    "        return ResponseEntity.ok(userService.findById(id));",
    "    }",
    "",
    "    @Operation(summary = \"사용자 생성\", description = \"새로운 사용자를 생성합니다\")",
    "    @ApiResponses({",
    "        @ApiResponse(responseCode = \"201\", description = \"생성 성공\"),",
    "        @ApiResponse(responseCode = \"400\", description = \"잘못된 요청\")",
    "    })",
    "    @PostMapping",
    "    public ResponseEntity<UserResponse> createUser(",
    "            @Valid @RequestBody UserCreateRequest request",
    "    ) {",
    "        UserResponse user = userService.create(request);",
    "        return ResponseEntity.status(HttpStatus.CREATED).body(user);",
    "    }",
    "",
    "    @Operation(summary = \"사용자 수정\", description = \"기존 사용자 정보를 수정합니다\")",
    "    @ApiResponses({",
    "        @ApiResponse(responseCode = \"200\", description = \"수정 성공\"),",
    "        @ApiResponse(responseCode = \"404\", description = \"사용자를 찾을 수 없음\")",
    "    })",
    "    @PutMapping(\"/{id}\")",
    "    public ResponseEntity<UserResponse> updateUser(",
    "            @Parameter(description = \"사용자 ID\", required = true)",
    "            @PathVariable Long id,",
    "            @Valid @RequestBody UserUpdateRequest request",
    "    ) {",
    "        return ResponseEntity.ok(userService.update(id, request));",
    "    }",
    "",
    "    @Operation(summary = \"사용자 삭제\", description = \"사용자를 삭제합니다 (소프트 삭제)\")",
    "    @ApiResponses({",
    "        @ApiResponse(responseCode = \"204\", description = \"삭제 성공\"),",
    "        @ApiResponse(responseCode = \"404\", description = \"사용자를 찾을 수 없음\")",
    "    })",
    "    @DeleteMapping(\"/{id}\")",
    "    public ResponseEntity<Void> deleteUser(",
    "            @Parameter(description = \"사용자 ID\", required = true)",
    "            @PathVariable Long id",
    "    ) {",
    "        userService.delete(id);",
    "        return ResponseEntity.noContent().build();",
    "    }",
    "}",
