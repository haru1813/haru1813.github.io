    "package com.example.demo.service;",
    "",
    "import com.example.demo.dto.CreateProductRequest;",
    "import com.example.demo.dto.ProductResponse;",
    "import org.springframework.stereotype.Service;",
    "",
    "import java.util.ArrayList;",
    "import java.util.List;",
    "import java.util.Map;",
    "import java.util.concurrent.ConcurrentHashMap;",
    "import java.util.concurrent.atomic.AtomicLong;",
    "",
    "@Service",
    "public class ProductService {",
    "",
    "    private final Map<Long, ProductResponse> store = new ConcurrentHashMap<>();",
    "    private final AtomicLong sequence = new AtomicLong(1);",
    "",
    "    public List<ProductResponse> findAll() {",
    "        return new ArrayList<>(store.values());",
    "    }",
    "",
    "    public ProductResponse findById(Long id) {",
    "        ProductResponse product = store.get(id);",
    "        if (product == null) {",
    "            throw new IllegalArgumentException(\"상품을 찾을 수 없습니다. id=\" + id);",
    "        }",
    "        return product;",
    "    }",
    "",
    "    public ProductResponse create(CreateProductRequest request) {",
    "        Long id = sequence.getAndIncrement();",
    "        ProductResponse product = ProductResponse.builder()",
    "                .id(id)",
    "                .name(request.getName())",
    "                .price(request.getPrice())",
    "                .description(request.getDescription())",
    "                .build();",
    "        store.put(id, product);",
    "        return product;",
    "    }",
    "",
    "    public ProductResponse update(Long id, CreateProductRequest request) {",
    "        if (!store.containsKey(id)) {",
    "            throw new IllegalArgumentException(\"상품을 찾을 수 없습니다. id=\" + id);",
    "        }",
    "        ProductResponse updated = ProductResponse.builder()",
    "                .id(id)",
    "                .name(request.getName())",
    "                .price(request.getPrice())",
    "                .description(request.getDescription())",
    "                .build();",
    "        store.put(id, updated);",
    "        return updated;",
    "    }",
    "",
    "    public void delete(Long id) {",
    "        if (!store.containsKey(id)) {",
    "            throw new IllegalArgumentException(\"상품을 찾을 수 없습니다. id=\" + id);",
    "        }",
    "        store.remove(id);",
    "    }",
    "}",
