    "## JWT 인증 설정",
    "",
    "Swagger UI에서 **JWT 토큰을 입력**하여 인증이 필요한 API를 테스트할 수 있습니다.",
    "",
    "### SecurityScheme 설정",
    "",
    "```java",
    "import io.swagger.v3.oas.models.Components;",
    "import io.swagger.v3.oas.models.OpenAPI;",
    "import io.swagger.v3.oas.models.info.Info;",
    "import io.swagger.v3.oas.models.security.SecurityRequirement;",
    "import io.swagger.v3.oas.models.security.SecurityScheme;",
    "import org.springframework.context.annotation.Bean;",
    "import org.springframework.context.annotation.Configuration;",
    "",
    "@Configuration",
    "public class OpenApiConfig {",
    "",
    "    @Bean",
    "    public OpenAPI openAPI() {",
    "        String securitySchemeName = \"bearerAuth\";",
    "",
    "        return new OpenAPI()",
    "                .info(new Info()",
    "                        .title(\"My Blog API\")",
    "                        .version(\"v1.0.0\")",
    "                        .description(\"JWT 인증이 적용된 블로그 API\"))",
    "                .addSecurityItem(new SecurityRequirement()",
    "                        .addList(securitySchemeName))",
    "                .components(new Components()",
    "                        .addSecuritySchemes(securitySchemeName,",
    "                                new SecurityScheme()",
    "                                        .name(securitySchemeName)",
    "                                        .type(SecurityScheme.Type.HTTP)",
    "                                        .scheme(\"bearer\")",
    "                                        .bearerFormat(\"JWT\")",
    "                                        .description(\"JWT 토큰을 입력하세요 (Bearer 접두사 불필요)\")));",
    "    }",
    "}",
    "```",
    "",
    "### 특정 API만 인증 적용 - @SecurityRequirement",
    "",
    "```java",
    "import io.swagger.v3.oas.annotations.security.SecurityRequirement;",
    "",
    "// 전체 Controller에 인증 적용",
    "@SecurityRequirement(name = \"bearerAuth\")",
    "@RestController",
    "@RequestMapping(\"/api/admin\")",
    "public class AdminController {",
    "    // 모든 API에 인증 필요",
    "}",
    "",
    "// 특정 메서드만 인증 적용",
    "@RestController",
    "@RequestMapping(\"/api/posts\")",
    "public class PostController {",
    "",
    "    @GetMapping",
    "    public List<PostResponse> getPosts() {",
    "        // 인증 불필요 (공개 API)",
    "        return postService.findAll();",
    "    }",
    "",
    "    @SecurityRequirement(name = \"bearerAuth\")",
    "    @PostMapping",
    "    public PostResponse createPost(@RequestBody PostCreateRequest request) {",
    "        // 인증 필요",
    "        return postService.create(request);",
    "    }",
    "}",
    "```",
    "",
