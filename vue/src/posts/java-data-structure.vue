    "",
    "| 연산 | 시간 복잡도 |",
    "|------|-----------|",
    "| 삽입 | O(log n) |",
    "| 최솟값/최댓값 조회 | O(1) |",
    "| 최솟값/최댓값 삭제 | O(log n) |",
