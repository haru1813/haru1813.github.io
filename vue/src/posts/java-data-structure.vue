    "| 후위 (Postorder) | 왼쪽 → 오른쪽 → **루트** | 4 → 5 → 2 → 3 → 1 |",
    "| 레벨 (Level-order) | 층별로 왼→오 | 1 → 2 → 3 → 4 → 5 |",
