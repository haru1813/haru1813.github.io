    "## 7. 힙 (Heap)",
    "",
    "**완전 이진 트리** 기반으로 최솟값 또는 최댓값을 O(1)에 꺼낼 수 있는 자료구조입니다.",
    "",
    "### 종류",
    "- **최소 힙 (Min Heap)**: 부모 ≤ 자식 → 루트가 최솟값",
    "- **최대 힙 (Max Heap)**: 부모 ≥ 자식 → 루트가 최댓값",
    "",
    "```mermaid",
    "graph TD",
    "    subgraph \"최소 힙\"",
    "        M1((\"10\")) --> M2((\"20\"))",
    "        M1 --> M3((\"15\"))",
    "        M2 --> M4((\"30\"))",
    "        M2 --> M5((\"25\"))",
    "    end",
    "```",
    "",
    "### 배열로 표현",
    "",
    "힙은 배열로 효율적으로 표현됩니다:",
    "- 부모 인덱스: `(i - 1) / 2`",
    "- 왼쪽 자식: `2 * i + 1`",
    "- 오른쪽 자식: `2 * i + 2`",
    "",
    "### Java 직접 구현 - 최소 힙",
    "",
    "```java",
    "public class MinHeap {",
    "    private int[] heap;",
    "    private int size;",
    "",
    "    public MinHeap(int capacity) {",
    "        heap = new int[capacity];",
    "        size = 0;",
    "    }",
    "",
    "    public void insert(int value) {",
    "        heap[size] = value;",
    "        siftUp(size);",
    "        size++;",
    "    }",
    "",
    "    public int extractMin() {",
    "        int min = heap[0];",
    "        heap[0] = heap[--size];",
    "        siftDown(0);",
    "        return min;",
    "    }",
    "",
    "    private void siftUp(int index) {",
    "        while (index > 0) {",
    "            int parent = (index - 1) / 2;",
    "            if (heap[index] >= heap[parent]) break;",
    "            swap(index, parent);",
    "            index = parent;",
    "        }",
    "    }",
    "",
    "    private void siftDown(int index) {",
    "        while (2 * index + 1 < size) {",
    "            int child = 2 * index + 1;",
    "            if (child + 1 < size && heap[child + 1] < heap[child]) {",
    "                child++;",
    "            }",
    "            if (heap[index] <= heap[child]) break;",
    "            swap(index, child);",
    "            index = child;",
    "        }",
    "    }",
    "",
    "    private void swap(int a, int b) {",
    "        int temp = heap[a];",
    "        heap[a] = heap[b];",
    "        heap[b] = temp;",
    "    }",
    "}",
    "```",
    "",
    "### Java PriorityQueue 사용",
    "",
    "```java",
    "// 최소 힙 (기본)",
    "PriorityQueue<Integer> minHeap = new PriorityQueue<>();",
    "minHeap.offer(30);",
    "minHeap.offer(10);",
    "minHeap.offer(20);",
    "System.out.println(minHeap.poll()); // 10",
