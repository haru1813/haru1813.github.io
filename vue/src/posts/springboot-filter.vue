    "```java",
    "package com.example.demo.filter;",
    "",
    "import jakarta.servlet.*;",
    "import jakarta.servlet.http.HttpServletRequest;",
    "import jakarta.servlet.http.HttpServletResponse;",
    "import lombok.extern.slf4j.Slf4j;",
    "",
    "import java.io.IOException;",
    "",
    "@Slf4j",
    "public class AuthenticationFilter implements Filter {",
    "",
    "    @Override",
    "    public void doFilter(ServletRequest request,",
    "                         ServletResponse response,",
    "                         FilterChain chain) throws IOException, ServletException {",
    "",
    "        HttpServletRequest httpReq = (HttpServletRequest) request;",
    "        HttpServletResponse httpRes = (HttpServletResponse) response;",
    "",
    "        // GET 요청은 인증 없이 통과 (조회는 누구나 가능)",
    "        if (\"GET\".equalsIgnoreCase(httpReq.getMethod())) {",
    "            chain.doFilter(request, response);",
    "            return;",
    "        }",
    "",
    "        // POST, PUT, DELETE 등은 토큰 필요",
    "        String authHeader = httpReq.getHeader(\"Authorization\");",
    "",
    "        if (authHeader == null || !authHeader.startsWith(\"Bearer \")) {",
    "            log.warn(\"[인증 실패] 토큰 없음: {} {}\", httpReq.getMethod(), httpReq.getRequestURI());",
    "            httpRes.setStatus(HttpServletResponse.SC_UNAUTHORIZED);",
    "            httpRes.setContentType(\"application/json;charset=UTF-8\");",
    "            httpRes.getWriter().write(",
    "                    \"{\\\"error\\\":\\\"UNAUTHORIZED\\\",\\\"message\\\":\\\"Authorization 헤더에 Bearer 토큰이 필요합니다\\\"}\"",
    "            );",
    "            return;  // chain.doFilter()를 호출하지 않아 요청 차단!",
    "        }",
    "",
    "        String token = authHeader.substring(7);",
    "",
    "        if (token.length() < 10) {",
    "            log.warn(\"[인증 실패] 유효하지 않은 토큰: {} {}\", httpReq.getMethod(), httpReq.getRequestURI());",
    "            httpRes.setStatus(HttpServletResponse.SC_FORBIDDEN);",
    "            httpRes.setContentType(\"application/json;charset=UTF-8\");",
    "            httpRes.getWriter().write(",
    "                    \"{\\\"error\\\":\\\"FORBIDDEN\\\",\\\"message\\\":\\\"유효하지 않은 토큰입니다\\\"}\"",
    "            );",
    "            return;",
