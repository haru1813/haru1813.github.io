    "## 실전 예제 2: CORS Filter",
    "",
    "브라우저의 **Cross-Origin 요청**을 허용하는 헤더를 모든 응답에 추가합니다.",
    "",
    "### CorsFilter.java",
    "",
    "```java",
    "package com.example.demo.filter;",
    "",
    "import jakarta.servlet.*;",
    "import jakarta.servlet.http.HttpServletRequest;",
    "import jakarta.servlet.http.HttpServletResponse;",
    "import lombok.extern.slf4j.Slf4j;",
    "",
    "import java.io.IOException;",
    "",
    "@Slf4j",
    "public class CorsFilter implements Filter {",
    "",
    "    @Override",
    "    public void doFilter(ServletRequest request,",
    "                         ServletResponse response,",
    "                         FilterChain chain) throws IOException, ServletException {",
    "",
    "        HttpServletRequest httpReq = (HttpServletRequest) request;",
    "        HttpServletResponse httpRes = (HttpServletResponse) response;",
    "",
    "        httpRes.setHeader(\"Access-Control-Allow-Origin\", \"*\");",
    "        httpRes.setHeader(\"Access-Control-Allow-Methods\", \"GET, POST, PUT, DELETE, OPTIONS\");",
    "        httpRes.setHeader(\"Access-Control-Allow-Headers\", \"Authorization, Content-Type\");",
    "        httpRes.setHeader(\"Access-Control-Max-Age\", \"3600\");",
    "",
    "        // OPTIONS preflight 요청은 바로 응답",
    "        if (\"OPTIONS\".equalsIgnoreCase(httpReq.getMethod())) {",
    "            httpRes.setStatus(HttpServletResponse.SC_OK);",
    "            return;",
    "        }",
    "",
    "        chain.doFilter(request, response);",
    "    }",
    "}",
    "```",
    "",
    "### CORS 헤더 설명",
    "",
    "| 헤더 | 역할 |",
    "|------|------|",
    "| `Access-Control-Allow-Origin` | 허용할 출처 (`*` = 모든 출처) |",
    "| `Access-Control-Allow-Methods` | 허용할 HTTP 메서드 |",
    "| `Access-Control-Allow-Headers` | 허용할 요청 헤더 |",
    "| `Access-Control-Max-Age` | Preflight 결과 캐시 시간(초) |",
