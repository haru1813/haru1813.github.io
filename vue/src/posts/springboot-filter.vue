    "## 실전 예제 3: 인증 토큰 검증 Filter",
    "",
    "API 요청의 `Authorization` 헤더에서 토큰을 검증합니다.",
    "",
    "### AuthenticationFilter.java",
    "",
    "```java",
    "package com.example.demo.filter;",
    "",
    "import jakarta.servlet.*;",
    "import jakarta.servlet.http.HttpServletRequest;",
    "import jakarta.servlet.http.HttpServletResponse;",
    "import lombok.extern.slf4j.Slf4j;",
    "",
    "import java.io.IOException;",
    "import java.util.Set;",
    "",
    "@Slf4j",
    "public class AuthenticationFilter implements Filter {",
    "",
    "    // 인증 없이 접근 가능한 경로",
    "    private static final Set<String> WHITELIST = Set.of(",
    "            \"/api/auth/login\",",
    "            \"/api/auth/signup\",",
    "            \"/api/health\"",
    "    );",
    "",
    "    @Override",
    "    public void doFilter(ServletRequest request,",
    "                         ServletResponse response,",
    "                         FilterChain chain) throws IOException, ServletException {",
    "",
    "        HttpServletRequest httpReq = (HttpServletRequest) request;",
    "        HttpServletResponse httpRes = (HttpServletResponse) response;",
    "",
    "        String uri = httpReq.getRequestURI();",
    "",
    "        // 화이트리스트 경로는 통과",
    "        if (WHITELIST.contains(uri)) {",
    "            chain.doFilter(request, response);",
    "            return;",
    "        }",
    "",
    "        // OPTIONS 요청 통과 (CORS preflight)",
    "        if (\"OPTIONS\".equalsIgnoreCase(httpReq.getMethod())) {",
    "            chain.doFilter(request, response);",
    "            return;",
    "        }",
    "",
    "        // Authorization 헤더 확인",
    "        String authHeader = httpReq.getHeader(\"Authorization\");",
    "",
    "        if (authHeader == null || !authHeader.startsWith(\"Bearer \")) {",
    "            log.warn(\"[인증 실패] 토큰 없음: {} {}\", httpReq.getMethod(), uri);",
    "            httpRes.setStatus(HttpServletResponse.SC_UNAUTHORIZED);",
    "            httpRes.setContentType(\"application/json;charset=UTF-8\");",
    "            httpRes.getWriter().write(",
    "                    \"{\\\"code\\\":\\\"UNAUTHORIZED\\\",\\\"message\\\":\\\"토큰이 필요합니다\\\"}\"",
    "            );",
    "            return;  // chain.doFilter()를 호출하지 않아 요청 차단",
    "        }",
    "",
    "        String token = authHeader.substring(7);",
    "",
    "        if (!isValidToken(token)) {",
    "            log.warn(\"[인증 실패] 유효하지 않은 토큰: {} {}\", httpReq.getMethod(), uri);",
    "            httpRes.setStatus(HttpServletResponse.SC_FORBIDDEN);",
    "            httpRes.setContentType(\"application/json;charset=UTF-8\");",
    "            httpRes.getWriter().write(",
    "                    \"{\\\"code\\\":\\\"FORBIDDEN\\\",\\\"message\\\":\\\"유효하지 않은 토큰입니다\\\"}\"",
    "            );",
    "            return;",
    "        }",
    "",
    "        log.info(\"[인증 성공] {} {}\", httpReq.getMethod(), uri);",
    "        chain.doFilter(request, response);",
    "    }",
    "",
    "    private boolean isValidToken(String token) {",
    "        // 실무에서는 JWT 파싱, 서명 검증, 만료 시간 확인",
    "        return token != null && token.length() > 10;",
    "    }",
    "}",
    "```",
    "",
    "**요청 차단 방법:**",
    "",
    "| Interceptor | Filter |",
    "|-------------|--------|",
    "| `return false;` | `chain.doFilter()`를 **호출하지 않고 return** |",
