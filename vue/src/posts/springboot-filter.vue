    "            log.info(\"[CORS] OPTIONS preflight 요청 처리\");",
    "            httpRes.setStatus(HttpServletResponse.SC_OK);",
    "            return;",
    "        }",
    "",
    "        chain.doFilter(request, response);",
    "    }",
    "}",
