    "## OncePerRequestFilter (Spring 제공)",
    "",
    "Spring은 `jakarta.servlet.Filter` 대신 **`OncePerRequestFilter`** 추상 클래스를 제공합니다. 요청당 **1회만** 실행되는 것을 보장합니다.",
    "",
    "### 왜 필요한가?",
    "",
    "서블릿 스펙에서는 `forward`나 `include`로 내부 이동이 발생하면 같은 요청에서 Filter가 **여러 번** 호출될 수 있습니다. `OncePerRequestFilter`는 이를 방지합니다.",
    "",
    "### 예제: OncePerRequestFilter 기반 로깅",
    "",
    "```java",
    "package com.example.demo.filter;",
    "",
    "import jakarta.servlet.FilterChain;",
    "import jakarta.servlet.ServletException;",
    "import jakarta.servlet.http.HttpServletRequest;",
    "import jakarta.servlet.http.HttpServletResponse;",
    "import lombok.extern.slf4j.Slf4j;",
    "import org.springframework.web.filter.OncePerRequestFilter;",
    "",
    "import java.io.IOException;",
    "",
    "@Slf4j",
    "public class ApiLoggingFilter extends OncePerRequestFilter {",
    "",
    "    @Override",
    "    protected void doFilterInternal(HttpServletRequest request,",
    "                                    HttpServletResponse response,",
    "                                    FilterChain filterChain)",
    "            throws ServletException, IOException {",
    "",
    "        long start = System.currentTimeMillis();",
    "        log.info(\"[API] {} {} 시작\", request.getMethod(), request.getRequestURI());",
    "",
    "        filterChain.doFilter(request, response);",
    "",
    "        long elapsed = System.currentTimeMillis() - start;",
    "        log.info(\"[API] {} {} → {} ({}ms)\",",
    "                request.getMethod(), request.getRequestURI(), response.getStatus(), elapsed);",
    "    }",
    "",
    "    @Override",
    "    protected boolean shouldNotFilter(HttpServletRequest request) {",
    "        String uri = request.getRequestURI();",
    "        return uri.startsWith(\"/css\")",
    "                || uri.startsWith(\"/js\")",
    "                || uri.startsWith(\"/images\");",
    "    }",
    "}",
    "```",
