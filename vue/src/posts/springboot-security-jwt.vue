    "| 메서드 | 역할 |",
    "|--------|------|",
    "| `generateAccessToken()` | 인증 정보로 Access Token 생성 (username + roles 포함) |",
    "| `generateRefreshToken()` | Refresh Token 생성 (username만 포함, 긴 만료) |",
    "| `getUsername()` | 토큰에서 사용자 이름 추출 |",
    "| `getRoles()` | 토큰에서 권한 정보 추출 |",
    "| `validateToken()` | 서명 + 만료 + 형식 등 유효성 검증 |",
