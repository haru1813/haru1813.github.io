    "## Access Token + Refresh Token 전체 흐름",
    "",
    "```mermaid",
    "sequenceDiagram",
    "    participant C as 클라이언트",
    "    participant S as 서버",
    "    participant DB as DB/저장소",
    "",
    "    Note over C,S: 1. 로그인",
    "    C->>S: POST /api/auth/login {username, password}",
    "    S->>S: AuthenticationManager → UserDetailsService → PasswordEncoder 검증",
    "    S->>S: Access Token (30분) + Refresh Token (7일) 생성",
    "    S->>DB: Refresh Token 저장",
    "    S-->>C: { accessToken, refreshToken, tokenType, expiresIn }",
    "",
    "    Note over C,S: 2. API 호출",
    "    C->>S: GET /api/me (Authorization: Bearer {accessToken})",
    "    S->>S: JwtAuthenticationFilter → 토큰 검증",
    "    S-->>C: 200 OK { username, roles }",
    "",
    "    Note over C,S: 3. Access Token 만료",
    "    C->>S: GET /api/me (Authorization: Bearer {만료된 토큰})",
    "    S-->>C: 401 Unauthorized",
    "",
    "    Note over C,S: 4. 토큰 갱신",
    "    C->>S: POST /api/auth/refresh { refreshToken }",
    "    S->>S: Refresh Token 유효성 검증",
    "    S->>DB: 저장된 Refresh Token과 비교",
    "    S->>S: 새 Access Token 생성",
    "    S-->>C: { accessToken(새로운), refreshToken, expiresIn }",
    "",
    "    Note over C,S: 5. 로그아웃",
    "    C->>S: POST /api/auth/logout",
    "    S->>DB: Refresh Token 삭제",
    "    S-->>C: { message: \"로그아웃 성공\" }",
    "```",
    "",
    "### Access Token vs Refresh Token",
    "",
    "| 구분 | Access Token | Refresh Token |",
    "|------|-------------|---------------|",
    "| 용도 | API 인증 | Access Token 재발급 |",
    "| 만료 시간 | **짧음** (15~30분) | **김** (7~30일) |",
    "| 저장 위치 | 클라이언트 (메모리/변수) | 클라이언트 (httpOnly Cookie 권장) |",
    "| 서버 저장 | X | O (DB에 저장하여 무효화 가능) |",
    "| 탈취 시 위험도 | 높음 (짧은 만료로 완화) | 매우 높음 (서버에서 삭제 가능) |",
    "",
