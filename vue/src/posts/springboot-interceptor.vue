    "",
    "세션 대신 **Authorization 헤더의 토큰**으로 인증하는 방식입니다.",
    "",
    "### TokenAuthInterceptor.java",
    "",
    "```java",
    "package com.example.demo.interceptor;",
    "",
    "import jakarta.servlet.http.HttpServletRequest;",
    "import jakarta.servlet.http.HttpServletResponse;",
    "import lombok.extern.slf4j.Slf4j;",
    "import org.springframework.http.HttpStatus;",
    "import org.springframework.stereotype.Component;",
    "import org.springframework.web.servlet.HandlerInterceptor;",
    "",
    "@Slf4j",
    "@Component",
    "public class TokenAuthInterceptor implements HandlerInterceptor {",
    "",
    "    @Override",
    "    public boolean preHandle(HttpServletRequest request,",
    "                             HttpServletResponse response,",
    "                             Object handler) throws Exception {",
    "",
    "        if (\"OPTIONS\".equalsIgnoreCase(request.getMethod())) {",
    "            return true;",
    "        }",
    "",
    "        String authHeader = request.getHeader(\"Authorization\");",
    "",
    "        if (authHeader == null || !authHeader.startsWith(\"Bearer \")) {",
    "            log.warn(\"[토큰 없음] {} {}\", request.getMethod(), request.getRequestURI());",
    "            response.setStatus(HttpStatus.UNAUTHORIZED.value());",
    "            response.setContentType(\"application/json;charset=UTF-8\");",
    "            response.getWriter().write(",
    "                    \"{\\\"code\\\":\\\"UNAUTHORIZED\\\",\\\"message\\\":\\\"토큰이 필요합니다\\\"}\"",
    "            );",
    "            return false;",
    "        }",
    "",
    "        String token = authHeader.substring(7);",
    "",
    "        // 토큰 검증 로직 (실무에서는 JWT 파싱·서명 검증)",
    "        if (!isValidToken(token)) {",
    "            log.warn(\"[토큰 만료/위조] {} {}\", request.getMethod(), request.getRequestURI());",
    "            response.setStatus(HttpStatus.FORBIDDEN.value());",
    "            response.setContentType(\"application/json;charset=UTF-8\");",
    "            response.getWriter().write(",
    "                    \"{\\\"code\\\":\\\"FORBIDDEN\\\",\\\"message\\\":\\\"유효하지 않은 토큰입니다\\\"}\"",
    "            );",
    "            return false;",
    "        }",
    "",
    "        request.setAttribute(\"userId\", extractUserId(token));",
    "        return true;",
    "    }",
    "",
    "    private boolean isValidToken(String token) {",
    "        // 실제로는 JWT 서명 검증, 만료 시간 확인 등",
    "        return token != null && token.length() > 10;",
    "    }",
    "",
    "    private Long extractUserId(String token) {",
    "        // 실제로는 JWT에서 subject(userId)를 추출",
    "        return 1L;",
    "    }",
    "}",
    "```",
