    "",
    "```java",
    "package com.example.demo.interceptor;",
    "",
    "import jakarta.servlet.http.HttpServletRequest;",
    "import jakarta.servlet.http.HttpServletResponse;",
    "import lombok.extern.slf4j.Slf4j;",
    "import org.springframework.http.HttpStatus;",
    "import org.springframework.stereotype.Component;",
    "import org.springframework.web.servlet.HandlerInterceptor;",
    "",
    "@Slf4j",
    "@Component",
    "public class AuthCheckInterceptor implements HandlerInterceptor {",
    "",
    "    @Override",
    "    public boolean preHandle(HttpServletRequest request,",
    "                             HttpServletResponse response,",
    "                             Object handler) throws Exception {",
    "",
    "        // GET 요청은 인증 없이 통과 (조회는 누구나 가능)",
    "        if (\"GET\".equalsIgnoreCase(request.getMethod())) {",
    "            return true;",
    "        }",
    "",
    "        // POST, PUT, DELETE 등은 토큰 필요",
    "        String authHeader = request.getHeader(\"Authorization\");",
    "",
    "        if (authHeader == null || !authHeader.startsWith(\"Bearer \")) {",
    "            log.warn(\"[인증 실패] {} {} - 토큰이 없습니다\",",
    "                    request.getMethod(), request.getRequestURI());",
    "",
    "            response.setStatus(HttpStatus.UNAUTHORIZED.value());",
    "            response.setContentType(\"application/json;charset=UTF-8\");",
    "            response.getWriter().write(",
    "                    \"{\\\"error\\\":\\\"UNAUTHORIZED\\\",\\\"message\\\":\\\"Authorization 헤더에 Bearer 토큰이 필요합니다\\\"}\"",
    "            );",
    "            return false;",
    "        }",
    "",
    "        String token = authHeader.substring(7);",
    "",
    "        // 간단한 토큰 검증 (실무에서는 JWT 파싱·서명 검증)",
    "        if (token.length() < 10) {",
    "            log.warn(\"[인증 실패] {} {} - 유효하지 않은 토큰\",",
    "                    request.getMethod(), request.getRequestURI());",
    "",
    "            response.setStatus(HttpStatus.FORBIDDEN.value());",
    "            response.setContentType(\"application/json;charset=UTF-8\");",
    "            response.getWriter().write(",
    "                    \"{\\\"error\\\":\\\"FORBIDDEN\\\",\\\"message\\\":\\\"유효하지 않은 토큰입니다\\\"}\"",
    "            );",
    "            return false;",
    "        }",
    "",
    "        log.info(\"[인증 성공] 토큰 확인됨\");",
