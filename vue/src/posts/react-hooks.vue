    "## useReducer - 복잡한 상태 관리",
    "",
    "상태 로직이 복잡하거나 여러 상태가 연관될 때 `useState` 대신 사용합니다.",
    "",
    "```jsx",
    "import { useReducer } from 'react';",
    "",
    "const initialState = { count: 0, step: 1 };",
    "",
    "function reducer(state, action) {",
    "  switch (action.type) {",
    "    case 'increment':",
    "      return { ...state, count: state.count + state.step };",
    "    case 'decrement':",
    "      return { ...state, count: state.count - state.step };",
    "    case 'setStep':",
    "      return { ...state, step: action.payload };",
    "    case 'reset':",
    "      return initialState;",
    "    default:",
    "      throw new Error(`Unknown action: ${action.type}`);",
    "  }",
    "}",
    "",
    "function Counter() {",
    "  const [state, dispatch] = useReducer(reducer, initialState);",
    "",
    "  return (",
    "    <div>",
    "      <p>카운트: {state.count} (step: {state.step})</p>",
    "      <button onClick={() => dispatch({ type: 'increment' })}>+</button>",
    "      <button onClick={() => dispatch({ type: 'decrement' })}>-</button>",
    "      <button onClick={() => dispatch({ type: 'reset' })}>초기화</button>",
    "      <input",
    "        type=\"number\"",
    "        value={state.step}",
    "        onChange={(e) => dispatch({ type: 'setStep', payload: Number(e.target.value) })}",
    "      />",
    "    </div>",
    "  );",
    "}",
    "```",
    "",
    "### Todo 앱 with useReducer",
    "```jsx",
    "function todoReducer(state, action) {",
    "  switch (action.type) {",
    "    case 'add':",
    "      return [...state, { id: Date.now(), text: action.text, done: false }];",
    "    case 'toggle':",
    "      return state.map((t) =>",
    "        t.id === action.id ? { ...t, done: !t.done } : t",
    "      );",
    "    case 'delete':",
    "      return state.filter((t) => t.id !== action.id);",
    "    case 'clearDone':",
    "      return state.filter((t) => !t.done);",
    "    default:",
    "      return state;",
    "  }",
    "}",
    "",
    "function TodoApp() {",
    "  const [todos, dispatch] = useReducer(todoReducer, []);",
    "  const [input, setInput] = useState('');",
    "",
    "  const handleAdd = () => {",
    "    if (!input.trim()) return;",
    "    dispatch({ type: 'add', text: input });",
    "    setInput('');",
    "  };",
    "",
    "  return (",
    "    <div>",
    "      <input value={input} onChange={(e) => setInput(e.target.value)}",
    "             onKeyDown={(e) => e.key === 'Enter' && handleAdd()} />",
    "      <button onClick={handleAdd}>추가</button>",
    "      <button onClick={() => dispatch({ type: 'clearDone' })}>완료 삭제</button>",
    "      <ul>",
    "        {todos.map((t) => (",
    "          <li key={t.id}>",
    "            <span onClick={() => dispatch({ type: 'toggle', id: t.id })}",
    "                  style={{ textDecoration: t.done ? 'line-through' : 'none' }}>",
    "              {t.text}",
    "            </span>",
    "            <button onClick={() => dispatch({ type: 'delete', id: t.id })}>삭제</button>",
    "          </li>",
    "        ))}",
    "      </ul>",
    "    </div>",
    "  );",
    "}",
    "```",
