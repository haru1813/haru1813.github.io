    "## useImperativeHandle - 부모에게 노출할 API 제어",
    "",
    "`useImperativeHandle`은 `forwardRef`와 함께 사용하여, 부모 컴포넌트가 `ref`로 접근할 때 **노출되는 메서드를 직접 정의**합니다. 자식 컴포넌트의 내부 구현을 숨기고 필요한 API만 공개할 수 있습니다.",
    "",
    "### 기본 문법",
    "",
    "```jsx",
    "useImperativeHandle(ref, () => ({",
    "  // 부모에게 노출할 메서드들",
    "  focus: () => { /* ... */ },",
    "  clear: () => { /* ... */ },",
    "}), [deps]); // 의존성 배열 (선택)",
    "```",
    "",
    "| 파라미터 | 설명 |",
    "|---------|------|",
    "| `ref` | `forwardRef`로 전달받은 ref |",
    "| `createHandle` | 부모에게 노출할 객체를 반환하는 함수 |",
    "| `deps` (선택) | 의존성 배열 — 값이 바뀔 때만 핸들 재생성 |",
    "",
    "### 커스텀 Input 예제",
    "",
    "**JavaScript**",
    "```jsx",
    "import { useRef, forwardRef, useImperativeHandle, useState } from 'react';",
    "",
    "const CustomInput = forwardRef(function CustomInput({ placeholder }, ref) {",
    "  const inputRef = useRef(null);",
    "  const [value, setValue] = useState('');",
    "",
    "  useImperativeHandle(ref, () => ({",
    "    focus: () => inputRef.current.focus(),",
    "    blur: () => inputRef.current.blur(),",
    "    clear: () => setValue(''),",
    "    getValue: () => value,",
    "    setValue: (text) => setValue(text),",
    "  }));",
    "",
    "  return (",
    "    <input",
    "      ref={inputRef}",
    "      value={value}",
    "      onChange={(e) => setValue(e.target.value)}",
    "      placeholder={placeholder}",
    "      style={{ padding: '8px', border: '2px solid #3498db', borderRadius: '8px' }}",
    "    />",
    "  );",
    "});",
    "",
    "function App() {",
    "  const inputRef = useRef(null);",
    "",
    "  return (",
    "    <div>",
    "      <CustomInput ref={inputRef} placeholder=\"이름 입력\" />",
    "      <button onClick={() => inputRef.current.focus()}>포커스</button>",
    "      <button onClick={() => inputRef.current.clear()}>비우기</button>",
    "      <button onClick={() => inputRef.current.setValue('홍길동')}>이름 설정</button>",
    "      <button onClick={() => alert(inputRef.current.getValue())}>값 확인</button>",
    "    </div>",
    "  );",
    "}",
    "```",
    "",
    "**TypeScript**",
    "```tsx",
    "import { useRef, forwardRef, useImperativeHandle, useState } from 'react';",
