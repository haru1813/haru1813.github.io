    "## useLayoutEffect - 동기적 부수 효과",
    "",
    "`useLayoutEffect`는 `useEffect`와 거의 동일하지만, **DOM이 화면에 그려지기 전(paint 전)에 동기적으로 실행**됩니다. 레이아웃 측정이나 깜빡임(flicker) 방지가 필요할 때 사용합니다.",
    "",
    "### useEffect vs useLayoutEffect 실행 시점",
    "",
    "| 단계 | useEffect | useLayoutEffect |",
    "|------|-----------|-----------------|",
    "| 1. 렌더링 (가상 DOM 계산) | - | - |",
    "| 2. DOM 업데이트 | - | - |",
    "| 3. **화면 paint 전** | - | ✅ **여기서 실행** |",
    "| 4. **화면 paint 후** | ✅ **여기서 실행** | - |",
    "",
    "### 깜빡임 방지 예제",
    "",
    "버튼을 클릭하면 위치를 랜덤으로 바꾸는 예제입니다. `useEffect`를 쓰면 원래 위치가 잠깐 보였다가 이동하는 **깜빡임**이 발생합니다.",
    "",
    "```jsx",
    "import { useState, useLayoutEffect, useRef } from 'react';",
    "",
    "function NoFlickerBox() {",
    "  const [position, setPosition] = useState({ x: 0, y: 0 });",
    "  const boxRef = useRef(null);",
    "",
    "  // useEffect로 바꾸면 깜빡임 발생",
    "  useLayoutEffect(() => {",
    "    const box = boxRef.current;",
    "    if (!box) return;",
    "    // DOM이 업데이트된 직후, 화면에 그리기 전에 위치 보정",
    "    box.style.transform = `translate(${position.x}px, ${position.y}px)`;",
    "  }, [position]);",
    "",
    "  const randomize = () => {",
    "    setPosition({",
    "      x: Math.random() * 200,",
    "      y: Math.random() * 200,",
    "    });",
    "  };",
    "",
    "  return (",
    "    <div>",
    "      <button onClick={randomize}>위치 이동</button>",
    "      <div ref={boxRef} style={{",
    "        width: '50px', height: '50px', background: '#3498db',",
    "        borderRadius: '8px', transition: 'none',",
    "      }} />",
    "    </div>",
    "  );",
    "}",
    "```",
    "",
    "### DOM 측정 후 위치 조정",
    "",
    "요소의 크기를 측정한 뒤 위치를 조정해야 할 때 `useLayoutEffect`를 사용하면 **측정 → 조정**이 화면에 반영되기 전에 완료됩니다.",
    "",
    "```jsx",
    "import { useState, useLayoutEffect, useRef } from 'react';",
    "",
    "function Tooltip({ text, targetRef }) {",
    "  const tooltipRef = useRef(null);",
    "  const [coords, setCoords] = useState({ top: 0, left: 0 });",
    "",
    "  useLayoutEffect(() => {",
    "    if (!targetRef.current || !tooltipRef.current) return;",
    "    const targetRect = targetRef.current.getBoundingClientRect();",
    "    const tooltipRect = tooltipRef.current.getBoundingClientRect();",
    "    setCoords({",
    "      top: targetRect.top - tooltipRect.height - 8,",
    "      left: targetRect.left + (targetRect.width - tooltipRect.width) / 2,",
    "    });",
    "  }, [targetRef]);",
    "",
    "  return (",
    "    <div ref={tooltipRef} style={{",
    "      position: 'fixed', top: coords.top, left: coords.left,",
    "      background: '#333', color: 'white', padding: '4px 8px',",
    "      borderRadius: '4px', fontSize: '12px',",
    "    }}>",
    "      {text}",
    "    </div>",
    "  );",
    "}",
    "```",
    "",
    "### TypeScript에서 useLayoutEffect",
    "```tsx",
    "import { useState, useLayoutEffect, useRef } from 'react';",
    "",
    "interface Size {",
    "  width: number;",
    "  height: number;",
    "}",
    "",
    "function useMeasure<T extends HTMLElement>() {",
    "  const ref = useRef<T>(null);",
    "  const [size, setSize] = useState<Size>({ width: 0, height: 0 });",
    "",
    "  useLayoutEffect(() => {",
    "    if (!ref.current) return;",
    "    const { width, height } = ref.current.getBoundingClientRect();",
    "    setSize({ width, height });",
    "  }, []);",
    "",
    "  return { ref, size };",
    "}",
    "",
    "// 사용",
    "function App(): JSX.Element {",
    "  const { ref, size } = useMeasure<HTMLDivElement>();",
    "",
    "  return (",
    "    <div>",
    "      <div ref={ref} style={{ padding: '20px', background: '#f0f0f0' }}>",
    "        측정 대상 요소",
    "      </div>",
    "      <p>크기: {Math.round(size.width)} × {Math.round(size.height)}px</p>",
    "    </div>",
    "  );",
    "}",
    "```",
