    "### useState vs useReducer",
    "",
    "| 구분 | useState | useReducer |",
    "|------|----------|------------|",
    "| 적합한 경우 | 단순한 상태 (숫자, 문자열) | 복잡한 상태 (객체, 여러 상태 연관) |",
    "| 업데이트 방식 | `setState(newValue)` | `dispatch({ type, payload })` |",
    "| 상태 로직 위치 | 컴포넌트 안에 분산 | `reducer` 함수에 집중 |",
    "| 테스트 | 어려움 | `reducer`는 순수 함수라 테스트 쉬움 |",
