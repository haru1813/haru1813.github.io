    "  );",
    "}",
    "",
    "function Content() {",
    "  const { theme } = useContext(ThemeContext);",
    "  return (",
    "    <main style={{",
    "      background: theme === 'dark' ? '#222' : '#f5f5f5',",
    "      color: theme === 'dark' ? '#eee' : '#333',",
