    "### useReducer with TypeScript",
    "",
    "타입스크립트에서 `useReducer`는 **Discriminated Union**으로 액션 타입을 정의하면 강력한 타입 안전성을 얻을 수 있습니다.",
    "",
    "```tsx",
    "import { useReducer } from 'react';",
    "",
    "// 상태 타입",
    "interface CounterState {",
    "  count: number;",
    "  step: number;",
    "}",
    "",
    "// 액션 타입 (Discriminated Union)",
    "type CounterAction =",
    "  | { type: 'increment' }",
    "  | { type: 'decrement' }",
    "  | { type: 'setStep'; payload: number }",
    "  | { type: 'reset' };",
    "",
    "const initialState: CounterState = { count: 0, step: 1 };",
    "",
    "function reducer(state: CounterState, action: CounterAction): CounterState {",
    "  switch (action.type) {",
    "    case 'increment':",
    "      return { ...state, count: state.count + state.step };",
    "    case 'decrement':",
    "      return { ...state, count: state.count - state.step };",
    "    case 'setStep':",
    "      return { ...state, step: action.payload }; // payload 자동완성 지원",
    "    case 'reset':",
    "      return initialState;",
    "  }",
    "}",
    "",
    "function Counter() {",
    "  const [state, dispatch] = useReducer(reducer, initialState);",
    "",
    "  return (",
    "    <div>",
    "      <p>카운트: {state.count} (step: {state.step})</p>",
    "      <button onClick={() => dispatch({ type: 'increment' })}>+</button>",
    "      <button onClick={() => dispatch({ type: 'decrement' })}>-</button>",
    "      <button onClick={() => dispatch({ type: 'reset' })}>초기화</button>",
    "      <input type=\"number\" value={state.step}",
    "        onChange={(e) => dispatch({ type: 'setStep', payload: Number(e.target.value) })} />",
    "    </div>",
    "  );",
    "}",
    "```",
