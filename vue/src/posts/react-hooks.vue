    "#### useLocalStorage",
    "```tsx",
    "function useLocalStorage<T>(key: string, initialValue: T): [T, (value: T | ((prev: T) => T)) => void] {",
    "  const [storedValue, setStoredValue] = useState<T>(() => {",
    "    try {",
    "      const item = window.localStorage.getItem(key);",
    "      return item ? (JSON.parse(item) as T) : initialValue;",
    "    } catch {",
    "      return initialValue;",
    "    }",
    "  });",
    "",
    "  const setValue = (value: T | ((prev: T) => T)): void => {",
    "    const valueToStore = value instanceof Function ? value(storedValue) : value;",
    "    setStoredValue(valueToStore);",
    "    window.localStorage.setItem(key, JSON.stringify(valueToStore));",
    "  };",
    "",
    "  return [storedValue, setValue];",
    "}",
    "",
    "// 사용 - 제네릭 덕분에 타입이 자동 추론됨",
    "function App() {",
    "  const [name, setName] = useLocalStorage<string>('name', '');",
    "  const [settings, setSettings] = useLocalStorage<{ darkMode: boolean }>(",
    "    'settings', { darkMode: false }",
    "  );",
    "",
    "  return (",
    "    <div>",
    "      <input value={name} onChange={(e) => setName(e.target.value)} />",
    "      <button onClick={() => setSettings((prev) => ({ ...prev, darkMode: !prev.darkMode }))}> ",
    "        다크모드: {settings.darkMode ? 'ON' : 'OFF'}",
    "      </button>",
    "    </div>",
    "  );",
    "}",
    "```",
