    "  return context;",
    "}",
    "",
    "const AuthProvider: FC<{ children: ReactNode }> = ({ children }) => {",
    "  const [state, dispatch] = useReducer(authReducer, {",
    "    user: null,",
    "    isLoggedIn: false,",
    "  });",
    "  return (",
    "    <AuthContext.Provider value={{ state, dispatch }}>",
    "      {children}",
    "    </AuthContext.Provider>",
    "  );",
    "};",
    "",
    "function LoginButton() {",
