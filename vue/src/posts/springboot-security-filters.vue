    "",
    "### 1. SecurityContextHolderFilter (SecurityContext 관리)",
    "",
    "모든 보안의 기반이 되는 필터입니다. `SecurityContextRepository`에서 `SecurityContext`를 로드하여 `SecurityContextHolder`에 설정합니다.",
    "",
