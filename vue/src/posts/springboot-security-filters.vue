    "    participant H as SecurityContextHolder",
    "    participant C as 다음 필터",
    "",
    "    F->>R: loadDeferredContext(request)",
    "    R-->>F: SecurityContext (인증 정보 포함)",
    "    F->>H: setContext(securityContext)",
    "    F->>C: doFilter() - 다음 필터로 진행",
    "    C-->>F: 응답 반환",
    "    F->>H: clearContext() - 정리",
    "```",
    "",
    "```java",
    "// SecurityContext에서 현재 인증 정보를 가져오는 방법",
