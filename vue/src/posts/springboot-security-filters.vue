    "## 정리",
    "",
    "| 개념 | 설명 |",
    "|------|------|",
    "| `DelegatingFilterProxy` | Servlet Filter → Spring Bean 위임 |",
    "| `FilterChainProxy` | SecurityFilterChain들을 관리하는 핵심 엔진 |",
    "| `SecurityFilterChain` | 보안 필터들의 순서있는 목록 |",
    "| 기본 필터 ~15개 | 인증, 인가, CSRF, CORS, 세션, 예외 처리 등 |",
    "| `addFilterBefore/After` | 커스텀 필터를 원하는 위치에 삽입 |",
    "| `OncePerRequestFilter` | 커스텀 필터 구현의 권장 기반 클래스 |",
    "",
    "| 주요 필터 | 한줄 요약 |",
    "|----------|---------|",
