    "",
    "### 2. HeaderWriterFilter (보안 응답 헤더)",
    "",
    "응답에 보안 관련 HTTP 헤더를 자동으로 추가합니다.",
    "",
    "| 헤더 | 값 | 보호 대상 |",
    "|------|-----|---------|",
    "| `X-Content-Type-Options` | `nosniff` | MIME 타입 스니핑 방지 |",
    "| `X-Frame-Options` | `DENY` | 클릭재킹(iframe 삽입) 방지 |",
    "| `X-XSS-Protection` | `0` | 브라우저 XSS 필터 (비활성화 권장) |",
    "| `Cache-Control` | `no-cache, no-store` | 민감 데이터 캐시 방지 |",
    "| `Strict-Transport-Security` | `max-age=31536000` | HTTPS 강제 (HSTS) |",
    "",
    "```java",
    "@Bean",
    "public SecurityFilterChain filterChain(HttpSecurity http) throws Exception {",
    "    http.headers(headers -> headers",
    "        .frameOptions(frame -> frame.sameOrigin())       // iframe 같은 도메인만 허용",
    "        .contentTypeOptions(Customizer.withDefaults())    // nosniff",
    "        .httpStrictTransportSecurity(hsts -> hsts",
    "            .maxAgeInSeconds(31536000)",
    "            .includeSubDomains(true)",
    "        )",
    "    );",
    "    return http.build();",
    "}",
    "```",
