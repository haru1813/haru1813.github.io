    "| `UsernamePasswordAuthenticationFilter` | 폼 로그인 인증 |",
    "| `ExceptionTranslationFilter` | 보안 예외 → 401/403 응답 변환 |",
    "| `AuthorizationFilter` | URL별 접근 권한 최종 확인 |",
    "| `AnonymousAuthenticationFilter` | 미인증 → 익명 토큰 부여 |",
    "| `LogoutFilter` | 로그아웃 처리 + 세션/쿠키 정리 |",
