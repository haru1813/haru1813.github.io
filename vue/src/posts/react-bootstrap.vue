    "## 실전 예제: Todo App UI",
    "",
    "react-bootstrap 컴포넌트들을 조합한 간단한 Todo App 예제입니다.",
    "",
    "```jsx",
    "import { useState } from 'react';",
    "import Container from 'react-bootstrap/Container';",
    "import Form from 'react-bootstrap/Form';",
    "import Button from 'react-bootstrap/Button';",
    "import ListGroup from 'react-bootstrap/ListGroup';",
    "import Badge from 'react-bootstrap/Badge';",
    "import InputGroup from 'react-bootstrap/InputGroup';",
    "",
    "function TodoApp() {",
    "  const [todos, setTodos] = useState([",
    "    { id: 1, text: 'React 공부하기', done: false },",
    "    { id: 2, text: 'Bootstrap 적용하기', done: true },",
    "  ]);",
    "  const [input, setInput] = useState('');",
    "",
    "  const addTodo = () => {",
    "    if (!input.trim()) return;",
    "    setTodos([...todos, { id: Date.now(), text: input, done: false }]);",
    "    setInput('');",
    "  };",
    "",
    "  const toggleTodo = (id) => {",
    "    setTodos(todos.map((t) =>",
    "      t.id === id ? { ...t, done: !t.done } : t",
    "    ));",
    "  };",
    "",
    "  const deleteTodo = (id) => {",
    "    setTodos(todos.filter((t) => t.id !== id));",
    "  };",
    "",
    "  const remaining = todos.filter((t) => !t.done).length;",
    "",
    "  return (",
    "    <Container className=\"mt-4\" style={{ maxWidth: '600px' }}>",
    "      <h2 className=\"mb-3\">",
    "        Todo List <Badge bg=\"secondary\">{remaining}개 남음</Badge>",
    "      </h2>",
    "",
    "      <InputGroup className=\"mb-3\">",
    "        <Form.Control",
    "          placeholder=\"할 일 입력...\"",
    "          value={input}",
    "          onChange={(e) => setInput(e.target.value)}",
    "          onKeyDown={(e) => e.key === 'Enter' && addTodo()}",
    "        />",
    "        <Button variant=\"primary\" onClick={addTodo}>추가</Button>",
    "      </InputGroup>",
    "",
    "      <ListGroup>",
    "        {todos.map((todo) => (",
    "          <ListGroup.Item",
    "            key={todo.id}",
    "            className=\"d-flex justify-content-between align-items-center\"",
    "          >",
    "            <Form.Check",
    "              type=\"checkbox\"",
    "              checked={todo.done}",
    "              onChange={() => toggleTodo(todo.id)}",
    "              label={",
    "                <span style={{",
    "                  textDecoration: todo.done ? 'line-through' : 'none',",
    "                  color: todo.done ? '#999' : 'inherit',",
    "                }}>",
    "                  {todo.text}",
    "                </span>",
    "              }",
    "            />",
    "            <Button",
    "              variant=\"outline-danger\"",
    "              size=\"sm\"",
    "              onClick={() => deleteTodo(todo.id)}",
    "            >",
    "              삭제",
    "            </Button>",
    "          </ListGroup.Item>",
    "        ))}",
    "      </ListGroup>",
    "    </Container>",
    "  );",
    "}",
    "",
