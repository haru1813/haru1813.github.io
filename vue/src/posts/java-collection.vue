    "// [김철수, 김영희]",
    "",
    "// map: 요소를 변환",
    "List<Integer> nameLengths = names.stream()",
    "    .map(String::length)",
    "    .toList();",
    "// [3, 3, 3, 3, 3]",
    "",
    "// sorted: 정렬",
    "List<String> sorted = names.stream()",
    "    .sorted()",
    "    .toList();",
    "// [김영희, 김철수, 박민수, 이영희, 홍길동]",
