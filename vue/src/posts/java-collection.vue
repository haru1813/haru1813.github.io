    "## Queue / Deque - 대기열과 양방향 큐",
    "",
    "### Queue (FIFO)",
    "",
    "```java",
    "Queue<String> queue = new LinkedList<>();",
    "",
    "// 삽입",
    "queue.offer(\"1번 손님\");",
    "queue.offer(\"2번 손님\");",
    "queue.offer(\"3번 손님\");",
    "",
    "// 조회 (제거하지 않음)",
    "String front = queue.peek(); // \"1번 손님\"",
    "",
    "// 추출 (제거)",
    "String served = queue.poll(); // \"1번 손님\"",
    "System.out.println(queue);    // [2번 손님, 3번 손님]",
    "```",
    "",
    "### Queue 메서드 비교",
    "",
    "| 연산 | 예외 발생 | null 반환 |",
    "|------|----------|----------|",
    "| 삽입 | `add(e)` → IllegalStateException | `offer(e)` → false |",
    "| 조회 | `element()` → NoSuchElementException | `peek()` → null |",
    "| 추출 | `remove()` → NoSuchElementException | `poll()` → null |",
    "",
    "> **실무 팁**: 예외 대신 null을 반환하는 `offer`, `peek`, `poll`을 주로 사용합니다.",
    "",
    "### PriorityQueue (우선순위 큐)",
    "",
    "요소가 **우선순위(기본: 오름차순)**에 따라 정렬됩니다.",
    "",
    "```java",
    "// 기본: 최소 힙 (작은 값이 먼저)",
    "Queue<Integer> minHeap = new PriorityQueue<>();",
    "minHeap.offer(30);",
    "minHeap.offer(10);",
    "minHeap.offer(20);",
    "System.out.println(minHeap.poll()); // 10",
    "System.out.println(minHeap.poll()); // 20",
    "",
    "// 최대 힙 (큰 값이 먼저)",
    "Queue<Integer> maxHeap = new PriorityQueue<>(Comparator.reverseOrder());",
    "maxHeap.offer(30);",
    "maxHeap.offer(10);",
    "maxHeap.offer(20);",
    "System.out.println(maxHeap.poll()); // 30",
    "```",
    "",
    "### Deque (양방향 큐)",
    "",
    "`Deque`는 **양쪽 끝에서 삽입/삭제**가 가능한 자료구조로, 스택과 큐를 모두 대체할 수 있습니다.",
    "",
    "```java",
    "Deque<String> deque = new ArrayDeque<>();",
    "",
    "// 큐처럼 사용 (FIFO)",
    "deque.offerLast(\"A\");",
    "deque.offerLast(\"B\");",
    "deque.pollFirst(); // \"A\"",
    "",
    "// 스택처럼 사용 (LIFO)",
    "deque.push(\"X\");   // = offerFirst",
    "deque.push(\"Y\");",
    "deque.pop();        // \"Y\" (= pollFirst)",
    "```",
    "",
    "> **`Stack` 클래스 대신 `ArrayDeque`를 사용하세요.** `Stack`은 `Vector` 기반 레거시 클래스이며, `ArrayDeque`가 더 빠릅니다.",
    "",
