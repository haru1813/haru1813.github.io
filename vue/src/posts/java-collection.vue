    "### 실전 Stream 활용",
    "",
    "```java",
    "// 상품 리스트 처리",
    "record Product(String name, String category, int price) {}",
    "",
    "List<Product> products = List.of(",
    "    new Product(\"노트북\", \"전자기기\", 1500000),",
    "    new Product(\"마우스\", \"전자기기\", 35000),",
    "    new Product(\"셔츠\", \"의류\", 45000),",
    "    new Product(\"모니터\", \"전자기기\", 350000),",
    "    new Product(\"청바지\", \"의류\", 65000),",
    "    new Product(\"키보드\", \"전자기기\", 120000)",
    ");",
    "",
    "// 카테고리별 평균 가격",
    "Map<String, Double> avgByCategory = products.stream()",
    "    .collect(Collectors.groupingBy(",
    "        Product::category,",
    "        Collectors.averagingInt(Product::price)",
    "    ));",
    "// {전자기기=501250.0, 의류=55000.0}",
    "",
    "// 가격 높은 순으로 상위 3개 상품명",
    "List<String> top3 = products.stream()",
    "    .sorted(Comparator.comparingInt(Product::price).reversed())",
    "    .limit(3)",
    "    .map(Product::name)",
    "    .toList();",
    "// [노트북, 모니터, 키보드]",
    "",
    "// 10만원 이상 전자기기 총 가격",
    "int totalExpensive = products.stream()",
