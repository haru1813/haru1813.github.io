    "## Stream API (Java 8+)",
    "",
    "**Stream**은 컬렉션 데이터를 **함수형 스타일**로 처리하는 API입니다. 원본 데이터를 변경하지 않고, 파이프라인 형태로 필터링·변환·집계를 수행합니다.",
    "",
    "```mermaid",
    "graph LR",
    "    A[\"컬렉션\"] -->|stream()| B[\"Stream 생성\"]",
    "    B -->|filter / map / sorted| C[\"중간 연산 (지연 실행)\"]",
    "    C -->|collect / forEach / count| D[\"최종 연산 (실행 트리거)\"]",
    "```",
    "",
    "### Stream 생성",
    "",
    "```java",
    "// 컬렉션에서 생성",
    "List<String> names = List.of(\"홍길동\", \"김철수\", \"이영희\", \"박민수\");",
    "Stream<String> stream1 = names.stream();",
    "",
    "// 배열에서 생성",
    "int[] arr = {1, 2, 3, 4, 5};",
    "IntStream stream2 = Arrays.stream(arr);",
    "",
    "// 직접 생성",
    "Stream<String> stream3 = Stream.of(\"A\", \"B\", \"C\");",
    "",
    "// 범위 생성",
    "IntStream range = IntStream.rangeClosed(1, 10); // 1~10",
    "```",
    "",
    "### 중간 연산 - filter, map, sorted",
    "",
    "중간 연산은 **지연 실행(Lazy)**되어, 최종 연산이 호출될 때까지 실행되지 않습니다.",
    "",
    "```java",
    "List<String> names = List.of(\"홍길동\", \"김철수\", \"이영희\", \"박민수\", \"김영희\");",
    "",
    "// filter: 조건에 맞는 요소만 통과",
    "List<String> kims = names.stream()",
    "    .filter(name -> name.startsWith(\"김\"))",
