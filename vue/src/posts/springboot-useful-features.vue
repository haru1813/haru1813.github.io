    "# 6. RestTemplate (레거시 동기 HTTP 클라이언트)",
    "",
    "## 개념",
    "",
    "`RestTemplate`은 Spring 3부터 사용해온 **동기 블로킹 HTTP 클라이언트**입니다. Spring 5부터 **유지 모드(maintenance mode)**이며 신규 프로젝트에서는 `WebClient`를 권장합니다.",
    "",
    "## Bean 설정",
    "",
    "```java",
    "@Configuration",
    "public class RestTemplateConfig {",
    "",
    "    @Bean",
    "    public RestTemplate restTemplate(RestTemplateBuilder builder) {",
    "        return builder",
    "                .setConnectTimeout(Duration.ofSeconds(5))",
    "                .setReadTimeout(Duration.ofSeconds(5))",
    "                .build();",
    "    }",
    "}",
    "```",
    "",
    "## GET 요청",
    "",
    "```java",
    "@Service",
    "@RequiredArgsConstructor",
    "public class UserApiService {",
    "",
    "    private final RestTemplate restTemplate;",
    "",
    "    // 단건 조회",
    "    public UserResponse getUser(Long id) {",
    "        return restTemplate.getForObject(",
    "                \"https://api.example.com/users/{id}\",",
    "                UserResponse.class,",
    "                id);",
    "    }",
    "",
    "    // 상태 코드 포함 응답",
    "    public ResponseEntity<UserResponse> getUserWithStatus(Long id) {",
    "        return restTemplate.getForEntity(",
    "                \"https://api.example.com/users/{id}\",",
    "                UserResponse.class,",
    "                id);",
    "    }",
    "",
    "    // 목록 조회",
    "    public List<UserResponse> getAllUsers() {",
    "        ResponseEntity<UserResponse[]> response = restTemplate.getForEntity(",
    "                \"https://api.example.com/users\",",
    "                UserResponse[].class);",
    "        return Arrays.asList(response.getBody());",
    "    }",
    "}",
    "```",
    "",
    "## POST 요청",
    "",
    "```java",
    "public UserResponse createUser(CreateUserRequest request) {",
    "    HttpHeaders headers = new HttpHeaders();",
