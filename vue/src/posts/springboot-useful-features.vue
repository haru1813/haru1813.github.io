    "# 3. @Retryable (자동 재시도)",
    "",
    "## 개념",
    "",
    "네트워크 오류, 일시적 DB 연결 실패 등 **일시적 장애**에 대해 자동으로 재시도합니다.",
    "",
    "```mermaid",
    "flowchart TD",
    "    A[\"메서드 호출\"] --> B{\"성공?\"}",
    "    B -->|예| C[\"결과 반환\"]",
    "    B -->|아니오| D{\"재시도 횟수 < maxAttempts?\"}",
    "    D -->|예| E[\"대기 (backoff)\"]",
    "    E --> A",
    "    D -->|아니오| F[\"@Recover 호출\"]",
    "```",
    "",
    "## 프로젝트 설정",
    "",
    "```groovy",
    "dependencies {",
    "    implementation 'org.springframework.retry:spring-retry'",
    "    implementation 'org.springframework:spring-aspects'",
    "}",
    "```",
    "",
    "```java",
    "@SpringBootApplication",
    "@EnableRetry",
    "public class DemoApplication { ... }",
    "```",
    "",
    "## 사용법",
    "",
    "```java",
    "@Service",
    "@Slf4j",
    "public class ExternalApiService {",
    "",
    "    @Retryable(",
    "        retryFor = {RestClientException.class, SocketTimeoutException.class},",
    "        maxAttempts = 3,",
    "        backoff = @Backoff(delay = 1000, multiplier = 2.0)",
    "    )",
    "    public ApiResponse callExternalApi(String endpoint) {",
    "        log.info(\"API 호출 시도: {}\", endpoint);",
    "        return restClient.get(endpoint);  // 실패 시 자동 재시도",
    "    }",
    "",
    "    @Recover",
    "    public ApiResponse recoverFromApiFailure(",
    "            RestClientException e, String endpoint) {",
    "        log.error(\"API 호출 최종 실패 (3회 재시도 후): {}\", endpoint);",
    "        return ApiResponse.fallback(\"서비스 일시 장애\");",
    "    }",
    "}",
    "```",
    "",
    "### 실행 흐름",
    "",
    "```",
    "API 호출 시도: /api/data       ← 1회차 (실패, 1초 대기)",
    "API 호출 시도: /api/data       ← 2회차 (실패, 2초 대기)",
    "API 호출 시도: /api/data       ← 3회차 (실패)",
    "API 호출 최종 실패: /api/data   ← @Recover 호출",
    "```",
    "",
    "### @Retryable 속성",
    "",
    "| 속성 | 설명 | 기본값 |",
    "|------|------|--------|",
    "| `retryFor` | 재시도할 예외 | `Exception.class` |",
    "| `noRetryFor` | 재시도하지 않을 예외 | - |",
    "| `maxAttempts` | 최대 시도 횟수 | 3 |",
    "| `backoff` | 재시도 간격 | 1000ms |",
    "",
    "### @Backoff 속성",
    "",
    "| 속성 | 설명 | 기본값 |",
    "|------|------|--------|",
    "| `delay` | 재시도 대기 시간 (ms) | 1000 |",
    "| `multiplier` | 대기 시간 배수 | 0 (고정) |",
    "| `maxDelay` | 최대 대기 시간 (ms) | 0 (무제한) |",
    "",
    "```java",
    "// 1초 → 2초 → 4초 (지수 백오프), 최대 10초",
    "@Backoff(delay = 1000, multiplier = 2.0, maxDelay = 10000)",
    "```",
    "",
    "### @Recover 규칙",
    "",
    "```java",
    "// @Recover 메서드는 반드시:",
    "// 1. 같은 클래스에 있어야 함",
    "// 2. 리턴 타입이 @Retryable 메서드와 같아야 함",
    "// 3. 첫 번째 파라미터가 예외 타입이어야 함",
    "// 4. 나머지 파라미터는 @Retryable 메서드와 같아야 함",
    "",
    "@Retryable(retryFor = IOException.class)",
    "public String fetchData(String url, int timeout) { ... }",
    "",
    "@Recover",
    "public String recoverFetch(IOException e, String url, int timeout) {",
    "    return \"fallback data\";",
    "}",
    "```",
    "",
