    "# 4. @Lock (동시성 제어)",
    "",
    "## 개념",
    "",
    "여러 요청이 **동시에 같은 데이터를 수정**할 때 데이터 정합성을 보장하는 메커니즘입니다.",
    "",
    "```mermaid",
    "sequenceDiagram",
    "    participant A as 사용자 A",
    "    participant B as 사용자 B",
    "    participant DB as DB (재고: 1개)",
    "",
    "    Note over A,DB: ❌ 동시성 문제 (락 없음)",
    "    A->>DB: SELECT 재고 → 1개",
    "    B->>DB: SELECT 재고 → 1개",
    "    A->>DB: UPDATE 재고 = 0 (주문 성공)",
    "    B->>DB: UPDATE 재고 = 0 (주문 성공?! → 재고 부족인데 성공)",
    "```",
    "",
    "## 4.1 낙관적 락 (Optimistic Lock)",
    "",
    "**충돌이 드물다**고 가정하고, 업데이트 시점에 버전을 비교하여 충돌을 감지합니다.",
    "",
    "```mermaid",
    "flowchart TD",
    "    A[\"조회 (version=1)\"] --> B[\"비즈니스 로직\"]",
    "    B --> C{\"UPDATE WHERE version=1\"}",
    "    C -->|성공 (version→2)| D[\"완료\"]",
    "    C -->|실패 (version 불일치)| E[\"OptimisticLockingFailureException\"]",
    "    E --> F[\"재시도 또는 에러 반환\"]",
    "```",
    "",
    "### Entity에 @Version 추가",
    "",
    "```java",
    "@Entity",
    "@Getter @Setter",
    "public class Product {",
    "",
    "    @Id @GeneratedValue(strategy = GenerationType.IDENTITY)",
    "    private Long id;",
    "",
    "    private String name;",
    "    private int stock;",
    "",
    "    @Version",
    "    private Long version;  // JPA가 자동 관리",
    "}",
    "```",
    "",
    "> `@Version` 필드가 있으면 JPA는 UPDATE 시 `WHERE version = ?`을 자동 추가합니다.",
    "",
    "### Repository에서 Lock 사용",
    "",
    "```java",
    "public interface ProductRepository extends JpaRepository<Product, Long> {",
    "",
    "    @Lock(LockModeType.OPTIMISTIC)",
    "    @Query(\"SELECT p FROM Product p WHERE p.id = :id\")",
    "    Optional<Product> findByIdWithOptimisticLock(@Param(\"id\") Long id);",
    "}",
    "```",
    "",
    "### Service",
    "",
    "```java",
    "@Service",
    "@RequiredArgsConstructor",
    "public class OrderService {",
    "",
    "    private final ProductRepository productRepository;",
    "",
    "    @Transactional",
    "    public void order(Long productId, int quantity) {",
    "        Product product = productRepository",
    "                .findByIdWithOptimisticLock(productId)",
    "                .orElseThrow();",
    "",
    "        if (product.getStock() < quantity) {",
    "            throw new RuntimeException(\"재고 부족\");",
    "        }",
    "",
    "        product.setStock(product.getStock() - quantity);",
    "        // 커밋 시 version 검증 → 충돌 시 OptimisticLockingFailureException",
    "    }",
    "}",
    "```",
    "",
    "### @Retryable과 조합",
    "",
    "```java",
    "@Retryable(",
    "    retryFor = OptimisticLockingFailureException.class,",
    "    maxAttempts = 3,",
    "    backoff = @Backoff(delay = 100)",
    ")",
    "@Transactional",
    "public void order(Long productId, int quantity) {",
    "    // 충돌 시 자동 재시도",
    "}",
    "```",
    "",
    "## 4.2 비관적 락 (Pessimistic Lock)",
    "",
    "**충돌이 자주 발생**한다고 가정하고, 조회 시점부터 **행을 잠급니다**.",
    "",
    "```mermaid",
    "sequenceDiagram",
    "    participant A as 사용자 A",
    "    participant B as 사용자 B",
    "    participant DB as DB",
    "",
    "    A->>DB: SELECT ... FOR UPDATE (행 잠금 🔒)",
    "    B->>DB: SELECT ... FOR UPDATE (대기 ⏳)",
    "    A->>DB: UPDATE 재고 = 0",
    "    A->>DB: COMMIT (잠금 해제 🔓)",
    "    B->>DB: 잠금 획득 → SELECT",
    "    B->>DB: 재고 = 0 → 재고 부족 에러",
    "```",
    "",
    "```java",
    "public interface ProductRepository extends JpaRepository<Product, Long> {",
    "",
    "    @Lock(LockModeType.PESSIMISTIC_WRITE)",
    "    @Query(\"SELECT p FROM Product p WHERE p.id = :id\")",
    "    Optional<Product> findByIdWithPessimisticLock(@Param(\"id\") Long id);",
    "}",
    "```",
    "",
    "## 낙관적 vs 비관적 락 비교",
    "",
    "| 비교 | 낙관적 락 (Optimistic) | 비관적 락 (Pessimistic) |",
    "|------|---------------------|----------------------|",
    "| 잠금 시점 | 커밋 시점 (충돌 감지) | **조회 시점** (선점) |",
    "| DB 잠금 | 없음 (`@Version` 비교) | `SELECT FOR UPDATE` |",
    "| 성능 | 충돌 적으면 높음 | 항상 잠금 → 대기 발생 |",
    "| 충돌 시 | 예외 + 재시도 | 대기 후 처리 |",
    "| 적합 | 읽기 많고 충돌 드문 경우 | **동시 쓰기가 빈번**한 경우 |",
    "| 예시 | 상품 정보 수정 | 재고 차감, 좌석 예약 |",
    "",
    "---",
