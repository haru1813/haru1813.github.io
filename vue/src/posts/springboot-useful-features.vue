    "# 2. @TransactionalEventListener",
    "",
    "기본 `@EventListener`는 이벤트 발행 즉시 실행되므로, **트랜잭션이 롤백되어도 이미 실행된 작업을 되돌릴 수 없습니다**. `@TransactionalEventListener`는 트랜잭션 커밋 후에 실행됩니다.",
    "",
    "```mermaid",
    "sequenceDiagram",
    "    participant S as Service",
    "    participant DB as DB",
    "    participant L as Listener",
    "",
    "    Note over S,L: @EventListener (위험)",
    "    S->>DB: 회원 저장",
    "    S->>L: 이벤트 → 이메일 발송 ✅",
    "    S->>DB: 후속 로직 예외 → 롤백 ❌",
    "    Note over L: 이메일은 이미 발송됨 😱",
    "",
    "    Note over S,L: @TransactionalEventListener (안전)",
    "    S->>DB: 회원 저장",
    "    S->>S: 이벤트 등록 (대기)",
    "    S->>DB: 트랜잭션 커밋 ✅",
    "    S->>L: 커밋 후 이메일 발송 ✅",
    "```",
    "",
    "### 사용법",
    "",
    "```java",
    "@Component",
    "@Slf4j",
    "public class MemberEventListener {",
    "",
    "    // 트랜잭션 커밋 후 실행 (기본)",
    "    @TransactionalEventListener(phase = TransactionPhase.AFTER_COMMIT)",
    "    public void onMemberCreated(MemberCreatedEvent event) {",
    "        log.info(\"[커밋 후] 환영 이메일 발송: {}\", event.getEmail());",
    "    }",
    "",
    "    // 트랜잭션 롤백 후 실행",
    "    @TransactionalEventListener(phase = TransactionPhase.AFTER_ROLLBACK)",
    "    public void onMemberCreationFailed(MemberCreatedEvent event) {",
    "        log.error(\"[롤백 후] 가입 실패 기록: {}\", event.getEmail());",
    "    }",
    "}",
    "```",
    "",
    "### TransactionPhase",
    "",
    "| Phase | 실행 시점 | 사용 예 |",
    "|-------|---------|--------|",
    "| `AFTER_COMMIT` (기본) | 커밋 후 | 이메일, 알림, 외부 API 호출 |",
    "| `AFTER_ROLLBACK` | 롤백 후 | 실패 로깅, 보상 처리 |",
    "| `AFTER_COMPLETION` | 커밋 또는 롤백 후 | 리소스 정리 |",
    "| `BEFORE_COMMIT` | 커밋 직전 | 최종 검증 |",
    "",
    "### 주의: 트랜잭션 없으면 실행 안 됨",
    "",
    "```java",
    "// ❌ @Transactional 없으면 @TransactionalEventListener가 동작 안 함",
    "public void someMethod() {",
    "    eventPublisher.publishEvent(new SomeEvent());",
    "}",
    "",
    "// ✅ @Transactional이 있어야 정상 동작",
    "@Transactional",
    "public void someMethod() {",
    "    eventPublisher.publishEvent(new SomeEvent());",
    "}",
    "",
    "// ✅ 또는 fallbackExecution = true로 트랜잭션 없이도 실행 허용",
    "@TransactionalEventListener(fallbackExecution = true)",
    "public void handle(SomeEvent event) { ... }",
    "```",
    "",
