    "        log.info(\"감사 로그 저장 (독립 트랜잭션)\");",
    "        // 이 메서드는 별도 트랜잭션",
    "        // OrderService 트랜잭션이 롤백되어도 로그는 저장됨",
    "    }",
    "}",
    "```",
