    "# 1. @Transactional 고급",
    "",
    "## 1.1 트랜잭션 전파 (Propagation)",
    "",
    "하나의 트랜잭션 안에서 **다른 트랜잭션 메서드를 호출**할 때 어떻게 동작할지를 결정합니다.",
    "",
    "```mermaid",
    "flowchart TD",
    "    A[\"ServiceA.methodA()\"] -->|호출| B[\"ServiceB.methodB()\"]",
    "    A -.->|\"트랜잭션 A가<br/>이미 존재\"| B",
    "    B -.->|\"methodB의<br/>Propagation에 따라<br/>동작 결정\"| C{\"전파 옵션?\"}",
    "    C -->|REQUIRED| D[\"기존 트랜잭션 참여\"]",
    "    C -->|REQUIRES_NEW| E[\"새 트랜잭션 생성\"]",
    "    C -->|MANDATORY| F[\"기존 없으면 예외\"]",
    "```",
    "",
    "### 전파 옵션",
    "",
    "| Propagation | 기존 트랜잭션 있을 때 | 없을 때 | 사용 예 |",
    "|------------|-----------------|--------|--------|",
    "| `REQUIRED` (기본) | **참여** | 새로 생성 | 대부분의 Service 메서드 |",
    "| `REQUIRES_NEW` | **일시 중단** → 새로 생성 | 새로 생성 | 독립 트랜잭션이 필요할 때 |",
    "| `MANDATORY` | 참여 | **예외 발생** | 반드시 트랜잭션 내에서만 호출 |",
    "| `SUPPORTS` | 참여 | 트랜잭션 없이 실행 | 있으면 쓰고 없으면 말고 |",
    "| `NOT_SUPPORTED` | 일시 중단 | 트랜잭션 없이 실행 | 트랜잭션 불필요한 작업 |",
    "| `NEVER` | **예외 발생** | 트랜잭션 없이 실행 | 트랜잭션 금지 |",
    "| `NESTED` | 중첩 트랜잭션(Savepoint) | 새로 생성 | 부분 롤백이 필요할 때 |",
    "",
    "### REQUIRED (기본) vs REQUIRES_NEW",
    "",
    "```java",
    "@Service",
    "@RequiredArgsConstructor",
    "public class OrderService {",
    "",
    "    private final OrderRepository orderRepository;",
    "    private final PaymentService paymentService;",
    "    private final AuditLogService auditLogService;",
    "",
    "    @Transactional",
    "    public void createOrder(OrderRequest request) {",
    "        Order order = orderRepository.save(request.toEntity());",
    "",
    "        // REQUIRED: 주문과 같은 트랜잭션 → 결제 실패 시 주문도 롤백",
    "        paymentService.processPayment(order);",
    "",
    "        // REQUIRES_NEW: 별도 트랜잭션 → 주문 롤백과 무관하게 로그 저장",
    "        auditLogService.saveLog(order);",
    "    }",
    "}",
    "```",
    "",
    "```java",
    "@Service",
    "public class PaymentService {",
    "",
    "    @Transactional(propagation = Propagation.REQUIRED)  // 기본값",
    "    public void processPayment(Order order) {",
    "        // OrderService의 트랜잭션에 참여",
    "        // 여기서 예외 → OrderService도 롤백",
    "    }",
    "}",
    "```",
