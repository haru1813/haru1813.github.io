    "## 1.2 트랜잭션 격리 수준 (Isolation)",
    "",
    "여러 트랜잭션이 **동시에 같은 데이터에 접근**할 때 어떤 수준으로 격리할지를 결정합니다.",
    "",
    "| Isolation | Dirty Read | Non-Repeatable Read | Phantom Read | 성능 |",
    "|-----------|-----------|-------------------|-------------|------|",
    "| `DEFAULT` | DB 기본값 | DB 기본값 | DB 기본값 | - |",
    "| `READ_UNCOMMITTED` | O | O | O | 가장 빠름 |",
    "| `READ_COMMITTED` | **X** | O | O | 빠름 |",
    "| `REPEATABLE_READ` | **X** | **X** | O | 보통 |",
    "| `SERIALIZABLE` | **X** | **X** | **X** | 가장 느림 |",
    "",
    "### 용어 설명",
    "",
    "| 현상 | 설명 |",
    "|------|------|",
    "| **Dirty Read** | 다른 트랜잭션이 커밋하지 않은 데이터를 읽음 |",
    "| **Non-Repeatable Read** | 같은 쿼리를 두 번 실행했는데 결과가 다름 (UPDATE) |",
    "| **Phantom Read** | 같은 조건 조회인데 행이 추가/삭제됨 (INSERT/DELETE) |",
    "",
    "```java",
    "// 정산 같이 정확한 데이터가 필요한 경우",
    "@Transactional(isolation = Isolation.REPEATABLE_READ)",
    "public BigDecimal calculateDailyRevenue(LocalDate date) {",
    "    return orderRepository.sumAmountByDate(date);",
    "}",
    "",
    "// 단순 조회 (성능 우선)",
    "@Transactional(isolation = Isolation.READ_COMMITTED)",
    "public List<Product> getProducts() {",
    "    return productRepository.findAll();",
    "}",
    "```",
    "",
    "> 대부분의 경우 `DEFAULT`(DB 기본값)로 충분합니다. MySQL은 `REPEATABLE_READ`, PostgreSQL은 `READ_COMMITTED`가 기본입니다.",
    "",
    "## 1.3 읽기 전용 트랜잭션",
    "",
    "```java",
    "@Service",
    "@RequiredArgsConstructor",
    "@Transactional(readOnly = true)  // 클래스 레벨: 모든 메서드 기본 읽기 전용",
    "public class ProductQueryService {",
    "",
    "    private final ProductRepository productRepository;",
    "",
    "    // readOnly = true (클래스 레벨 상속)",
    "    public List<Product> findAll() {",
    "        return productRepository.findAll();",
    "    }",
    "",
    "    // readOnly = true (클래스 레벨 상속)",
    "    public Product findById(Long id) {",
    "        return productRepository.findById(id).orElseThrow();",
    "    }",
    "",
    "    // 쓰기가 필요한 메서드만 readOnly = false 재정의",
    "    @Transactional(readOnly = false)",
    "    public Product save(Product product) {",
    "        return productRepository.save(product);",
    "    }",
    "}",
    "```",
    "",
    "### readOnly = true의 이점",
    "",
    "| 항목 | 설명 |",
    "|------|------|",
    "| **Dirty Checking 생략** | 영속성 컨텍스트가 스냅샷을 유지하지 않음 → 메모리 절약 |",
    "| **DB 힌트** | MySQL은 슬레이브(읽기 전용 DB)로 라우팅 가능 |",
    "| **안전성** | 실수로 `save()`를 호출해도 DB에 반영 안 됨 |",
    "",
    "```mermaid",
    "flowchart LR",
    "    subgraph \"readOnly = false (기본)\"",
    "        A1[\"조회\"] --> B1[\"스냅샷 저장\"]",
    "        B1 --> C1[\"커밋 시 비교 (Dirty Checking)\"]",
    "        C1 --> D1[\"변경 있으면 UPDATE\"]",
    "    end",
    "    subgraph \"readOnly = true\"",
    "        A2[\"조회\"] --> B2[\"스냅샷 저장 안 함\"]",
    "        B2 --> C2[\"Dirty Checking 생략 ⚡\"]",
    "    end",
    "```",
