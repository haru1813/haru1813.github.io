    "-- 롤에 포함된 시스템 권한 조회",
    "SELECT * FROM ROLE_SYS_PRIVS",
    " WHERE ROLE = 'MANAGER_ROLE';",
    "",
    "-- 롤에 포함된 객체 권한 조회",
