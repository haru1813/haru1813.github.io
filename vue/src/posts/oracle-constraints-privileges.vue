    "## 10. 롤 (ROLE)",
    "",
    "롤은 여러 권한을 하나로 묶어서 **한 번에 부여**할 수 있는 권한 그룹입니다. 사용자마다 일일이 권한을 부여하는 번거로움을 줄여줍니다.",
    "",
    "### Oracle 기본 제공 롤",
    "",
    "| 롤 | 포함된 권한 |",
    "|-----|------------|",
    "| **CONNECT** | CREATE SESSION 등 접속 관련 권한 |",
    "| **RESOURCE** | CREATE TABLE, CREATE SEQUENCE, CREATE PROCEDURE 등 리소스 생성 권한 |",
    "| **DBA** | 데이터베이스 관리에 필요한 모든 권한 |",
    "",
    "```sql",
    "-- 기본 롤로 권한 부여 (가장 많이 쓰는 패턴)",
    "GRANT CONNECT, RESOURCE TO hong;",
    "```",
    "",
    "### 사용자 정의 롤 생성",
    "",
    "```sql",
    "-- 1. 롤 생성",
    "CREATE ROLE manager_role;",
    "",
    "-- 2. 롤에 권한 부여",
    "GRANT CREATE SESSION, CREATE TABLE, CREATE VIEW TO manager_role;",
    "GRANT SELECT, INSERT ON SCOTT.EMP TO manager_role;",
    "",
    "-- 3. 롤을 사용자에게 부여",
    "GRANT manager_role TO hong;",
    "GRANT manager_role TO kim;",
    "",
    "-- 여러 사용자에게 동일한 권한을 한 번에 관리할 수 있음",
    "```",
    "",
    "### 롤 권한 회수 및 삭제",
    "",
    "```sql",
    "-- 사용자에게서 롤 회수",
    "REVOKE manager_role FROM hong;",
    "",
    "-- 롤 삭제 (롤에 포함된 권한도 회수됨)",
    "DROP ROLE manager_role;",
    "```",
    "",
    "### 롤 정보 조회",
    "",
    "```sql",
    "-- 사용자에게 부여된 롤 조회",
    "SELECT * FROM USER_ROLE_PRIVS;",
