    "",
    "| 레이블 | 카디널리티 | 주의 |",
    "|--------|-----------|------|",
    "| `method` (GET/POST) | 낮음 ✅ | 안전 |",
    "| `status` (200/404/500) | 낮음 ✅ | 안전 |",
    "| `uri` (/api/users) | 중간 ⚠️ | 경로 변수 주의 |",
    "| `userId` (사용자 ID) | 높음 ❌ | 절대 사용 금지 |",
    "| `orderId` (주문 ID) | 높음 ❌ | 절대 사용 금지 |",
    "",
