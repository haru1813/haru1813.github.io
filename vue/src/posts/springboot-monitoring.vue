    "## PromQL 기본",
    "",
    "**PromQL**(Prometheus Query Language)은 시계열 데이터를 쿼리하는 언어입니다.",
    "",
    "### 기본 문법",
    "",
    "```",
    "# 특정 메트릭 조회",
    "http_server_requests_seconds_count",
    "",
    "# 레이블 필터링",
    "http_server_requests_seconds_count{method=\"GET\", uri=\"/api/orders\"}",
    "",
    "# 정규식 필터링",
    "http_server_requests_seconds_count{uri=~\"/api/.*\"}",
    "",
    "# 제외 필터",
    "http_server_requests_seconds_count{status!=\"200\"}",
    "```",
    "",
    "### 주요 함수",
    "",
    "| 함수 | 설명 | 예시 |",
    "|------|------|------|",
    "| `rate()` | 초당 평균 증가율 | `rate(http_server_requests_seconds_count[5m])` |",
    "| `increase()` | 지정 기간 동안 총 증가량 | `increase(orders_created_total[1h])` |",
    "| `sum()` | 합계 | `sum(rate(http_server_requests_seconds_count[5m]))` |",
    "| `avg()` | 평균 | `avg(jvm_memory_used_bytes)` |",
    "| `max()` / `min()` | 최대/최소 | `max(jvm_threads_live_threads)` |",
    "| `histogram_quantile()` | 히스토그램에서 퍼센타일 | `histogram_quantile(0.95, ...)` |",
    "| `topk()` | 상위 N개 | `topk(5, rate(http_server_requests_seconds_count[5m]))` |",
    "",
    "### 자주 쓰는 PromQL 쿼리",
    "",
    "| 목적 | PromQL |",
    "|------|--------|",
    "| 초당 요청 수 (RPS) | `sum(rate(http_server_requests_seconds_count[5m]))` |",
    "| 95번째 백분위 응답 시간 | `histogram_quantile(0.95, sum(rate(http_server_requests_seconds_bucket[5m])) by (le))` |",
    "| 에러율 (5xx) | `sum(rate(http_server_requests_seconds_count{status=~\"5..\"}[5m])) / sum(rate(http_server_requests_seconds_count[5m])) * 100` |",
    "| JVM 힙 메모리 사용률 | `jvm_memory_used_bytes{area=\"heap\"} / jvm_memory_max_bytes{area=\"heap\"} * 100` |",
    "| GC 횟수 (분당) | `rate(jvm_gc_pause_seconds_count[5m]) * 60` |",
    "| 활성 스레드 수 | `jvm_threads_live_threads` |",
    "| 1시간 동안 주문 수 | `increase(orders_created_total[1h])` |",
    "| CPU 사용률 | `system_cpu_usage * 100` |",
    "",
