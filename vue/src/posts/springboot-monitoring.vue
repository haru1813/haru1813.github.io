    "    .register(registry);",
    "",
    "// ✅ 좋은 예 - 낮은 카디널리티",
    "Counter.builder(\"api.calls\")",
    "    .tag(\"endpoint\", \"/api/orders\")",
    "    .tag(\"method\", \"GET\")",
    "    .register(registry);",
