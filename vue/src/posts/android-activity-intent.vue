    "| `singleTop` | 이미 최상단에 있으면 재사용 (onNewIntent 호출) |",
    "| `singleTask` | Task 내에 하나만 존재. 위의 Activity들 제거 |",
    "| `singleInstance` | 독립 Task에서 단독 실행 |",
