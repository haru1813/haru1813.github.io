    "    end",
    "    D -.->|뒤로 가기| C",
    "    C -.->|뒤로 가기| B",
    "    B -.->|뒤로 가기| A",
    "```",
    "",
    "### Back Stack 동작 원리",
    "",
    "1. **startActivity()** → 새 Activity가 Stack 맨 위에 추가",
    "2. **뒤로 가기 버튼** → 현재 Activity가 Stack에서 제거(pop), 이전 Activity가 보임",
    "3. **finish()** → 현재 Activity를 종료하고 Stack에서 제거",
    "",
