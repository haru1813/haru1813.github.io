    "## Task와 Back Stack",
    "",
    "**Task**는 사용자가 특정 작업을 수행할 때 상호작용하는 **Activity들의 모음**입니다. Activity들은 **Back Stack(뒤로 가기 스택)**에 쌓입니다.",
