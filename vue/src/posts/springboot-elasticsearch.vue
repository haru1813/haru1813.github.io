    "# 8. Aggregation (집계)",
    "",
    "SQL의 `GROUP BY`와 비슷하지만 훨씬 강력합니다.",
    "",
    "```java",
    "public Map<String, Long> getCategoryStats() {",
    "    NativeQuery query = NativeQuery.builder()",
    "            .withQuery(MatchAllQuery.of(m -> m)._toQuery())",
    "            .withAggregation(\"category_stats\",",
    "                    Aggregation.of(a -> a.terms(t -> t.field(\"category\").size(50))))",
    "            .withMaxResults(0)",
    "            .build();",
    "",
    "    SearchHits<ProductDocument> hits = operations.search(query, ProductDocument.class);",
    "",
    "    Map<String, Long> stats = new java.util.LinkedHashMap<>();",
    "    ElasticsearchAggregations aggs = (ElasticsearchAggregations) hits.getAggregations();",
    "    if (aggs != null) {",
    "        aggs.aggregationsAsMap().get(\"category_stats\")",
    "                .aggregation().getAggregate().sterms().buckets().array()",
    "                .forEach(bucket -> stats.put(",
    "                        bucket.key().stringValue(),",
    "                        bucket.docCount())",
    "                );",
    "    }",
    "    return stats;",
    "}",
    "```",
    "",
    "```json",
    "// 응답 예시: 카테고리별 상품 수",
    "{",
    "  \"전자제품\": 150,",
    "  \"의류\": 89,",
    "  \"도서\": 210,",
    "  \"식품\": 67",
    "}",
    "```",
    "",
    "### 가격 범위 집계",
    "",
    "```java",
    "public Map<String, Object> getPriceStats() {",
    "    NativeQuery query = NativeQuery.builder()",
    "            .withQuery(MatchAllQuery.of(m -> m)._toQuery())",
    "            .withAggregation(\"price_stats\",",
    "                    Aggregation.of(a -> a.stats(s -> s.field(\"price\"))))",
    "            .withAggregation(\"price_ranges\",",
    "                    Aggregation.of(a -> a.range(r -> r.field(\"price\")",
    "                            .ranges(",
