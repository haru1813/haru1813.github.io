    "### FieldType 비교",
    "",
    "| FieldType | 설명 | 검색 | 정렬/집계 |",
    "|-----------|------|------|----------|",
    "| `Text` | 전문 검색용 (형태소 분석됨) | **O** | X |",
    "| `Keyword` | 정확한 값 매칭 | 완전 일치만 | **O** |",
    "| `Integer` / `Long` | 숫자 | 범위 검색 | **O** |",
    "| `Double` / `Float` | 실수 | 범위 검색 | **O** |",
    "| `Date` | 날짜 | 범위 검색 | **O** |",
    "| `Boolean` | true/false | 필터 | O |",
