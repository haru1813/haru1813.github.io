    "# 11. RDBMS ↔ Elasticsearch 동기화",
    "",
    "실무에서는 **RDBMS가 원본**, Elasticsearch는 **검색용 복제본**으로 사용합니다.",
    "",
    "```mermaid",
    "flowchart LR",
    "    A[\"클라이언트\"] -->|CUD| B[\"Spring Boot\"]",
    "    B -->|저장| C[(\"MySQL (원본)\")]",
    "    B -->|동기화| D[\"Elasticsearch (검색)\"]",
    "    A -->|Read/Search| B",
    "    B -->|단순 조회| C",
    "    B -->|검색| D",
    "```",
    "",
    "### 동기화 방법",
    "",
    "```java",
    "@Service",
    "@RequiredArgsConstructor",
    "@Transactional",
    "public class ProductService {",
    "",
    "    private final ProductRepository productRepository;       // JPA",
    "    private final ProductSearchRepository searchRepository;   // Elasticsearch",
    "",
    "    public Product create(CreateProductRequest request) {",
    "        // 1. RDBMS에 저장 (원본)",
    "        Product product = productRepository.save(request.toEntity());",
    "",
    "        // 2. Elasticsearch에 동기화 (검색용)",
    "        ProductDocument document = ProductDocument.builder()",
    "                .id(product.getId().toString())",
    "                .name(product.getName())",
    "                .description(product.getDescription())",
    "                .category(product.getCategory())",
    "                .price(product.getPrice())",
    "                .createdAt(product.getCreatedAt())",
    "                .active(true)",
    "                .build();",
    "        searchRepository.save(document);",
    "",
    "        return product;",
    "    }",
    "",
    "    public void delete(Long id) {",
    "        productRepository.deleteById(id);",
    "        searchRepository.deleteById(id.toString());",
    "    }",
    "}",
    "```",
    "",
    "### 동기화 전략 비교",
    "",
    "| 전략 | 방법 | 장점 | 단점 |",
    "|------|------|------|------|",
    "| **동기 동기화** | Service에서 직접 저장 | 구현 간단 | ES 장애 시 트랜잭션 영향 |",
    "| **이벤트 기반** | `@EventListener` | 느슨한 결합 | 약간의 지연 |",
    "| **메시지 큐** | Kafka/RabbitMQ | 안정적, 재시도 | 인프라 복잡 |",
    "| **CDC** | Debezium (DB 로그) | DB 변경 자동 감지 | 설정 복잡 |",
