    "### 핵심 어노테이션",
    "",
    "| 어노테이션 | 역할 |",
    "|-----------|------|",
    "| `@Document(indexName)` | 인덱스(테이블) 이름 지정 |",
    "| `@Id` | 문서 고유 ID |",
    "| `@Field(type)` | 필드 타입 지정 |",
    "| `@Setting` | 인덱스 설정 (샤드, 리플리카, 분석기) |",
    "| `@Mapping` | 커스텀 매핑 JSON 파일 지정 |",
