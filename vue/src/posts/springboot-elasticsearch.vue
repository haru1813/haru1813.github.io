    "## кІҖмғү м„ңл№„мҠӨ",
    "",
    "```java",
    "package com.example.demo.service;",
    "",
    "import co.elastic.clients.elasticsearch._types.query_dsl.*;",
    "import com.example.demo.document.ProductDocument;",
    "import lombok.RequiredArgsConstructor;",
    "import org.springframework.data.domain.PageRequest;",
    "import org.springframework.data.domain.Sort;",
    "import org.springframework.data.elasticsearch.client.elc.NativeQuery;",
    "import org.springframework.data.elasticsearch.core.ElasticsearchOperations;",
    "import org.springframework.data.elasticsearch.core.SearchHit;",
    "import org.springframework.data.elasticsearch.core.SearchHits;",
    "import org.springframework.stereotype.Service;",
    "",
    "import java.util.List;",
    "",
    "@Service",
    "@RequiredArgsConstructor",
    "public class ProductQueryService {",
    "",
    "    private final ElasticsearchOperations operations;",
    "",
    "    // ============================",
    "    // Match Query (м „л¬ё кІҖмғү)",
    "    // ============================",
    "    public List<ProductDocument> searchByName(String keyword) {",
    "        Query query = MatchQuery.of(m -> m",
    "                .field(\"name\")",
    "                .query(keyword)",
    "        )._toQuery();",
    "",
    "        NativeQuery nativeQuery = NativeQuery.builder()",
    "                .withQuery(query)",
    "                .build();",
    "",
    "        SearchHits<ProductDocument> hits = operations.search(nativeQuery, ProductDocument.class);",
    "        return hits.getSearchHits().stream()",
    "                .map(SearchHit::getContent)",
    "                .toList();",
    "    }",
    "",
    "    // ============================",
    "    // Multi-Match Query (м—¬лҹ¬ н•„л“ң кІҖмғү)",
    "    // ============================",
    "    public List<ProductDocument> searchByKeyword(String keyword) {",
    "        Query query = MultiMatchQuery.of(m -> m",
    "                .query(keyword)",
    "                .fields(\"name\", \"description\", \"brand\")",
    "                .type(TextQueryType.BestFields)",
    "        )._toQuery();",
    "",
    "        NativeQuery nativeQuery = NativeQuery.builder()",
    "                .withQuery(query)",
    "                .build();",
    "",
    "        SearchHits<ProductDocument> hits = operations.search(nativeQuery, ProductDocument.class);",
    "        return hits.getSearchHits().stream()",
    "                .map(SearchHit::getContent)",
    "                .toList();",
    "    }",
    "",
    "    // ============================",
    "    // Bool Query (ліөн•© мЎ°кұҙ кІҖмғү)",
    "    // ============================",
    "    public List<ProductDocument> searchProducts(",
    "            String keyword, String category, Integer minPrice, Integer maxPrice) {",
    "",
    "        BoolQuery.Builder boolBuilder = new BoolQuery.Builder();",
    "",
    "        // must: л°ҳл“ңмӢң нҸ¬н•Ё",
    "        if (keyword != null && !keyword.isBlank()) {",
    "            boolBuilder.must(MultiMatchQuery.of(m -> m",
    "                    .query(keyword)",
    "                    .fields(\"name\", \"description\")",
    "            )._toQuery());",
    "        }",
    "",
    "        // filter: мЎ°кұҙ н•„н„° (мҠӨмҪ”м–ҙ мҳҒн–Ҙ X)",
    "        if (category != null && !category.isBlank()) {",
    "            boolBuilder.filter(TermQuery.of(t -> t",
    "                    .field(\"category\")",
    "                    .value(category)",
    "            )._toQuery());",
    "        }",
    "",
    "        // filter: к°ҖкІ© лІ”мң„",
    "        if (minPrice != null || maxPrice != null) {",
    "            boolBuilder.filter(RangeQuery.of(r -> {",
    "                r.number(n -> {",
    "                    n.field(\"price\");",
    "                    if (minPrice != null) n.gte((double) minPrice);",
    "                    if (maxPrice != null) n.lte((double) maxPrice);",
    "                    return n;",
    "                });",
    "                return r;",
    "            })._toQuery());",
    "        }",
    "",
    "        // filter: active = true",
    "        boolBuilder.filter(TermQuery.of(t -> t",
    "                .field(\"active\")",
    "                .value(true)",
    "        )._toQuery());",
    "",
    "        NativeQuery nativeQuery = NativeQuery.builder()",
    "                .withQuery(boolBuilder.build()._toQuery())",
    "                .withSort(Sort.by(Sort.Direction.DESC, \"createdAt\"))",
    "                .build();",
    "",
    "        SearchHits<ProductDocument> hits = operations.search(nativeQuery, ProductDocument.class);",
    "        return hits.getSearchHits().stream()",
    "                .map(SearchHit::getContent)",
    "                .toList();",
    "    }",
    "",
    "    // ============================",
    "    // Fuzzy Query (мҳӨнғҖ н—Ҳмҡ© кІҖмғү)",
    "    // ============================",
    "    public List<ProductDocument> searchFuzzy(String keyword) {",
    "        Query query = FuzzyQuery.of(f -> f",
    "                .field(\"name\")",
    "                .value(keyword)",
    "                .fuzziness(\"AUTO\")",
    "        )._toQuery();",
    "",
    "        NativeQuery nativeQuery = NativeQuery.builder()",
    "                .withQuery(query)",
    "                .build();",
    "",
    "        SearchHits<ProductDocument> hits = operations.search(nativeQuery, ProductDocument.class);",
    "        return hits.getSearchHits().stream()",
    "                .map(SearchHit::getContent)",
    "                .toList();",
    "    }",
    "",
    "    // ============================",
    "    // Wildcard Query (мҷҖмқјл“ңм№ҙл“ң кІҖмғү)",
    "    // ============================",
    "    public List<ProductDocument> searchWildcard(String pattern) {",
    "        Query query = WildcardQuery.of(w -> w",
    "                .field(\"name\")",
    "                .value(\"*\" + pattern + \"*\")",
    "        )._toQuery();",
    "",
    "        NativeQuery nativeQuery = NativeQuery.builder()",
    "                .withQuery(query)",
    "                .build();",
    "",
    "        SearchHits<ProductDocument> hits = operations.search(nativeQuery, ProductDocument.class);",
    "        return hits.getSearchHits().stream()",
    "                .map(SearchHit::getContent)",
    "                .toList();",
    "    }",
    "}",
