    "            val result = withContext(Dispatchers.IO) {",
    "                fetchDataFromServer()",
    "            }",
    "            // 자동으로 메인 스레드에서 UI 업데이트",
    "            textView.text = result",
    "        }",
    "    }",
    "",
    "    private suspend fun fetchDataFromServer(): String {",
    "        delay(2000) // 네트워크 시뮬레이션",
