    "| Top-level await | ❌ | ✅ |",
    "| `this` | `module.exports` | `undefined` |",
    "| 브라우저 호환 | ❌ (번들러 필요) | ✅ (네이티브 지원) |",
    "| 순환 참조 | 부분 로드 (일부 값 undefined) | 라이브 바인딩 (항상 최신값) |",
