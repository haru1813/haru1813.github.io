    "@RequiredArgsConstructor",
    "public class CustomOAuth2UserService implements OAuth2UserService<OAuth2UserRequest, OAuth2User> {",
    "",
    "    private final MemberRepository memberRepository;",
    "",
    "    @Override",
    "    public OAuth2User loadUser(OAuth2UserRequest userRequest)",
    "            throws OAuth2AuthenticationException {",
    "",
    "        // 1. 기본 OAuth2UserService로 사용자 정보 가져오기",
    "        OAuth2UserService<OAuth2UserRequest, OAuth2User> delegate =",
    "                new DefaultOAuth2UserService();",
    "        OAuth2User oAuth2User = delegate.loadUser(userRequest);",
    "",
    "        // 2. registrationId (google, kakao, naver) 가져오기",
    "        String registrationId = userRequest.getClientRegistration()",
    "                .getRegistrationId();",
    "",
    "        // 3. userNameAttributeName (사용자 식별 키) 가져오기",
    "        String userNameAttributeName = userRequest.getClientRegistration()",
    "                .getProviderDetails()",
    "                .getUserInfoEndpoint()",
    "                .getUserNameAttributeName();",
    "",
    "        // 4. OAuthAttributes로 소셜별 사용자 정보 매핑",
    "        OAuthAttributes attributes = OAuthAttributes.of(",
    "                registrationId, userNameAttributeName,",
    "                oAuth2User.getAttributes());",
    "",
    "        // 5. 회원 저장 또는 업데이트",
    "        Member member = saveOrUpdate(attributes);",
    "",
    "        // 6. DefaultOAuth2User 반환",
    "        return new DefaultOAuth2User(",
    "                Collections.singleton(",
    "                    new SimpleGrantedAuthority(member.getRole().getKey())),",
    "                attributes.getAttributes(),",
    "                attributes.getNameAttributeKey());",
    "    }",
    "",
    "    private Member saveOrUpdate(OAuthAttributes attributes) {",
    "        Member member = memberRepository",
    "                .findBySocialTypeAndSocialId(",
    "                    attributes.getSocialType(),",
    "                    attributes.getSocialId())",
    "                .map(entity -> entity.update(",
    "                    attributes.getName(),",
    "                    attributes.getProfileImage()))",
    "                .orElse(attributes.toEntity());",
    "",
    "        return memberRepository.save(member);",
    "    }",
    "}",
