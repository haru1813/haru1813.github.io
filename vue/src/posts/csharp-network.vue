    "## Echo 서버",
    "",
    "클라이언트가 보낸 메시지를 그대로 되돌려보내는 서버입니다.",
    "",
    "```csharp",
    "using System;",
    "using System.Net;",
    "using System.Net.Sockets;",
    "using System.Text;",
    "",
    "class EchoServer",
    "{",
    "    static void Main(string[] args)",
    "    {",
    "        string bindIp = args[0];",
    "        const int bindPort = 5425;",
    "        TcpListener server = null;",
    "",
    "        try",
    "        {",
    "            IPEndPoint localAddress =",
    "                new IPEndPoint(IPAddress.Parse(bindIp), bindPort);",
    "",
    "            server = new TcpListener(localAddress);",
    "            server.Start();",
    "            Console.WriteLine(\"메아리 서버 시작...\");",
    "",
    "            while (true)",
    "            {",
    "                // 클라이언트 접속 대기",
    "                TcpClient client = server.AcceptTcpClient();",
    "                Console.WriteLine($\"클라이언트 접속 : {client.Client.RemoteEndPoint}\");",
    "",
    "                NetworkStream stream = client.GetStream();",
    "",
    "                int length;",
    "                byte[] bytes = new byte[256];",
    "",
    "                // 데이터 수신 → 그대로 송신",
    "                while ((length = stream.Read(bytes, 0, bytes.Length)) != 0)",
    "                {",
    "                    string data = Encoding.Default.GetString(bytes, 0, length);",
    "                    Console.WriteLine($\"수신: {data}\");",
    "",
    "                    byte[] msg = Encoding.Default.GetBytes(data);",
    "                    stream.Write(msg, 0, msg.Length);",
    "                    Console.WriteLine($\"송신: {data}\");",
    "                }",
    "",
    "                stream.Close();",
    "                client.Close();",
    "            }",
    "        }",
    "        catch (SocketException e)",
    "        {",
    "            Console.WriteLine(e);",
    "        }",
    "        finally",
    "        {",
    "            server.Stop();",
    "        }",
    "    }",
    "}",
    "```",
    "",
    "### 핵심 흐름",
    "1. `TcpListener`로 IP/포트에 바인딩",
