    "### 파일 전송 흐름",
    "",
    "```mermaid",
    "sequenceDiagram",
    "    participant C as 클라이언트(FileSender)",
    "    participant S as 서버(FileReceiver)",
    "    C->>S: REQ_FILE_SEND (파일명, 크기)",
    "    S->>C: REP_FILE_SEND (수락/거부)",
    "    C->>S: FILE_SEND_DATA",
    "    C->>S: FILE_SEND_DATA",
    "    C->>S: FILE_SEND_DATA (LAST)",
    "    Note over C,S: 4KB 청크 단위로 반복 전송",
    "    S->>C: FILE_SEND_RES (성공/실패)",
    "```",
