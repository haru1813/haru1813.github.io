    "### 커스텀 프로토콜 설계 시 고려사항",
    "",
    "| 고려사항 | 설명 |",
    "|----------|------|",
    "| **메시지 경계** | TCP는 스트림이므로 메시지 구분 방법 필요 (길이 접두사, 구분자) |",
    "| **엔디안** | 바이트 순서 통일 (네트워크: Big Endian, x86: Little Endian) |",
    "| **헤더 설계** | 고정 크기 헤더 + 가변 크기 바디 패턴 권장 |",
    "| **버전 관리** | 프로토콜 버전 필드로 하위 호환성 확보 |",
    "| **에러 처리** | 에러 코드 정의, 타임아웃, 재전송 정책 |",
    "| **보안** | 인증, 암호화 (TLS) |",
    "| **페이로드 포맷** | 바이너리 vs JSON vs Protobuf |",
