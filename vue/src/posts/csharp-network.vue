    "### 메시지 타입",
    "",
    "| 상수 | 값 | 설명 |",
    "|------|-----|------|",
    "| `REQ_FILE_SEND` | 0x01 | 파일 전송 요청 |",
    "| `REP_FILE_SEND` | 0x02 | 파일 전송 응답 (수락/거부) |",
    "| `FILE_SEND_DATA` | 0x03 | 파일 데이터 전송 |",
    "| `FILE_SEND_RES` | 0x04 | 전송 결과 (성공/실패) |",
    "",
    "### Header 클래스",
    "",
    "```csharp",
    "public class Header : ISerializable",
    "{",
    "    public uint MSGID { get; set; }",
    "    public uint MSGTYPE { get; set; }",
    "    public uint BODYLEN { get; set; }",
    "    public byte FRAGMENTED { get; set; }",
    "    public byte LASTMSG { get; set; }",
    "    public ushort SEQ { get; set; }",
    "",
    "    public Header(byte[] bytes)",
    "    {",
    "        MSGID = BitConverter.ToUInt32(bytes, 0);",
    "        MSGTYPE = BitConverter.ToUInt32(bytes, 4);",
    "        BODYLEN = BitConverter.ToUInt32(bytes, 8);",
    "        FRAGMENTED = bytes[12];",
    "        LASTMSG = bytes[13];",
    "        SEQ = BitConverter.ToUInt16(bytes, 14);",
    "    }",
    "",
    "    public byte[] GetBytes()",
    "    {",
    "        byte[] bytes = new byte[16];",
    "        Array.Copy(BitConverter.GetBytes(MSGID), 0, bytes, 0, 4);",
    "        Array.Copy(BitConverter.GetBytes(MSGTYPE), 0, bytes, 4, 4);",
    "        Array.Copy(BitConverter.GetBytes(BODYLEN), 0, bytes, 8, 4);",
    "        bytes[12] = FRAGMENTED;",
    "        bytes[13] = LASTMSG;",
    "        Array.Copy(BitConverter.GetBytes(SEQ), 0, bytes, 14, 2);",
    "        return bytes;",
    "    }",
    "",
    "    public int GetSize() => 16;",
    "}",
    "```",
    "",
    "### Message 클래스",
    "",
    "```csharp",
    "public interface ISerializable",
    "{",
    "    byte[] GetBytes();",
    "    int GetSize();",
    "}",
    "",
    "public class Message : ISerializable",
    "{",
    "    public Header Header { get; set; }",
    "    public ISerializable Body { get; set; }",
    "",
    "    public byte[] GetBytes()",
    "    {",
    "        byte[] bytes = new byte[GetSize()];",
    "        Header.GetBytes().CopyTo(bytes, 0);",
    "        Body.GetBytes().CopyTo(bytes, Header.GetSize());",
    "        return bytes;",
    "    }",
    "",
    "    public int GetSize() => Header.GetSize() + Body.GetSize();",
    "}",
    "```",
    "",
    "### MessageUtil (송수신 유틸)",
    "",
    "```csharp",
    "public class MessageUtil",
    "{",
    "    public static void Send(Stream writer, Message msg)",
    "    {",
    "        writer.Write(msg.GetBytes(), 0, msg.GetSize());",
    "    }",
    "",
    "    public static Message Receive(Stream reader)",
    "    {",
    "        // 1. 헤더(16바이트) 수신",
    "        byte[] hBuffer = new byte[16];",
    "        int totalRecv = 0, sizeToRead = 16;",
    "        while (sizeToRead > 0)",
    "        {",
    "            byte[] buffer = new byte[sizeToRead];",
    "            int recv = reader.Read(buffer, 0, sizeToRead);",
    "            if (recv == 0) return null;",
    "            buffer.CopyTo(hBuffer, totalRecv);",
    "            totalRecv += recv;",
    "            sizeToRead -= recv;",
    "        }",
    "        Header header = new Header(hBuffer);",
    "",
    "        // 2. 바디(BODYLEN 바이트) 수신",
    "        byte[] bBuffer = new byte[header.BODYLEN];",
    "        totalRecv = 0;",
    "        sizeToRead = (int)header.BODYLEN;",
    "        while (sizeToRead > 0)",
    "        {",
    "            byte[] buffer = new byte[sizeToRead];",
    "            int recv = reader.Read(buffer, 0, sizeToRead);",
    "            if (recv == 0) return null;",
    "            buffer.CopyTo(bBuffer, totalRecv);",
    "            totalRecv += recv;",
    "            sizeToRead -= recv;",
    "        }",
    "",
    "        // 3. MSGTYPE에 따라 Body 파싱",
    "        ISerializable body = header.MSGTYPE switch",
    "        {",
    "            CONSTANTS.REQ_FILE_SEND  => new BodyRequest(bBuffer),",
    "            CONSTANTS.REP_FILE_SEND  => new BodyResponse(bBuffer),",
    "            CONSTANTS.FILE_SEND_DATA => new BodyData(bBuffer),",
    "            CONSTANTS.FILE_SEND_RES  => new BodyResult(bBuffer),",
    "            _ => throw new Exception($\"Unknown MSGTYPE: {header.MSGTYPE}\")",
    "        };",
    "",
    "        return new Message() { Header = header, Body = body };",
    "    }",
    "}",
    "```",
