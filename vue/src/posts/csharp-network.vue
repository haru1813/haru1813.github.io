    "## 실전 예제: 멀티 클라이언트 채팅 서버 (비동기)",
    "",
    "```csharp",
    "using System;",
    "using System.Collections.Concurrent;",
    "using System.Net;",
    "using System.Net.Sockets;",
    "using System.Text;",
    "using System.Threading.Tasks;",
    "",
    "class ChatServer",
    "{",
    "    private static ConcurrentDictionary<string, TcpClient> _clients = new();",
    "",
    "    static async Task Main()",
    "    {",
    "        var listener = new TcpListener(IPAddress.Any, 9000);",
    "        listener.Start();",
    "        Console.WriteLine(\"채팅 서버 시작 (포트: 9000)\");",
    "",
    "        while (true)",
    "        {",
    "            TcpClient client = await listener.AcceptTcpClientAsync();",
    "            _ = HandleClientAsync(client);",
    "        }",
    "    }",
    "",
    "    static async Task HandleClientAsync(TcpClient client)",
    "    {",
    "        string clientId = client.Client.RemoteEndPoint.ToString();",
    "        _clients.TryAdd(clientId, client);",
    "        await BroadcastAsync($\"[서버] {clientId} 입장\");",
    "",
    "        NetworkStream stream = client.GetStream();",
    "        byte[] buffer = new byte[4096];",
    "",
    "        try",
    "        {",
    "            int bytesRead;",
    "            while ((bytesRead = await stream.ReadAsync(buffer)) > 0)",
    "            {",
    "                string message = Encoding.UTF8.GetString(buffer, 0, bytesRead);",
    "                Console.WriteLine($\"[{clientId}] {message}\");",
    "                await BroadcastAsync($\"[{clientId}] {message}\", clientId);",
    "            }",
    "        }",
    "        catch (Exception) { }",
    "        finally",
    "        {",
    "            _clients.TryRemove(clientId, out _);",
    "            client.Close();",
    "            await BroadcastAsync($\"[서버] {clientId} 퇴장\");",
    "        }",
    "    }",
    "",
    "    static async Task BroadcastAsync(string message, string excludeId = null)",
    "    {",
    "        byte[] data = Encoding.UTF8.GetBytes(message);",
    "        foreach (var (id, client) in _clients)",
    "        {",
    "            if (id == excludeId) continue;",
    "            try",
    "            {",
    "                await client.GetStream().WriteAsync(data);",
    "            }",
    "            catch",
    "            {",
    "                _clients.TryRemove(id, out _);",
    "            }",
    "        }",
    "    }",
    "}",
    "```",
