    "## 비동기 TCP 서버 (async/await)",
    "",
    "```csharp",
    "using System;",
    "using System.Collections.Generic;",
    "using System.Net;",
    "using System.Net.Sockets;",
    "using System.Text;",
    "using System.Threading.Tasks;",
    "",
    "class AsyncEchoServer",
    "{",
    "    private static List<TcpClient> _clients = new();",
    "",
    "    static async Task Main(string[] args)",
    "    {",
    "        var listener = new TcpListener(IPAddress.Any, 5425);",
    "        listener.Start();",
    "        Console.WriteLine(\"비동기 서버 시작 (포트: 5425)\");",
    "",
    "        while (true)",
    "        {",
    "            TcpClient client = await listener.AcceptTcpClientAsync();",
    "            Console.WriteLine($\"클라이언트 접속: {client.Client.RemoteEndPoint}\");",
    "            _clients.Add(client);",
    "",
    "            // 각 클라이언트를 별도 Task로 처리 (비블로킹)",
    "            _ = HandleClientAsync(client);",
    "        }",
    "    }",
    "",
    "    static async Task HandleClientAsync(TcpClient client)",
    "    {",
    "        NetworkStream stream = client.GetStream();",
    "        byte[] buffer = new byte[4096];",
    "",
    "        try",
    "        {",
    "            int bytesRead;",
    "            while ((bytesRead = await stream.ReadAsync(buffer, 0, buffer.Length)) > 0)",
    "            {",
    "                string message = Encoding.UTF8.GetString(buffer, 0, bytesRead);",
    "                Console.WriteLine($\"[{client.Client.RemoteEndPoint}] {message}\");",
    "",
    "                // Echo 응답",
    "                byte[] response = Encoding.UTF8.GetBytes($\"Echo: {message}\");",
    "                await stream.WriteAsync(response, 0, response.Length);",
    "            }",
    "        }",
    "        catch (Exception ex)",
    "        {",
    "            Console.WriteLine($\"클라이언트 에러: {ex.Message}\");",
    "        }",
    "        finally",
    "        {",
    "            _clients.Remove(client);",
    "            client.Close();",
    "            Console.WriteLine(\"클라이언트 연결 종료\");",
    "        }",
    "    }",
    "}",
    "```",
    "",
