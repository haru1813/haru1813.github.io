    "## HttpClient 사용법 상세",
    "",
    "```csharp",
    "using System.Net.Http;",
    "using System.Net.Http.Headers;",
    "using System.Text;",
    "using System.Text.Json;",
    "",
    "// HttpClient 싱글톤 (또는 IHttpClientFactory 사용)",
    "static readonly HttpClient _httpClient = new HttpClient",
    "{",
    "    BaseAddress = new Uri(\"https://api.example.com\"),",
    "    Timeout = TimeSpan.FromSeconds(30)",
    "};",
    "",
    "// GET 요청",
    "async Task<string> GetAsync(string endpoint)",
    "{",
    "    HttpResponseMessage response = await _httpClient.GetAsync(endpoint);",
    "    response.EnsureSuccessStatusCode();",
    "    return await response.Content.ReadAsStringAsync();",
    "}",
    "",
    "// POST (JSON 전송)",
    "async Task<T> PostJsonAsync<T>(string endpoint, object data)",
    "{",
    "    string json = JsonSerializer.Serialize(data);",
    "    var content = new StringContent(json, Encoding.UTF8, \"application/json\");",
    "",
    "    HttpResponseMessage response = await _httpClient.PostAsync(endpoint, content);",
    "    response.EnsureSuccessStatusCode();",
    "",
    "    string responseJson = await response.Content.ReadAsStringAsync();",
    "    return JsonSerializer.Deserialize<T>(responseJson);",
    "}",
    "",
    "// PUT 요청",
    "async Task PutAsync(string endpoint, object data)",
    "{",
    "    string json = JsonSerializer.Serialize(data);",
    "    var content = new StringContent(json, Encoding.UTF8, \"application/json\");",
    "    HttpResponseMessage response = await _httpClient.PutAsync(endpoint, content);",
    "    response.EnsureSuccessStatusCode();",
    "}",
    "",
    "// DELETE 요청",
    "async Task DeleteAsync(string endpoint)",
    "{",
    "    HttpResponseMessage response = await _httpClient.DeleteAsync(endpoint);",
    "    response.EnsureSuccessStatusCode();",
    "}",
    "",
    "// 커스텀 헤더 설정",
    "async Task GetWithHeadersAsync()",
    "{",
    "    var request = new HttpRequestMessage(HttpMethod.Get, \"/api/data\");",
    "    request.Headers.Add(\"X-Api-Key\", \"my-api-key\");",
    "    request.Headers.Authorization =",
    "        new AuthenticationHeaderValue(\"Bearer\", \"my-token\");",
    "",
    "    HttpResponseMessage response = await _httpClient.SendAsync(request);",
    "    string result = await response.Content.ReadAsStringAsync();",
    "}",
    "```",
