    "## 네트워크 보안 기초 (SSL/TLS)",
    "",
    "```csharp",
    "using System.Net.Security;",
    "using System.Security.Cryptography.X509Certificates;",
    "",
    "// SSL/TLS 클라이언트",
    "async Task SecureClientAsync(string host, int port)",
    "{",
    "    using var client = new TcpClient();",
    "    await client.ConnectAsync(host, port);",
    "",
    "    using var sslStream = new SslStream(",
    "        client.GetStream(),",
    "        leaveInnerStreamOpen: false,",
    "        (sender, cert, chain, errors) =>",
    "        {",
    "            // 인증서 검증 콜백",
    "            if (errors == SslPolicyErrors.None) return true;",
    "            Console.WriteLine($\"인증서 오류: {errors}\");",
    "            return false;",
    "        });",
    "",
    "    await sslStream.AuthenticateAsClientAsync(host);",
    "",
    "    Console.WriteLine($\"암호화: {sslStream.IsEncrypted}\");",
    "    Console.WriteLine($\"프로토콜: {sslStream.SslProtocol}\");",
    "    Console.WriteLine($\"서버 인증서: {sslStream.RemoteCertificate?.Subject}\");",
    "",
    "    // 암호화된 데이터 송수신",
    "    byte[] message = Encoding.UTF8.GetBytes(\"GET / HTTP/1.1\\r\<br>Host: \" + host + \"\\r\<br>\\r\<br>\");",
    "    await sslStream.WriteAsync(message);",
    "",
    "    byte[] buffer = new byte[4096];",
    "    int bytesRead = await sslStream.ReadAsync(buffer);",
    "    Console.WriteLine(Encoding.UTF8.GetString(buffer, 0, bytesRead));",
    "}",
