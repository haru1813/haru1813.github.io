    "## Pointcut 표현식 문법",
    "",
    "Pointcut은 **어떤 메서드에 Aspect를 적용할지** 결정하는 조건식입니다.",
    "",
    "### execution 표현식 구조",
    "",
    "```",
    "execution(접근제어자? 반환타입 패키지.클래스.메서드(파라미터))",
    "```",
    "",
    "### 자주 쓰는 패턴",
    "",
    "| 표현식 | 의미 |",
    "|--------|------|",
    "| `execution(* com.example.demo.service..*(..))` | service 패키지와 하위 패키지의 모든 메서드 |",
    "| `execution(* com.example.demo.service.OrderService.*(..))` | OrderService의 모든 메서드 |",
    "| `execution(public * *(..))` | 모든 public 메서드 |",
    "| `execution(* create*(..))` | create로 시작하는 모든 메서드 |",
    "| `execution(* *(String, ..))` | 첫 번째 파라미터가 String인 메서드 |",
    "| `execution(String *(..))` | 반환 타입이 String인 메서드 |",
    "",
    "### 와일드카드 의미",
    "",
    "| 기호 | 의미 |",
    "|------|------|",
    "| `*` | 아무 값 하나 (패키지·클래스·메서드·반환타입) |",
    "| `..` | 0개 이상 (패키지 → 하위 패키지 포함, 파라미터 → 아무 개수) |",
    "",
    "### 기타 Pointcut 지시자",
    "",
    "| 지시자 | 예시 | 설명 |",
    "|--------|------|------|",
    "| `@annotation` | `@annotation(TimeTrace)` | 특정 어노테이션이 붙은 메서드 |",
    "| `@within` | `@within(org.springframework.stereotype.Service)` | 특정 어노테이션이 붙은 **클래스**의 모든 메서드 |",
    "| `within` | `within(com.example.demo.service..*)` | 특정 패키지 내의 모든 메서드 |",
    "| `bean` | `bean(orderService)` | 특정 Bean 이름의 모든 메서드 (Spring 전용) |",
    "| `args` | `args(String, ..)` | 런타임 파라미터 타입으로 매칭 |",
    "",
    "### Pointcut 조합 (AND, OR, NOT)",
    "",
    "```java",
    "@Aspect",
    "@Component",
    "public class CombinedAspect {",
    "",
    "    // 재사용 가능한 Pointcut 정의",
    "    @Pointcut(\"execution(* com.example.demo.service..*(..))\")",
    "    private void serviceLayer() {}",
    "",
    "    @Pointcut(\"execution(* com.example.demo.controller..*(..))\")",
    "    private void controllerLayer() {}",
    "",
    "    // 조합: Service OR Controller",
    "    @Before(\"serviceLayer() || controllerLayer()\")",
    "    public void logAll(JoinPoint joinPoint) {",
    "        log.info(\"[LOG] {}\", joinPoint.getSignature().toShortString());",
    "    }",
    "",
    "    // 조합: Service AND create로 시작하는 메서드",
    "    @Before(\"serviceLayer() && execution(* create*(..))\")",
    "    public void logCreate(JoinPoint joinPoint) {",
    "        log.info(\"[CREATE] {}\", joinPoint.getSignature().toShortString());",
    "    }",
    "",
    "    // NOT: Service 중에서 get으로 시작하지 않는 메서드",
    "    @Before(\"serviceLayer() && !execution(* get*(..))\")",
    "    public void logExceptGet(JoinPoint joinPoint) {",
    "        log.info(\"[NOT GET] {}\", joinPoint.getSignature().toShortString());",
    "    }",
    "}",
    "```",
