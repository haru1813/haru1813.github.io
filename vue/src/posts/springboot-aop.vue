    "|------------|------|",
    "| `@Target(ElementType.METHOD)` | 메서드에만 붙일 수 있음 |",
    "| `@Retention(RetentionPolicy.RUNTIME)` | 런타임까지 어노테이션 정보 유지 (리플렉션으로 읽을 수 있음) |",
    "",
    "### 2단계: Aspect 작성 (어노테이션 기반 Pointcut)",
    "",
    "```java",
    "package com.example.demo.aop;",
    "",
    "import com.example.demo.aop.annotation.TimeTrace;",
    "import lombok.extern.slf4j.Slf4j;",
    "import org.aspectj.lang.ProceedingJoinPoint;",
    "import org.aspectj.lang.annotation.Around;",
    "import org.aspectj.lang.annotation.Aspect;",
    "import org.springframework.stereotype.Component;",
    "",
    "@Slf4j",
    "@Aspect",
    "@Component",
    "public class TimeTraceAspect {",
    "",
    "    @Around(\"@annotation(com.example.demo.aop.annotation.TimeTrace)\")",
    "    public Object traceTime(ProceedingJoinPoint joinPoint) throws Throwable {",
    "        String method = joinPoint.getSignature().toShortString();",
    "        log.info(\"[TimeTrace 시작] {}\", method);",
    "",
    "        long start = System.nanoTime();",
    "        try {",
    "            return joinPoint.proceed();",
    "        } finally {",
    "            long elapsed = (System.nanoTime() - start) / 1_000_000;",
    "            log.info(\"[TimeTrace 종료] {} → {}ms\", method, elapsed);",
    "        }",
    "    }",
    "}",
