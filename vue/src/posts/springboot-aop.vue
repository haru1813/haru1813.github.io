    "",
    "```java",
    "package com.example.demo.aop;",
    "",
    "import lombok.extern.slf4j.Slf4j;",
    "import org.aspectj.lang.JoinPoint;",
    "import org.aspectj.lang.annotation.Aspect;",
    "import org.aspectj.lang.annotation.Before;",
    "import org.aspectj.lang.annotation.Pointcut;",
    "import org.springframework.stereotype.Component;",
    "",
    "@Slf4j",
    "@Aspect",
    "@Component",
    "public class CombinedAspect {",
    "",
    "    @Pointcut(\"execution(* com.example.demo.service..*(..))\")",
    "    private void serviceLayer() {}",
    "",
    "    @Pointcut(\"execution(* com.example.demo.controller..*(..))\")",
    "    private void controllerLayer() {}",
    "",
    "    // Service OR Controller",
    "    @Before(\"serviceLayer() || controllerLayer()\")",
    "    public void logAll(JoinPoint joinPoint) {",
    "        log.info(\"[LOG] {}\", joinPoint.getSignature().toShortString());",
    "    }",
    "",
    "    // Service AND create로 시작하는 메서드",
    "    @Before(\"serviceLayer() && execution(* create*(..))\")",
    "    public void logCreate(JoinPoint joinPoint) {",
    "        log.info(\"[CREATE] {}\", joinPoint.getSignature().toShortString());",
    "    }",
    "",
    "    // NOT: Service 중에서 get으로 시작하지 않는 메서드",
    "    @Before(\"serviceLayer() && !execution(* get*(..))\")",
    "    public void logExceptGet(JoinPoint joinPoint) {",
    "        log.info(\"[NOT GET] {}\", joinPoint.getSignature().toShortString());",
    "    }",
    "}",
