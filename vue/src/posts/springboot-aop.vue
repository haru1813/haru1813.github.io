    "### 자주 쓰는 패턴",
    "",
    "| 표현식 | 의미 |",
    "|--------|------|",
    "| `execution(* com.example.demo.service..*(..))` | service 패키지와 하위 패키지의 모든 메서드 |",
    "| `execution(* com.example.demo.service.OrderService.*(..))` | OrderService의 모든 메서드 |",
