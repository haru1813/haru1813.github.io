    "| Recoil | [recoiljs.org](https://recoiljs.org/) |",
    "",
    "### 핵심 개념",
    "",
    "| 개념 | 설명 |",
    "|------|------|",
    "| **Atom** | 상태의 최소 단위, 여러 컴포넌트가 구독 가능 |",
    "| **Selector** | atom 기반 **파생 상태** (계산된 값) |",
    "| **useRecoilState** | `useState`처럼 atom 읽기 + 쓰기 |",
    "| **useRecoilValue** | 읽기만 (Selector에 적합) |",
    "| **useSetRecoilState** | 쓰기만 (리렌더링 방지) |",
