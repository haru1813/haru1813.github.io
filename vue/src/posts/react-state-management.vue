    "",
    "### 핵심 특징",
    "",
    "| 특징 | 설명 |",
    "|------|------|",
    "| **Provider 불필요** | `<Provider>` 없이 어디서든 스토어 사용 |",
    "| **초경량** | 번들 크기 ~1KB |",
    "| **간결한 API** | `create` 하나로 스토어 생성 |",
    "| **선택적 리렌더링** | selector로 필요한 상태만 구독 |",
    "| **미들웨어** | persist, devtools, immer 등 내장 |",
    "",
    "### 설치",
    "```bash",
    "npm install zustand",
    "```",
    "",
    "### 카운터 예제",
    "",
    "**JavaScript**",
    "```jsx",
