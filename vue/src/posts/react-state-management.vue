    "",
    "**TypeScript**",
    "```tsx",
    "import { atom, selector, useRecoilState, useRecoilValue } from 'recoil';",
    "",
    "interface Todo {",
    "  id: number;",
    "  text: string;",
    "  done: boolean;",
    "}",
    "",
    "const todoListAtom = atom<Todo[]>({",
    "  key: 'todoList',",
    "  default: [],",
    "});",
    "",
    "const todoStatsSelector = selector({",
    "  key: 'todoStats',",
    "  get: ({ get }) => {",
    "    const todos = get(todoListAtom);",
    "    return {",
    "      total: todos.length,",
    "      done: todos.filter((t) => t.done).length,",
    "      undone: todos.filter((t) => !t.done).length,",
    "      percent: todos.length ? Math.round((todos.filter((t) => t.done).length / todos.length) * 100) : 0,",
    "    };",
    "  },",
    "});",
    "",
    "function TodoApp(): JSX.Element {",
    "  const [todos, setTodos] = useRecoilState(todoListAtom);",
    "  const stats = useRecoilValue(todoStatsSelector);",
    "",
    "  const addTodo = (text: string) => {",
    "    setTodos((prev) => [...prev, { id: Date.now(), text, done: false }]);",
    "  };",
    "",
    "  const toggleTodo = (id: number) => {",
    "    setTodos((prev) =>",
    "      prev.map((t) => (t.id === id ? { ...t, done: !t.done } : t))",
    "    );",
    "  };",
    "",
    "  return (",
    "    <div>",
    "      <p>전체: {stats.total} | 완료: {stats.done} | 진행률: {stats.percent}%</p>",
    "      <button onClick={() => addTodo(`할 일 ${todos.length + 1}`)}>추가</button>",
    "      <ul>",
    "        {todos.map((t) => (",
    "          <li key={t.id} onClick={() => toggleTodo(t.id)}",
    "            style={{ textDecoration: t.done ? 'line-through' : 'none', cursor: 'pointer' }}>",
    "            {t.done ? '✅' : '⬜'} {t.text}",
    "          </li>",
    "        ))}",
    "      </ul>",
    "    </div>",
    "  );",
    "}",
    "```",
