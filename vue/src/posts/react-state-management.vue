    "### 데이터 변경 (useMutation)",
    "",
    "**TypeScript**",
    "```tsx",
    "import { useMutation, useQueryClient } from '@tanstack/react-query';",
    "",
    "interface NewPost {",
    "  title: string;",
    "  body: string;",
    "}",
    "",
    "interface Post extends NewPost {",
    "  id: number;",
    "}",
    "",
    "function CreatePost(): JSX.Element {",
    "  const queryClient = useQueryClient();",
    "",
    "  const mutation = useMutation<Post, Error, NewPost>({",
    "    mutationFn: async (newPost) => {",
    "      const res = await fetch('https://jsonplaceholder.typicode.com/posts', {",
    "        method: 'POST',",
    "        headers: { 'Content-Type': 'application/json' },",
    "        body: JSON.stringify(newPost),",
    "      });",
    "      return res.json();",
    "    },",
    "    onSuccess: () => {",
    "      // 'posts' 쿼리 캐시 무효화 → 자동 리페치",
    "      queryClient.invalidateQueries({ queryKey: ['posts'] });",
    "    },",
    "  });",
    "",
    "  const handleSubmit = () => {",
    "    mutation.mutate({ title: '새 글', body: '내용입니다.' });",
    "  };",
    "",
    "  return (",
    "    <div>",
    "      <button onClick={handleSubmit} disabled={mutation.isPending}>",
    "        {mutation.isPending ? '생성 중...' : '글 작성'}",
    "      </button>",
    "      {mutation.isError && <p style={{ color: 'red' }}>에러: {mutation.error.message}</p>}",
    "      {mutation.isSuccess && <p style={{ color: 'green' }}>성공! ID: {mutation.data.id}</p>}",
    "    </div>",
    "  );",
    "}",
    "```",
