    "",
    "```tsx",
    "interface Post {",
    "  id: number;",
    "  title: string;",
    "  body: string;",
    "}",
    "",
    "function PostDetail({ postId }: { postId: number }): JSX.Element {",
    "  const { data: post, isLoading } = useQuery<Post>({",
    "    queryKey: ['post', postId],  // postId가 바뀌면 자동 리페치",
    "    queryFn: async () => {",
    "      const res = await fetch(`https://jsonplaceholder.typicode.com/posts/${postId}`);",
    "      return res.json();",
    "    },",
    "    enabled: postId > 0,  // postId가 0이면 쿼리 비활성화",
    "  });",
    "",
    "  if (isLoading) return <p>로딩 중...</p>;",
    "  return (",
    "    <article>",
    "      <h2>{post?.title}</h2>",
    "      <p>{post?.body}</p>",
    "    </article>",
    "  );",
    "}",
