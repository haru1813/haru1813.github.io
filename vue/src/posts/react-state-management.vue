    "## 2. Zustand",
    "",
    "Zustand은 **최소한의 API**로 전역 상태를 관리하는 경량 라이브러리입니다. 보일러플레이트가 거의 없고, Provider 없이 사용할 수 있습니다.",
    "",
    "### 공식 문서",
    "",
    "| 항목 | URL |",
    "|------|-----|",
    "| Zustand | [docs.pmnd.rs/zustand](https://docs.pmnd.rs/zustand/getting-started/introduction) |",
