    "## 4. React Query (TanStack Query)",
    "",
    "React Query는 **서버 상태(API 데이터)**를 관리하는 라이브러리입니다. 데이터 패칭, 캐싱, 동기화, 리페칭을 자동으로 처리합니다.",
    "",
    "### 공식 문서",
    "",
    "| 항목 | URL |",
    "|------|-----|",
    "| TanStack Query | [tanstack.com/query](https://tanstack.com/query/latest) |",
    "",
    "### 핵심 개념",
    "",
    "| 개념 | 설명 |",
    "|------|------|",
    "| **Query** | 서버에서 데이터 **읽기** (GET) |",
    "| **Mutation** | 서버 데이터 **변경** (POST, PUT, DELETE) |",
    "| **Query Key** | 쿼리를 고유하게 식별하는 키 (캐시 키) |",
    "| **staleTime** | 데이터가 \"신선\"한 시간 (이 동안 리페치 안 함) |",
    "| **cacheTime** | 비활성 데이터를 메모리에 유지하는 시간 |",
    "| **Invalidation** | 캐시를 무효화하고 데이터 재요청 |",
    "",
    "### 설치",
    "```bash",
    "npm install @tanstack/react-query",
    "```",
