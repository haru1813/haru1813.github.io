    "    F --> G[\"Health Check<br/>통과?\"]",
    "    G -->|Yes| H[\"Nginx 설정<br/>Green으로 전환\"]",
    "    G -->|No| I[\"롤백<br/>(Blue 유지)\"]",
    "",
    "    subgraph AFTER[\"전환 후: Green 활성\"]",
    "        J[\"Nginx\"] -->|트래픽| K[\"Green (v2)<br/>포트 8082<br/>✅ 활성\"]",
    "        L[\"Blue (v1)<br/>포트 8081<br/>⏸ 대기\"]",
    "    end",
    "",
    "    H --> AFTER",
    "",
    "    style C fill:#2196F3,color:#fff",
    "    style D fill:#4CAF50,color:#fff",
    "    style K fill:#4CAF50,color:#fff",
    "    style L fill:#2196F3,color:#fff",
    "```",
    "",
    "### 배포 스크립트",
    "",
    "```bash",
    "#!/bin/bash",
    "# deploy.sh - Blue-Green 배포 스크립트",
    "",
    "BLUE_PORT=8081",
    "GREEN_PORT=8082",
    "IMAGE=\"123456789012.dkr.ecr.ap-northeast-2.amazonaws.com/my-spring-app:latest\"",
    "",
    "# 현재 활성 포트 확인",
    "CURRENT_PORT=$(cat /etc/nginx/current_port 2>/dev/null || echo $BLUE_PORT)",
    "",
    "if [ \"$CURRENT_PORT\" == \"$BLUE_PORT\" ]; then",
    "    TARGET_PORT=$GREEN_PORT",
    "    TARGET_NAME=\"green\"",
    "    OLD_NAME=\"blue\"",
    "else",
    "    TARGET_PORT=$BLUE_PORT",
    "    TARGET_NAME=\"blue\"",
    "    OLD_NAME=\"green\"",
    "fi",
    "",
    "echo \">>> 현재 활성: $OLD_NAME($CURRENT_PORT)\"",
    "echo \">>> 배포 대상: $TARGET_NAME($TARGET_PORT)\"",
    "",
    "# 1. 새 버전 컨테이너 시작",
    "docker pull $IMAGE",
    "docker stop $TARGET_NAME 2>/dev/null || true",
    "docker rm $TARGET_NAME 2>/dev/null || true",
    "docker run -d --name $TARGET_NAME \\",
    "    -p $TARGET_PORT:8080 \\",
    "    -e SPRING_PROFILES_ACTIVE=prod \\",
    "    $IMAGE",
    "",
    "# 2. Health Check (최대 30초 대기)",
    "echo \">>> Health Check 시작...\"",
    "for i in $(seq 1 10); do",
    "    STATUS=$(curl -s -o /dev/null -w '%{http_code}' http://localhost:$TARGET_PORT/actuator/health)",
    "    if [ \"$STATUS\" == \"200\" ]; then",
    "        echo \">>> Health Check 통과!\"",
    "        break",
    "    fi",
    "    if [ $i -eq 10 ]; then",
    "        echo \">>> Health Check 실패! 롤백합니다.\"",
    "        docker stop $TARGET_NAME && docker rm $TARGET_NAME",
    "        exit 1",
    "    fi",
    "    sleep 3",
    "done",
    "",
    "# 3. Nginx 트래픽 전환",
    "cat > /etc/nginx/conf.d/app.conf << EOF",
    "upstream spring-app {",
    "    server localhost:$TARGET_PORT;",
    "}",
    "",
    "server {",
    "    listen 80;",
    "    location / {",
    "        proxy_pass http://spring-app;",
    "        proxy_set_header Host \\$host;",
    "        proxy_set_header X-Real-IP \\$remote_addr;",
    "    }",
    "}",
    "EOF",
    "",
    "nginx -s reload",
    "echo $TARGET_PORT > /etc/nginx/current_port",
    "",
    "# 4. 이전 컨테이너 중지",
    "sleep 5",
    "docker stop $OLD_NAME 2>/dev/null || true",
    "docker rm $OLD_NAME 2>/dev/null || true",
    "",
    "echo \">>> 배포 완료! 활성 포트: $TARGET_PORT\"",
    "```",
