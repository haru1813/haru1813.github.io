    "    Console.WriteLine(\"B\");",
    "",
    "    MyMethodAsync(3); // 비동기 호출 (await 없이)",
    "",
    "    Console.WriteLine(\"E\");",
    "    Console.WriteLine(\"F\");",
    "}",
    "",
    "// 실행 결과 (순서가 섞일 수 있음):",
    "// A → B → C → D → E → F → 1/3 → 2/3 → 3/3 → G → H",
