    "### async/await 핵심 규칙",
    "",
    "| 규칙 | 설명 |",
    "|------|------|",
    "| `async` | 메서드에 붙여 비동기 메서드로 선언 |",
    "| `await` | `Task`를 기다리며, 제어를 호출자에게 반환 |",
    "| 반환 타입 | `Task` (반환값 없음), `Task<T>` (반환값 있음), `void` (이벤트 핸들러) |",
    "| 명명 규칙 | 메서드 이름 끝에 `Async` 접미사 권장 |",
    "",
