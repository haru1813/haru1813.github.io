    "## async/await 내부 동작 원리",
    "",
    "컴파일러는 `async` 메서드를 **상태 머신(State Machine)**으로 변환합니다.",
    "",
    "```mermaid",
    "stateDiagram-v2",
    "    [*] --> State0 : 메서드 호출",
    "    State0 --> AwaitPoint1 : await 도달",
    "    AwaitPoint1 --> State1 : Task 완료 시 재개",
    "    State1 --> AwaitPoint2 : 다음 await 도달",
    "    AwaitPoint2 --> State2 : Task 완료 시 재개",
    "    State2 --> [*] : 메서드 완료",
    "",
    "    note right of AwaitPoint1 : 호출자에게 제어 반환\<br>(스레드 차단 없음)",
    "    note right of AwaitPoint2 : SynchronizationContext에\<br>따라 스레드 전환 가능",
    "```",
    "",
    "1. `async` 메서드 호출 → 상태 머신 생성",
    "2. `await`에 도달하면 Task가 완료되었는지 확인",
    "3. 미완료 시 → 현재 상태 저장, 호출자에게 제어 반환",
    "4. Task 완료 시 → 저장된 상태에서 이어서 실행",
