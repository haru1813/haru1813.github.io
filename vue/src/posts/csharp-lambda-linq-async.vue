    "## 비동기 스트림 (IAsyncEnumerable, C# 8.0+)",
    "",
    "비동기적으로 데이터를 **하나씩** 생산하고 소비합니다.",
    "",
    "```csharp",
    "// 비동기 시퀀스 생산",
    "async IAsyncEnumerable<int> GenerateNumbersAsync()",
    "{",
    "    for (int i = 1; i <= 10; i++)",
    "    {",
    "        await Task.Delay(200); // 비동기 대기",
    "        yield return i;",
    "    }",
    "}",
    "",
    "// 비동기 시퀀스 소비",
    "await foreach (int number in GenerateNumbersAsync())",
    "{",
    "    Console.WriteLine($\"받은 값: {number}\");",
    "}",
    "",
    "// CancellationToken과 함께 사용",
    "async IAsyncEnumerable<string> ReadLinesAsync(",
    "    string path,",
    "    [EnumeratorCancellation] CancellationToken token = default)",
    "{",
    "    using var reader = new StreamReader(path);",
    "    while (!reader.EndOfStream)",
    "    {",
    "        token.ThrowIfCancellationRequested();",
    "        yield return await reader.ReadLineAsync();",
    "    }",
    "}",
    "```",
