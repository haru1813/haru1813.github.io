    "    {",
    "        for (int i = 1; i <= count; i++)",
    "        {",
    "            Console.WriteLine($\"{i}/{count} ...\");",
    "            await Task.Delay(100);",
    "        }",
    "    });",
    "",
    "    Console.WriteLine(\"G\");",
    "    Console.WriteLine(\"H\");",
    "}",
