    "# 6. Service 실전 예제 (CRUD)",
    "",
    "```java",
    "@Service",
    "@RequiredArgsConstructor",
    "@Transactional(readOnly = true)",
    "public class MemberService {",
    "",
    "    private final MemberRepository memberRepository;",
    "    private final TeamRepository teamRepository;",
    "",
    "    public MemberResponse findById(Long id) {",
    "        Member member = memberRepository.findById(id)",
    "                .orElseThrow(() -> new RuntimeException(\"회원을 찾을 수 없습니다\"));",
    "        return MemberResponse.from(member);",
    "    }",
    "",
    "    public Page<MemberResponse> findAll(Pageable pageable) {",
    "        return memberRepository.findAll(pageable)",
    "                .map(MemberResponse::from);",
    "    }",
    "",
    "    @Transactional",
    "    public MemberResponse create(CreateMemberRequest request) {",
    "        if (memberRepository.existsByEmail(request.getEmail())) {",
    "            throw new RuntimeException(\"이미 존재하는 이메일입니다\");",
    "        }",
    "",
    "        Team team = teamRepository.findById(request.getTeamId())",
    "                .orElseThrow(() -> new RuntimeException(\"팀을 찾을 수 없습니다\"));",
    "",
    "        Member member = Member.builder()",
    "                .name(request.getName())",
    "                .email(request.getEmail())",
    "                .phone(request.getPhone())",
    "                .role(request.getRole())",
    "                .build();",
    "",
    "        team.addMember(member);  // 양방향 관계 설정",
    "        memberRepository.save(member);",
    "",
    "        return MemberResponse.from(member);",
    "    }",
    "",
    "    @Transactional",
    "    public MemberResponse update(Long id, UpdateMemberRequest request) {",
    "        Member member = memberRepository.findById(id).orElseThrow();",
    "        member.setName(request.getName());",
    "        member.setPhone(request.getPhone());",
    "        // Dirty Checking → save() 불필요",
    "        return MemberResponse.from(member);",
    "    }",
    "",
    "    @Transactional",
    "    public void delete(Long id) {",
    "        memberRepository.deleteById(id);",
    "    }",
    "}",
    "```",
    "",
    "> 클래스 레벨에 `@Transactional(readOnly = true)`를 설정하고, 쓰기 메서드에만 `@Transactional`을 재정의하는 패턴이 일반적입니다.",
    "",
