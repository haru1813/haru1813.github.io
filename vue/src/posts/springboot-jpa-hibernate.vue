    "| 관계 | 어노테이션 | FK 위치 | 주인 |",
    "|------|-----------|---------|------|",
    "| N:1 | `@ManyToOne` | N쪽 | **N쪽 (FK 보유)** |",
    "| 1:N | `@OneToMany(mappedBy)` | N쪽 | N쪽 (`mappedBy`로 지정) |",
    "| 1:1 | `@OneToOne` + `@JoinColumn` | 지정한 쪽 | FK 보유 쪽 |",
    "| N:M | 중간 엔티티 사용 | 중간 테이블 | 각각 `@ManyToOne` |",
    "",
    "### Cascade & orphanRemoval",
    "",
    "| 옵션 | 설명 |",
    "|------|------|",
    "| `cascade = ALL` | 부모 저장/삭제 시 자식도 함께 |",
    "| `cascade = PERSIST` | 부모 저장 시 자식도 저장 |",
    "| `orphanRemoval = true` | 부모에서 제거된 자식은 DB에서 삭제 |",
