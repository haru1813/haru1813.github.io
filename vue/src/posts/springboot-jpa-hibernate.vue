    "# 7. Querydsl (타입 안전한 동적 쿼리)",
    "",
    "## 개념",
    "",
    "JPQL을 문자열로 작성하면 오타가 있어도 **컴파일 시점에 발견이 안 됩니다**. Querydsl은 Java 코드로 쿼리를 작성하여 **컴파일 시점에 오류를 잡습니다**.",
    "",
    "```java",
    "// JPQL (문자열 → 런타임 에러)",
    "@Query(\"SELECT m FROM Membr m\")  // 오타! 실행해봐야 발견",
    "",
    "// Querydsl (코드 → 컴파일 에러)",
    "queryFactory.selectFrom(member)  // 오타면 컴파일 안 됨",
    "```",
    "",
    "## 의존성 (build.gradle)",
    "",
    "```groovy",
    "dependencies {",
    "    implementation 'com.querydsl:querydsl-jpa:5.1.0:jakarta'",
    "    annotationProcessor 'com.querydsl:querydsl-apt:5.1.0:jakarta'",
    "    annotationProcessor 'jakarta.annotation:jakarta.annotation-api'",
    "    annotationProcessor 'jakarta.persistence:jakarta.persistence-api'",
    "}",
    "```",
    "",
    "> 빌드하면 Entity 기반으로 `QMember`, `QTeam` 등 **Q클래스**가 자동 생성됩니다.",
    "",
    "## 설정",
    "",
    "```java",
    "@Configuration",
    "public class QuerydslConfig {",
    "",
    "    @Bean",
    "    public JPAQueryFactory jpaQueryFactory(EntityManager entityManager) {",
    "        return new JPAQueryFactory(entityManager);",
    "    }",
    "}",
    "```",
    "",
    "## 기본 사용법",
    "",
    "```java",
    "@Repository",
    "@RequiredArgsConstructor",
    "public class MemberQueryRepository {",
    "",
    "    private final JPAQueryFactory queryFactory;",
    "",
    "    public List<Member> findByCondition(String name, Role role, Integer minAge) {",
    "        QMember member = QMember.member;",
    "",
    "        return queryFactory",
    "                .selectFrom(member)",
    "                .where(",
    "                        nameContains(name),",
    "                        roleEq(role),",
    "                        ageGoe(minAge)",
    "                )",
    "                .orderBy(member.createdAt.desc())",
    "                .fetch();",
    "    }",
    "",
    "    // 조건 메서드 (null이면 자동으로 무시됨)",
    "    private BooleanExpression nameContains(String name) {",
    "        return name != null ? QMember.member.name.contains(name) : null;",
    "    }",
    "",
    "    private BooleanExpression roleEq(Role role) {",
    "        return role != null ? QMember.member.role.eq(role) : null;",
    "    }",
    "",
    "    private BooleanExpression ageGoe(Integer minAge) {",
    "        return minAge != null ? QMember.member.age.goe(minAge) : null;",
    "    }",
    "}",
    "```",
    "",
    "> `where()`에 `null`을 전달하면 해당 조건이 무시되므로, **동적 쿼리**를 깔끔하게 작성할 수 있습니다.",
    "",
    "## Querydsl 페이징",
    "",
    "```java",
    "public Page<MemberResponse> searchPaged(String name, Role role, Pageable pageable) {",
    "    QMember member = QMember.member;",
    "",
    "    List<Member> content = queryFactory",
    "            .selectFrom(member)",
    "            .where(nameContains(name), roleEq(role))",
    "            .orderBy(member.createdAt.desc())",
    "            .offset(pageable.getOffset())",
    "            .limit(pageable.getPageSize())",
    "            .fetch();",
    "",
    "    long total = queryFactory",
    "            .select(member.count())",
    "            .from(member)",
    "            .where(nameContains(name), roleEq(role))",
    "            .fetchOne();",
    "",
    "    return new PageImpl<>(content.stream().map(MemberResponse::from).toList(),",
    "            pageable, total);",
    "}",
    "```",
