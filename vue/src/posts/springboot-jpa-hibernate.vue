    "# 5. Fetch 전략과 N+1 문제",
    "",
    "## FetchType",
    "",
    "| 타입 | 동작 | 기본값 |",
    "|------|------|--------|",
    "| `LAZY` | **필요할 때** 조회 (프록시) | `@OneToMany`, `@ManyToMany` |",
    "| `EAGER` | **즉시** 조회 (JOIN) | `@ManyToOne`, `@OneToOne` |",
    "",
    "> **실무에서는 모든 연관관계를 `LAZY`로 설정**하는 것이 원칙입니다.",
    "",
    "```java",
    "@ManyToOne(fetch = FetchType.LAZY)   // 항상 LAZY!",
    "@JoinColumn(name = \"team_id\")",
    "private Team team;",
    "```",
    "",
    "## N+1 문제",
    "",
    "```java",
    "// 팀 목록 조회 → 1번 쿼리",
    "List<Team> teams = teamRepository.findAll();",
    "",
    "// 각 팀의 멤버 접근 → N번 추가 쿼리!",
    "for (Team team : teams) {",
    "    System.out.println(team.getMembers().size());",
    "}",
    "```",
    "",
    "```",
    "SELECT * FROM teams                          -- 1번",
    "SELECT * FROM members WHERE team_id = 1      -- +1",
    "SELECT * FROM members WHERE team_id = 2      -- +1",
    "SELECT * FROM members WHERE team_id = 3      -- +1",
    "...                                          -- = N+1번!",
    "```",
    "",
    "## N+1 해결 방법",
    "",
    "### 1) Fetch Join (JPQL)",
    "",
    "```java",
    "@Query(\"SELECT t FROM Team t JOIN FETCH t.members\")",
    "List<Team> findAllWithMembers();",
    "```",
    "",
    "```",
    "SELECT t.*, m.* FROM teams t",
    "JOIN members m ON t.id = m.team_id   -- 1번의 JOIN 쿼리로 해결!",
    "```",
    "",
    "### 2) @EntityGraph",
    "",
    "```java",
    "@EntityGraph(attributePaths = {\"members\"})",
    "@Query(\"SELECT t FROM Team t\")",
    "List<Team> findAllWithMembersGraph();",
    "```",
    "",
    "### 3) @BatchSize (Hibernate)",
    "",
    "```java",
    "@OneToMany(mappedBy = \"team\")",
    "@BatchSize(size = 100)  // IN 절로 100개씩 묶어서 조회",
    "private List<Member> members = new ArrayList<>();",
    "```",
    "",
    "```",
    "SELECT * FROM members WHERE team_id IN (1, 2, 3, ... 100)",
    "```",
    "",
    "### N+1 해결 비교",
    "",
    "| 방법 | 장점 | 단점 |",
    "|------|------|------|",
    "| `JOIN FETCH` | 쿼리 1번으로 해결 | 페이징 불가 |",
    "| `@EntityGraph` | 어노테이션으로 간편 | 복잡한 관계에 한계 |",
    "| `@BatchSize` | 페이징 가능, 간편 | 쿼리 횟수가 1번이 아님 |",
    "",
