    "## Spring ProxyFactory",
    "",
    "Spring은 `ProxyFactory`를 통해 JDK Dynamic Proxy와 CGLIB를 **통합 관리**합니다. 개발자가 프록시 방식을 신경 쓰지 않아도 됩니다.",
    "",
    "```java",
    "import org.aopalliance.intercept.MethodInterceptor;",
    "import org.springframework.aop.framework.ProxyFactory;",
    "",
    "OrderService target = new OrderServiceImpl();",
    "",
    "ProxyFactory proxyFactory = new ProxyFactory(target);",
    "proxyFactory.addAdvice((MethodInterceptor) invocation -> {",
    "    System.out.println(\"[ProxyFactory] Before: \" + invocation.getMethod().getName());",
    "    Object result = invocation.proceed();",
    "    System.out.println(\"[ProxyFactory] After: \" + invocation.getMethod().getName());",
    "    return result;",
    "});",
    "",
    "OrderService proxy = (OrderService) proxyFactory.getProxy();",
    "proxy.createOrder(\"마우스\");",
    "// [ProxyFactory] Before: createOrder",
    "// 주문 생성: 마우스",
    "// [ProxyFactory] After: createOrder",
    "```",
    "",
    "```mermaid",
    "flowchart TD",
    "    A[\"ProxyFactory\"] --> B{\"인터페이스 여부 + 설정\"}",
    "    B -->|\"인터페이스 O\<br>& proxyTargetClass=false\"| C[\"JDK Dynamic Proxy 생성\"]",
    "    B -->|\"인터페이스 X\<br>또는 proxyTargetClass=true\"| D[\"CGLIB Proxy 생성\"]",
    "    C --> E[\"동일한 Advice/Advisor 적용\"]",
    "    D --> E",
    "```",
    "",
    "> Spring이 내부적으로 `ProxyFactory`를 사용하기 때문에, 개발자는 `@Transactional`, `@Aspect` 같은 어노테이션만 사용하면 됩니다.",
