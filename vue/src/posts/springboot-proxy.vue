    "```mermaid",
    "flowchart TD",
    "    A[\"Bean 정의 로드\"] --> B[\"Bean 인스턴스 생성\"]",
    "    B --> C[\"의존성 주입\"]",
    "    C --> D[\"@PostConstruct 실행\"]",
