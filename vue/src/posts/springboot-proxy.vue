    "## 프록시의 핵심 주의사항",
    "",
    "### 1. 내부 호출 문제 (Self-Invocation)",
    "",
    "**같은 클래스 내에서** 메서드를 호출하면 프록시를 거치지 않아 어노테이션이 동작하지 않습니다. 이것이 프록시의 **가장 흔한 함정**입니다.",
    "",
    "```java",
    "@Service",
    "public class OrderService {",
    "",
    "    public void processOrder(Long orderId) {",
    "        // 내부 호출 → 프록시를 거치지 않음!",
    "        // @Transactional이 적용되지 않음!",
    "        this.createOrder(orderId);",
    "    }",
    "",
    "    @Transactional",
    "    public void createOrder(Long orderId) {",
    "        // 직접 호출하면 트랜잭션이 동작함",
    "        // 위의 processOrder()에서 내부 호출하면 트랜잭션 미적용",
    "        orderRepository.save(new Order(orderId));",
    "    }",
    "}",
    "```",
    "",
    "```mermaid",
    "flowchart LR",
    "    subgraph \"프록시 통과 O\"",
    "        A[\"외부\"] -->|\"proxy.createOrder()\"| B[\"프록시\"] --> C[\"실제 객체\"]",
    "    end",
    "    subgraph \"프록시 통과 X\"",
    "        D[\"processOrder\"] -->|\"this.createOrder()\"| E[\"실제 객체 직접 호출\"]",
    "    end",
    "```",
    "",
    "#### 해결 방법",
    "",
    "```java",
    "// 방법 1: 별도 클래스로 분리 (권장)",
    "@Service",
    "public class OrderProcessService {",
    "    private final OrderService orderService;",
    "",
    "    public void processOrder(Long orderId) {",
    "        orderService.createOrder(orderId); // 프록시를 통해 호출",
    "    }",
    "}",
    "",
    "// 방법 2: 자기 자신을 주입 받기",
    "@Service",
    "public class OrderService {",
    "    @Lazy",
    "    @Autowired",
    "    private OrderService self;",
    "",
    "    public void processOrder(Long orderId) {",
    "        self.createOrder(orderId); // 프록시를 통해 호출",
    "    }",
    "",
    "    @Transactional",
    "    public void createOrder(Long orderId) {",
    "        orderRepository.save(new Order(orderId));",
    "    }",
    "}",
    "```",
    "",
    "### 2. final 클래스/메서드 프록시 불가 (CGLIB)",
    "",
    "```java",
    "// CGLIB은 상속 기반이므로 final은 프록시 불가",
    "public final class FinalService { // 프록시 생성 실패!",
    "    public void doSomething() { }",
    "}",
    "",
    "public class SomeService {",
    "    public final void doSomething() { } // 이 메서드만 프록시 불가!",
    "}",
    "```",
    "",
    "### 3. private 메서드는 프록시 불가",
    "",
    "```java",
    "@Service",
    "public class UserService {",
    "",
    "    @Transactional",
    "    private void internalSave() {",
    "        // private 메서드에 @Transactional을 붙여도 동작하지 않음!",
    "        // Spring이 private 메서드를 프록시할 수 없음",
    "    }",
    "}",
    "```",
    "",
    "> `@Transactional`, `@Async`, `@Cacheable` 등은 **public 메서드**에만 적용하세요.",
    "",
    "### 주의사항 요약",
    "",
    "| 문제 | 원인 | 해결 |",
    "|------|------|------|",
    "| 내부 호출 시 어노테이션 미동작 | `this.method()`는 프록시 우회 | 별도 클래스로 분리 또는 self 주입 |",
    "| final 클래스/메서드 | CGLIB이 상속 불가 | final 제거 또는 인터페이스 기반 |",
    "| private 메서드 | 프록시가 접근 불가 | public으로 변경 |",
    "| 생성자 내 호출 | 프록시 초기화 전 호출 | `@PostConstruct` 또는 이벤트 사용 |",
