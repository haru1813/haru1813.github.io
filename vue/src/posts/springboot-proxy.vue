    "## 정리",
    "",
    "- **프록시**는 원본 객체를 대신하여 부가 기능을 수행하는 대리 객체",
    "- **JDK Dynamic Proxy**: 인터페이스 기반, `InvocationHandler`, 리플렉션 사용",
