    "        throw new FormatException($\"나이가 숫자가 아닙니다: {parts[1]}\");",
    "    string city = parts[2].Trim();",
    "",
    "    return (name, age, city);",
    "}",
    "",
    "var (name, age, city) = ParseCsvLine(\"하루, 25, 서울\");",
