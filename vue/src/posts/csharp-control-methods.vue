    "### 로컬 함수 vs 람다 비교",
    "",
    "| 구분 | 로컬 함수 | 람다 (Action/Func) |",
    "|------|----------|-------------------|",
    "| **선언 위치** | 메서드 내부 어디든 | 사용 전 선언 필요 |",
    "| **재귀** | 가능 | 번거로움 (변수 할당 필요) |",
    "| **성능** | 클로저 미생성 가능 (static) | 항상 delegate 객체 생성 |",
    "| **제네릭** | 가능 | 불가 |",
    "| **async/iterator** | 즉시 유효성 검사 가능 | 호출 시점에 검사 |",
    "",
    "```csharp",
    "// 로컬 함수: 재귀가 자연스러움",
    "static int Factorial(int n)",
    "{",
    "    return Inner(n);",
    "    static int Inner(int x) => x <= 1 ? 1 : x * Inner(x - 1);",
    "}",
    "",
    "// 람다: 재귀를 위해 변수 먼저 선언해야 함",
    "Func<int, int> factorial = null!;",
    "factorial = x => x <= 1 ? 1 : x * factorial(x - 1);",
    "```",
