    "## gRPC 에러 처리",
    "",
    "gRPC는 HTTP 상태 코드 대신 자체 **Status Code**를 사용합니다.",
    "",
    "### gRPC Status Code",
    "",
    "| gRPC Code | HTTP 대응 | 설명 |",
    "|-----------|----------|------|",
    "| `OK` | 200 | 성공 |",
    "| `INVALID_ARGUMENT` | 400 | 잘못된 요청 |",
    "| `NOT_FOUND` | 404 | 리소스 없음 |",
    "| `ALREADY_EXISTS` | 409 | 이미 존재 |",
    "| `PERMISSION_DENIED` | 403 | 권한 없음 |",
    "| `UNAUTHENTICATED` | 401 | 인증 필요 |",
