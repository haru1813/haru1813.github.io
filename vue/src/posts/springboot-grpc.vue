    "Metadata metadata = new Metadata();",
    "metadata.put(",
    "    Metadata.Key.of(\"error-code\", Metadata.ASCII_STRING_MARSHALLER),",
    "    \"USER_NOT_FOUND\"",
    ");",
    "",
    "responseObserver.onError(",
    "    Status.NOT_FOUND",
    "        .withDescription(\"사용자를 찾을 수 없습니다\")",
    "        .asRuntimeException(metadata)",
    ");",
    "```",
