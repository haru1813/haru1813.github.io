    "## gRPC 인터셉터",
    "",
    "REST의 Filter/Interceptor와 유사하게, gRPC에도 **인터셉터**가 있습니다.",
    "",
    "### 서버 인터셉터 (로깅)",
    "",
    "```java",
    "package com.example.demo.interceptor;",
    "",
    "import io.grpc.*;",
    "import lombok.extern.slf4j.Slf4j;",
    "import net.devh.boot.grpc.server.interceptor.GrpcGlobalServerInterceptor;",
    "",
    "@Slf4j",
    "@GrpcGlobalServerInterceptor",
    "public class LoggingInterceptor implements ServerInterceptor {",
    "",
    "    @Override",
    "    public <ReqT, RespT> ServerCall.Listener<ReqT> interceptCall(",
    "            ServerCall<ReqT, RespT> call,",
    "            Metadata headers,",
    "            ServerCallHandler<ReqT, RespT> next) {",
    "",
    "        String methodName = call.getMethodDescriptor().getFullMethodName();",
    "        long startTime = System.currentTimeMillis();",
    "",
    "        log.info(\"gRPC 요청: {}\", methodName);",
    "",
    "        return new ForwardingServerCallListener.SimpleForwardingServerCallListener<>(",
    "                next.startCall(",
    "                        new ForwardingServerCall.SimpleForwardingServerCall<>(call) {",
    "                            @Override",
    "                            public void close(Status status, Metadata trailers) {",
    "                                long elapsed = System.currentTimeMillis() - startTime;",
    "                                log.info(\"gRPC 응답: {} [{}] {}ms\",",
    "                                        methodName, status.getCode(), elapsed);",
    "                                super.close(status, trailers);",
    "                            }",
    "                        }, headers)",
    "        ) {};",
    "    }",
    "}",
    "```",
    "",
    "**로그 출력:**",
    "",
    "```",
    "gRPC 요청: user.UserService/GetUser",
    "gRPC 응답: user.UserService/GetUser [OK] 15ms",
    "```",
    "",
    "### 인증 인터셉터",
    "",
    "```java",
    "@GrpcGlobalServerInterceptor",
    "public class AuthInterceptor implements ServerInterceptor {",
    "",
    "    private static final Metadata.Key<String> AUTH_KEY =",
    "            Metadata.Key.of(\"authorization\", Metadata.ASCII_STRING_MARSHALLER);",
    "",
    "    @Override",
    "    public <ReqT, RespT> ServerCall.Listener<ReqT> interceptCall(",
    "            ServerCall<ReqT, RespT> call,",
    "            Metadata headers,",
    "            ServerCallHandler<ReqT, RespT> next) {",
    "",
    "        String token = headers.get(AUTH_KEY);",
    "",
    "        if (token == null || !token.startsWith(\"Bearer \")) {",
    "            call.close(Status.UNAUTHENTICATED",
    "                    .withDescription(\"인증 토큰이 필요합니다\"), new Metadata());",
    "            return new ServerCall.Listener<>() {};",
    "        }",
    "",
    "        // 토큰 검증 로직...",
    "        return next.startCall(call, headers);",
    "    }",
    "}",
    "```",
