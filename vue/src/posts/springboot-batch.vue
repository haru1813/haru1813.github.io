    "> `JobParameters`에 고유값(`runId`)을 넣어야 같은 Job을 여러 번 실행할 수 있습니다. Spring Batch는 동일 파라미터로 이미 성공한 Job을 재실행하지 않습니다.",
    "",
    "## 방법 2: @Scheduled로 자동 실행",
    "",
    "스케줄링과 배치를 조합하여 **정기적으로 자동 실행**합니다.",
    "",
    "```java",
    "package com.example.demo.batch.scheduler;",
    "",
    "import lombok.RequiredArgsConstructor;",
    "import lombok.extern.slf4j.Slf4j;",
    "import org.springframework.batch.core.Job;",
    "import org.springframework.batch.core.JobParametersBuilder;",
    "import org.springframework.batch.core.launch.JobLauncher;",
    "import org.springframework.scheduling.annotation.EnableScheduling;",
    "import org.springframework.scheduling.annotation.Scheduled;",
    "import org.springframework.stereotype.Component;",
    "",
    "import java.time.LocalDateTime;",
    "",
    "@Component",
    "@RequiredArgsConstructor",
    "@Slf4j",
    "@EnableScheduling",
    "public class BatchScheduler {",
    "",
    "    private final JobLauncher jobLauncher;",
    "    private final Job expiredTokenCleanupJob;",
    "    private final Job userMigrationJob;",
    "",
    "    // 매일 자정에 만료 토큰 정리",
    "    @Scheduled(cron = \"0 0 0 * * *\")",
    "    public void runTokenCleanup() {",
    "        runJob(expiredTokenCleanupJob, \"tokenCleanup\");",
    "    }",
    "",
    "    // 매월 1일 새벽 2시에 사용자 마이그레이션",
    "    @Scheduled(cron = \"0 0 2 1 * *\")",
    "    public void runUserMigration() {",
    "        runJob(userMigrationJob, \"userMigration\");",
    "    }",
    "",
    "    private void runJob(Job job, String jobName) {",
    "        try {",
    "            var params = new JobParametersBuilder()",
    "                    .addString(\"runId\", jobName + \"-\" + System.currentTimeMillis())",
    "                    .addLocalDateTime(\"executedAt\", LocalDateTime.now())",
    "                    .toJobParameters();",
    "",
    "            var execution = jobLauncher.run(job, params);",
    "            log.info(\"[Scheduler] {} 실행 완료: {}\", jobName, execution.getStatus());",
    "        } catch (Exception e) {",
    "            log.error(\"[Scheduler] {} 실행 실패\", jobName, e);",
    "        }",
    "    }",
    "}",
    "```",
    "",
    "```mermaid",
    "sequenceDiagram",
    "    participant Sch as @Scheduled",
    "    participant JL as JobLauncher",
    "    participant J as Job",
    "    participant S as Step",
    "    participant DB as DB",
    "",
    "    Note over Sch: 매일 자정 (cron)",
    "    Sch->>JL: Job 실행 요청",
    "    JL->>J: expiredTokenCleanupJob 실행",
    "    J->>S: cleanupStep 실행",
    "    S->>DB: DELETE expired tokens",
    "    S-->>J: Step 완료",
    "    J-->>JL: Job 완료 (COMPLETED)",
    "    JL-->>Sch: 실행 결과 로그",
    "```",
