    "### ItemProcessor (데이터 가공)",
    "",
    "```java",
    "package com.example.demo.batch.processor;",
    "",
    "import com.example.demo.domain.User;",
    "import com.example.demo.domain.UserBackup;",
    "import org.springframework.batch.item.ItemProcessor;",
    "import org.springframework.stereotype.Component;",
    "",
    "import java.time.LocalDateTime;",
    "",
    "@Component",
    "public class UserItemProcessor implements ItemProcessor<User, UserBackup> {",
    "",
    "    @Override",
    "    public UserBackup process(User user) throws Exception {",
    "",
    "        // null 반환 시 해당 항목은 건너뜀 (필터링)",
    "        if (user.getEmail() == null) {",
    "            return null;",
    "        }",
    "",
    "        return UserBackup.builder()",
    "                .maskedName(maskName(user.getName()))",
    "                .email(user.getEmail())",
    "                .grade(calculateGrade(user.getTotalPurchase()))",
    "                .migratedAt(LocalDateTime.now())",
    "                .build();",
    "    }",
    "",
    "    private String maskName(String name) {",
    "        if (name == null || name.length() <= 1) return \"*\";",
    "        return name.charAt(0) + \"*\".repeat(name.length() - 1);",
    "    }",
    "",
    "    private String calculateGrade(int totalPurchase) {",
    "        if (totalPurchase >= 1_000_000) return \"VIP\";",
    "        if (totalPurchase >= 500_000) return \"GOLD\";",
    "        if (totalPurchase >= 100_000) return \"SILVER\";",
    "        return \"BRONZE\";",
    "    }",
    "}",
    "```",
