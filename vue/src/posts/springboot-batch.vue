    "# 2. Tasklet 기반 배치 (단순 작업)",
    "",
    "Reader→Processor→Writer 패턴이 필요 없는 단순 작업에 사용합니다.",
    "",
    "## 예제: 만료 토큰 삭제",
    "",
    "```java",
    "package com.example.demo.batch.tasklet;",
    "",
    "import com.example.demo.repository.TokenRepository;",
    "import lombok.RequiredArgsConstructor;",
    "import lombok.extern.slf4j.Slf4j;",
    "import org.springframework.batch.core.StepContribution;",
    "import org.springframework.batch.core.scope.context.ChunkContext;",
    "import org.springframework.batch.core.step.tasklet.Tasklet;",
    "import org.springframework.batch.repeat.RepeatStatus;",
    "import org.springframework.stereotype.Component;",
    "",
    "import java.time.LocalDateTime;",
    "",
    "@Component",
    "@RequiredArgsConstructor",
    "@Slf4j",
    "public class ExpiredTokenCleanupTasklet implements Tasklet {",
    "",
    "    private final TokenRepository tokenRepository;",
    "",
    "    @Override",
    "    public RepeatStatus execute(StepContribution contribution,",
    "                                ChunkContext chunkContext) throws Exception {",
    "",
    "        LocalDateTime threshold = LocalDateTime.now().minusDays(30);",
    "        int deleted = tokenRepository.deleteByCreatedAtBefore(threshold);",
    "",
    "        log.info(\"[Tasklet] 만료 토큰 {}건 삭제 완료\", deleted);",
    "",
    "        return RepeatStatus.FINISHED;  // 한 번 실행 후 종료",
    "    }",
    "}",
    "```",
    "",
    "### RepeatStatus",
    "",
    "| 상태 | 설명 |",
    "|------|------|",
    "| `FINISHED` | 작업 완료, Step 종료 |",
    "| `CONTINUABLE` | 다시 `execute()` 호출 (반복 작업) |",
    "",
    "### Tasklet Job 설정",
    "",
    "```java",
    "@Configuration",
    "@RequiredArgsConstructor",
    "public class ExpiredTokenCleanupJobConfig {",
    "",
    "    private final JobRepository jobRepository;",
    "    private final PlatformTransactionManager transactionManager;",
    "    private final ExpiredTokenCleanupTasklet cleanupTasklet;",
    "",
    "    @Bean",
    "    public Job expiredTokenCleanupJob() {",
    "        return new JobBuilder(\"expiredTokenCleanupJob\", jobRepository)",
    "                .start(cleanupStep())",
    "                .build();",
    "    }",
    "",
    "    @Bean",
    "    public Step cleanupStep() {",
    "        return new StepBuilder(\"cleanupStep\", jobRepository)",
    "                .tasklet(cleanupTasklet, transactionManager)",
    "                .build();",
    "    }",
    "}",
    "```",
