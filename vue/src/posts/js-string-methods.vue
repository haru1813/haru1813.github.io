    "- **추출**: `slice()`가 표준 (음수 인덱스 지원)",
    "- **검색**: `includes()`(포함 여부), `indexOf()`(위치), `startsWith()`/`endsWith()`(시작/끝)",
    "- **변환**: `toUpperCase()`/`toLowerCase()`, `trim()`, `replace()`/`replaceAll()`",
    "- **분리/결합**: `split()` → 배열로, 배열의 `join()` → 문자열로",
    "- **반복/채우기**: `repeat()`, `padStart()`/`padEnd()`",
    "- **패턴**: `split('').reverse().join('')`(뒤집기), `split(char).length - 1`(개수 세기)"
