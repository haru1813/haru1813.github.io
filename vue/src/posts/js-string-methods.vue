    "",
    "| 메서드 | 설명 | 반환 |",
    "|--------|------|------|",
    "| `length` | 문자열 길이 (속성) | 숫자 |",
    "| `charAt(i)` | i번째 문자 | 문자열 |",
    "| `at(i)` | i번째 문자 (음수 가능) | 문자열 |",
    "| `indexOf(s)` | 처음 발견 위치 | 숫자 (-1) |",
    "| `lastIndexOf(s)` | 마지막 발견 위치 | 숫자 (-1) |",
    "| `includes(s)` | 포함 여부 | boolean |",
    "| `startsWith(s)` | 시작 여부 | boolean |",
    "| `endsWith(s)` | 끝 여부 | boolean |",
    "| `slice(start, end)` | 부분 추출 | 문자열 |",
    "| `toUpperCase()` | 대문자 변환 | 문자열 |",
    "| `toLowerCase()` | 소문자 변환 | 문자열 |",
    "| `trim()` | 양쪽 공백 제거 | 문자열 |",
    "| `replace(a, b)` | 첫 번째 치환 | 문자열 |",
    "| `replaceAll(a, b)` | 전체 치환 | 문자열 |",
    "| `split(sep)` | 문자열 → 배열 | 배열 |",
    "| `repeat(n)` | n번 반복 | 문자열 |",
    "| `padStart(len, ch)` | 앞쪽 채우기 | 문자열 |",
    "| `padEnd(len, ch)` | 뒤쪽 채우기 | 문자열 |",
