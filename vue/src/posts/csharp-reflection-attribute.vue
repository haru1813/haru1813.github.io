    "## 실전 예제: 간단한 DI 컨테이너",
    "",
    "```csharp",
    "class SimpleContainer",
    "{",
    "    private Dictionary<Type, Type> _registrations = new();",
    "",
    "    public void Register<TInterface, TImplementation>()",
    "        where TImplementation : TInterface",
    "    {",
    "        _registrations[typeof(TInterface)] = typeof(TImplementation);",
    "    }",
    "",
    "    public T Resolve<T>() => (T)Resolve(typeof(T));",
    "",
    "    private object Resolve(Type type)",
    "    {",
    "        // 등록된 타입이면 구현 타입으로 교체",
    "        if (_registrations.TryGetValue(type, out Type implType))",
    "            type = implType;",
    "",
    "        // 생성자의 매개변수를 재귀적으로 해결",
    "        var ctor = type.GetConstructors().First();",
    "        var parameters = ctor.GetParameters()",
    "            .Select(p => Resolve(p.ParameterType))",
    "            .ToArray();",
    "",
    "        return Activator.CreateInstance(type, parameters);",
    "    }",
    "}",
    "",
    "// 사용 예",
    "interface ILogger { void Log(string msg); }",
    "class ConsoleLogger : ILogger",
    "{",
    "    public void Log(string msg) => Console.WriteLine(msg);",
    "}",
    "",
    "interface IUserService { void CreateUser(string name); }",
    "class UserService : IUserService",
    "{",
    "    private readonly ILogger _logger;",
    "    public UserService(ILogger logger) => _logger = logger;",
    "    public void CreateUser(string name) => _logger.Log($\"유저 생성: {name}\");",
    "}",
    "",
    "var container = new SimpleContainer();",
    "container.Register<ILogger, ConsoleLogger>();",
    "container.Register<IUserService, UserService>();",
    "",
    "var userService = container.Resolve<IUserService>();",
    "userService.CreateUser(\"하루\"); // 유저 생성: 하루",
