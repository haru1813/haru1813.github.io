    "### 유효성 검사 어트리뷰트",
    "",
    "```csharp",
    "[AttributeUsage(AttributeTargets.Property)]",
    "class RequiredAttribute : Attribute { }",
    "",
    "[AttributeUsage(AttributeTargets.Property)]",
    "class StringLengthAttribute : Attribute",
    "{",
    "    public int MinLength { get; }",
    "    public int MaxLength { get; }",
    "    public StringLengthAttribute(int min, int max)",
    "    {",
    "        MinLength = min;",
    "        MaxLength = max;",
    "    }",
    "}",
    "",
    "[AttributeUsage(AttributeTargets.Property)]",
    "class RangeAttribute : Attribute",
    "{",
    "    public int Min { get; }",
    "    public int Max { get; }",
    "    public RangeAttribute(int min, int max) { Min = min; Max = max; }",
    "}",
    "",
    "class User",
    "{",
    "    [Required]",
    "    [StringLength(2, 50)]",
    "    public string Name { get; set; }",
    "",
    "    [Required]",
    "    [Range(1, 150)]",
    "    public int Age { get; set; }",
    "",
    "    [StringLength(0, 200)]",
    "    public string Bio { get; set; }",
    "}",
    "",
    "// 리플렉션으로 유효성 검사 실행",
    "static List<string> Validate(object obj)",
    "{",
    "    var errors = new List<string>();",
    "    var properties = obj.GetType().GetProperties();",
    "",
    "    foreach (var prop in properties)",
    "    {",
    "        object value = prop.GetValue(obj);",
    "",
    "        if (prop.GetCustomAttribute<RequiredAttribute>() != null)",
    "        {",
    "            if (value == null || (value is string s && string.IsNullOrEmpty(s)))",
    "                errors.Add($\"{prop.Name}은(는) 필수 항목입니다.\");",
    "        }",
    "",
    "        var strLen = prop.GetCustomAttribute<StringLengthAttribute>();",
    "        if (strLen != null && value is string str)",
    "        {",
    "            if (str.Length < strLen.MinLength || str.Length > strLen.MaxLength)",
    "                errors.Add($\"{prop.Name}은(는) {strLen.MinLength}~{strLen.MaxLength}자여야 합니다.\");",
    "        }",
    "",
    "        var range = prop.GetCustomAttribute<RangeAttribute>();",
    "        if (range != null && value is int num)",
    "        {",
    "            if (num < range.Min || num > range.Max)",
    "                errors.Add($\"{prop.Name}은(는) {range.Min}~{range.Max} 범위여야 합니다.\");",
    "        }",
    "    }",
    "    return errors;",
    "}",
    "```",
