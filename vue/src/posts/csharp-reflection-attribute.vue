    "### BindingFlags 상세",
    "",
    "`GetMethods()`, `GetFields()` 등에 `BindingFlags`를 전달하면 **조회 범위를 세밀하게 제어**할 수 있습니다.",
    "",
    "| BindingFlags | 설명 |",
    "|-------------|------|",
    "| `Public` | public 멤버 |",
    "| `NonPublic` | private, protected, internal 멤버 |",
    "| `Instance` | 인스턴스 멤버 |",
    "| `Static` | 정적 멤버 |",
    "| `DeclaredOnly` | 상속된 멤버 제외, 해당 타입에서 선언된 것만 |",
    "| `FlattenHierarchy` | 상속 계층의 정적 멤버 포함 |",
    "",
    "```csharp",
    "using System.Reflection;",
    "",
    "class Animal",
    "{",
    "    public string Name;",
    "    private int _age;",
    "    protected bool IsAlive;",
    "    public static int Count;",
    "}",
    "",
    "// public 인스턴스 필드만",
    "var publicFields = typeof(Animal).GetFields(",
    "    BindingFlags.Public | BindingFlags.Instance);",
    "// → Name",
    "",
    "// private 인스턴스 필드만",
    "var privateFields = typeof(Animal).GetFields(",
    "    BindingFlags.NonPublic | BindingFlags.Instance);",
    "// → _age, IsAlive",
    "",
    "// 모든 필드 (public + private + instance + static)",
    "var allFields = typeof(Animal).GetFields(",
    "    BindingFlags.Public | BindingFlags.NonPublic |",
    "    BindingFlags.Instance | BindingFlags.Static);",
    "// → Name, _age, IsAlive, Count",
