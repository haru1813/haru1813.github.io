    "## 비동기 메일 발송",
    "",
    "메일 발송은 **SMTP 서버와의 네트워크 통신**이므로 시간이 걸립니다. **@Async**를 사용하면 메일 발송을 비동기로 처리하여 **응답 시간을 단축**할 수 있습니다.",
    "",
    "```mermaid",
    "sequenceDiagram",
    "    participant C as 클라이언트",
    "    participant Ctrl as Controller",
    "    participant Svc as MailService",
    "    participant SMTP as SMTP 서버",
    "",
    "    Note over C,SMTP: 동기 방식 (느림)",
    "    C->>Ctrl: 회원가입 요청",
    "    Ctrl->>Svc: sendMail()",
    "    Svc->>SMTP: 메일 전송 (2~5초)",
    "    SMTP-->>Svc: 완료",
    "    Svc-->>Ctrl: 완료",
    "    Ctrl-->>C: 응답 (3~6초 소요)",
    "",
    "    Note over C,SMTP: 비동기 방식 (빠름)",
    "    C->>Ctrl: 회원가입 요청",
    "    Ctrl->>Svc: sendMailAsync()",
    "    Svc-->>Ctrl: 즉시 반환",
    "    Ctrl-->>C: 응답 (100ms)",
    "    Svc->>SMTP: 별도 스레드에서 메일 전송",
    "```",
    "",
    "### @Async 설정",
    "",
    "```java",
    "import org.springframework.context.annotation.Configuration;",
    "import org.springframework.scheduling.annotation.EnableAsync;",
    "import org.springframework.scheduling.concurrent.ThreadPoolTaskExecutor;",
    "import org.springframework.context.annotation.Bean;",
    "import java.util.concurrent.Executor;",
    "",
    "@Configuration",
    "@EnableAsync",
    "public class AsyncConfig {",
    "",
    "    @Bean(name = \"mailExecutor\")",
    "    public Executor mailExecutor() {",
    "        ThreadPoolTaskExecutor executor = new ThreadPoolTaskExecutor();",
    "        executor.setCorePoolSize(2);",
    "        executor.setMaxPoolSize(5);",
    "        executor.setQueueCapacity(100);",
    "        executor.setThreadNamePrefix(\"mail-\");",
    "        executor.initialize();",
    "        return executor;",
    "    }",
    "}",
    "```",
