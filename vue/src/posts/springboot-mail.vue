    "    U->>C: POST /api/auth/signup",
    "    C->>S: signup(request)",
    "    S->>S: 인증 코드 생성 (6자리)",
    "    S->>R: 코드 저장 (key: email, TTL: 5분)",
    "    S->>M: sendVerificationMail(email, code)",
