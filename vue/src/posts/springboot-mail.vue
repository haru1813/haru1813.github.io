    "### 인증 코드 생성 유틸리티",
    "",
    "```java",
    "import java.security.SecureRandom;",
    "",
    "public class VerificationCodeUtil {",
    "",
    "    private static final SecureRandom random = new SecureRandom();",
    "",
    "    public static String generateCode(int length) {",
    "        StringBuilder code = new StringBuilder();",
    "        for (int i = 0; i < length; i++) {",
    "            code.append(random.nextInt(10));",
    "        }",
    "        return code.toString();",
    "    }",
    "}",
    "```",
    "",
    "### AuthService 구현",
    "",
    "```java",
    "import lombok.RequiredArgsConstructor;",
    "import lombok.extern.slf4j.Slf4j;",
    "import org.springframework.data.redis.core.StringRedisTemplate;",
    "import org.springframework.stereotype.Service;",
    "",
    "import java.time.Duration;",
    "",
    "@Slf4j",
    "@Service",
    "@RequiredArgsConstructor",
    "public class AuthService {",
    "",
    "    private final UserRepository userRepository;",
    "    private final MailService mailService;",
    "    private final StringRedisTemplate redisTemplate;",
    "",
    "    private static final String VERIFY_PREFIX = \"email:verify:\";",
    "    private static final Duration CODE_TTL = Duration.ofMinutes(5);",
    "",
    "    public void sendVerificationMail(String email) {",
    "        String code = VerificationCodeUtil.generateCode(6);",
    "",
    "        redisTemplate.opsForValue()",
    "                .set(VERIFY_PREFIX + email, code, CODE_TTL);",
    "",
    "        try {",
    "            mailService.sendTemplateMail(",
    "                email,",
    "                \"이메일 인증 코드\",",
    "                \"welcome\",",
    "                Map.of(",
    "                    \"title\", \"이메일 인증\",",
    "                    \"userName\", email,",
    "                    \"message\", \"아래 인증 코드를 입력해주세요. (5분 이내)\",",
    "                    \"code\", code",
    "                )",
    "            );",
    "            log.info(\"인증 메일 발송 완료 - email: {}\", email);",
    "        } catch (Exception e) {",
    "            log.error(\"인증 메일 발송 실패 - email: {}\", email, e);",
    "            throw new MailSendException(\"메일 발송에 실패했습니다.\");",
    "        }",
    "    }",
    "",
    "    public void verifyEmail(String email, String code) {",
    "        String savedCode = redisTemplate.opsForValue()",
    "                .get(VERIFY_PREFIX + email);",
    "",
    "        if (savedCode == null) {",
    "            throw new VerificationException(\"인증 코드가 만료되었습니다.\");",
    "        }",
    "",
    "        if (!savedCode.equals(code)) {",
    "            throw new VerificationException(\"인증 코드가 일치하지 않습니다.\");",
    "        }",
    "",
    "        redisTemplate.delete(VERIFY_PREFIX + email);",
    "",
    "        User user = userRepository.findByEmail(email)",
    "                .orElseThrow(() -> new UserNotFoundException(email));",
    "        user.setEmailVerified(true);",
    "        userRepository.save(user);",
    "",
    "        log.info(\"이메일 인증 완료 - email: {}\", email);",
    "    }",
    "}",
    "```",
    "",
    "### AuthController",
    "",
    "```java",
    "@RestController",
    "@RequestMapping(\"/api/auth\")",
    "@RequiredArgsConstructor",
    "public class AuthController {",
    "",
    "    private final AuthService authService;",
    "",
    "    @PostMapping(\"/send-verification\")",
    "    public ResponseEntity<String> sendVerification(",
    "            @RequestBody @Valid EmailRequest request) {",
    "        authService.sendVerificationMail(request.email());",
    "        return ResponseEntity.ok(\"인증 메일이 발송되었습니다.\");",
    "    }",
    "",
    "    @PostMapping(\"/verify\")",
    "    public ResponseEntity<String> verifyEmail(",
    "            @RequestBody @Valid VerifyRequest request) {",
    "        authService.verifyEmail(request.email(), request.code());",
    "        return ResponseEntity.ok(\"이메일 인증이 완료되었습니다.\");",
    "    }",
    "}",
    "```",
