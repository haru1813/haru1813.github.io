    "## useEffect (사이드 이펙트)",
    "",
    "데이터 가져오기, 타이머, DOM 조작 등 **렌더링 이외의 작업**을 수행합니다.",
    "",
    "```tsx",
    "import { useState, useEffect } from 'react';",
    "",
    "function Timer() {",
    "  const [seconds, setSeconds] = useState(0);",
    "",
    "  useEffect(() => {",
    "    // 마운트 시 타이머 시작",
    "    const interval = setInterval(() => {",
    "      setSeconds(prev => prev + 1);",
    "    }, 1000);",
    "",
    "    // 클린업 (언마운트 시 타이머 정리)",
    "    return () => clearInterval(interval);",
    "  }, []); // 빈 배열: 마운트 시 1번만 실행",
    "",
    "  return <p>경과 시간: {seconds}초</p>;",
    "}",
    "```",
    "",
    "### 의존성 배열",
    "",
    "| 형태 | 실행 시점 |",
    "|------|----------|",
    "| `useEffect(() => {}, [])` | 마운트 시 **1번만** 실행 |",
    "| `useEffect(() => {}, [count])` | `count`가 **변경될 때마다** 실행 |",
    "| `useEffect(() => {})` | **매 렌더링마다** 실행 (주의!) |",
    "",
    "### API 데이터 가져오기",
    "```tsx",
    "interface Post {",
    "  id: number;",
    "  title: string;",
