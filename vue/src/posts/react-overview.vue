    "  const [loading, setLoading] = useState(true);",
    "",
    "  useEffect(() => {",
    "    fetch('https://jsonplaceholder.typicode.com/posts?_limit=5')",
    "      .then(res => res.json())",
    "      .then((data: Post[]) => {",
    "        setPosts(data);",
    "        setLoading(false);",
    "      });",
    "  }, []);",
    "",
    "  if (loading) return <p>로딩 중...</p>;",
    "",
    "  return (",
    "    <ul>",
    "      {posts.map(post => (",
    "        <li key={post.id}>{post.title}</li>",
    "      ))}",
    "    </ul>",
    "  );",
    "}",
    "```",
