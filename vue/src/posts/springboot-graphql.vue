    "## Controller (GraphQL 핵심)",
    "",
    "Spring for GraphQL에서는 `@Controller`에 **`@QueryMapping`**, **`@MutationMapping`**, **`@SchemaMapping`** 어노테이션을 사용합니다.",
    "",
    "### UserController.java",
    "",
    "```java",
    "package com.example.demo.controller;",
    "",
    "import com.example.demo.entity.Post;",
    "import com.example.demo.entity.User;",
    "import com.example.demo.service.PostService;",
    "import com.example.demo.service.UserService;",
    "import lombok.RequiredArgsConstructor;",
    "import org.springframework.graphql.data.method.annotation.*;",
    "import org.springframework.stereotype.Controller;",
    "",
    "import java.util.List;",
    "import java.util.Map;",
    "",
    "@Controller",
    "@RequiredArgsConstructor",
    "public class UserController {",
    "",
    "    private final UserService userService;",
    "    private final PostService postService;",
    "",
    "    // ─── Query ─────────────────────────────────",
    "",
    "    @QueryMapping",
    "    public List<User> users() {",
    "        return userService.findAll();",
    "    }",
    "",
    "    @QueryMapping",
    "    public User user(@Argument Long id) {",
    "        return userService.findById(id);",
    "    }",
    "",
    "    // ─── Mutation ──────────────────────────────",
    "",
    "    @MutationMapping",
    "    public User createUser(@Argument Map<String, Object> input) {",
    "        String name = (String) input.get(\"name\");",
    "        String email = (String) input.get(\"email\");",
    "        Integer age = (Integer) input.get(\"age\");",
    "        return userService.create(name, email, age);",
    "    }",
    "",
    "    @MutationMapping",
    "    public User updateUser(@Argument Long id, @Argument Map<String, Object> input) {",
    "        String name = (String) input.get(\"name\");",
    "        String email = (String) input.get(\"email\");",
    "        Integer age = (Integer) input.get(\"age\");",
    "        return userService.update(id, name, email, age);",
    "    }",
    "",
    "    @MutationMapping",
    "    public boolean deleteUser(@Argument Long id) {",
    "        return userService.delete(id);",
    "    }",
    "",
    "    // ─── SchemaMapping (관계 해석) ──────────────",
    "",
    "    @SchemaMapping(typeName = \"User\", field = \"posts\")",
    "    public List<Post> posts(User user) {",
    "        return postService.findByAuthorId(user.getId());",
    "    }",
