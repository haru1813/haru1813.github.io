    "## N+1 문제와 @BatchMapping",
    "",
    "### N+1 문제란?",
    "",
    "사용자 목록을 조회하면서 각 사용자의 게시글도 함께 요청하면, **사용자 수만큼 추가 쿼리**가 발생합니다.",
    "",
    "```graphql",
    "query {",
    "  users {        # 1번 쿼리: 사용자 목록",
    "    name",
    "    posts {      # N번 쿼리: 각 사용자별 게시글 조회",
    "      title",
    "    }",
    "  }",
    "}",
    "```",
    "",
    "```mermaid",
    "flowchart TD",
    "    A[\"users 조회 (1번 쿼리)\"] --> B[\"User 1의 posts 조회\"]",
    "    A --> C[\"User 2의 posts 조회\"]",
    "    A --> D[\"User 3의 posts 조회\"]",
    "    A --> E[\"...\"]",
    "    A --> F[\"User N의 posts 조회\"]",
    "    style A fill:#4a90d9,color:#fff",
    "    style B fill:#e74c3c,color:#fff",
    "    style C fill:#e74c3c,color:#fff",
    "    style D fill:#e74c3c,color:#fff",
    "    style F fill:#e74c3c,color:#fff",
    "```",
    "",
    "### 해결: @BatchMapping",
    "",
    "`@BatchMapping`을 사용하면 여러 부모 객체에 대한 하위 데이터를 **한 번에 일괄 조회**합니다.",
    "",
    "```java",
    "@Controller",
    "@RequiredArgsConstructor",
    "public class UserController {",
    "",
    "    private final UserService userService;",
    "    private final PostService postService;",
    "",
    "    @QueryMapping",
    "    public List<User> users() {",
    "        return userService.findAll();",
    "    }",
    "",
    "    // @SchemaMapping 대신 @BatchMapping 사용",
    "    @BatchMapping(typeName = \"User\", field = \"posts\")",
    "    public Map<User, List<Post>> posts(List<User> users) {",
    "        // 모든 사용자의 ID를 한 번에 모아서 조회",
    "        List<Long> userIds = users.stream()",
    "                .map(User::getId)",
    "                .toList();",
    "",
    "        List<Post> allPosts = postService.findByAuthorIds(userIds);",
    "",
    "        // 사용자별로 그룹핑하여 반환",
    "        return users.stream().collect(Collectors.toMap(",
    "                user -> user,",
    "                user -> allPosts.stream()",
    "                        .filter(p -> p.getAuthor().getId().equals(user.getId()))",
    "                        .toList()",
    "        ));",
    "    }",
    "}",
    "```",
    "",
    "```mermaid",
    "flowchart TD",
    "    A[\"users 조회 (1번 쿼리)\"] --> B[\"모든 User의 posts 일괄 조회 (1번 쿼리)\"]",
    "    B --> C[\"User 1 → posts 매핑\"]",
    "    B --> D[\"User 2 → posts 매핑\"]",
    "    B --> E[\"User 3 → posts 매핑\"]",
    "    style A fill:#4a90d9,color:#fff",
    "    style B fill:#27ae60,color:#fff",
    "```",
    "",
    "| 방식 | 쿼리 수 | 어노테이션 |",
    "|------|---------|------------|",
    "| `@SchemaMapping` | 1 + N (부모 1 + 자식 N) | 개별 호출 |",
    "| `@BatchMapping` | 1 + 1 (부모 1 + 자식 일괄 1) | 일괄 호출 |",
    "",
