    "## 2. 쿼리 우선순위",
    "",
    "React Testing Library는 **사용자가 요소를 찾는 방식**과 유사한 쿼리를 권장합니다.",
    "",
    "| 우선순위 | 쿼리 | 용도 |",
    "|---------|------|------|",
    "| 1 (최우선) | `getByRole` | 역할(button, heading 등) |",
    "| 2 | `getByLabelText` | 폼 input의 label |",
    "| 3 | `getByPlaceholderText` | placeholder |",
    "| 4 | `getByText` | 텍스트 내용 |",
    "| 5 | `getByDisplayValue` | input의 현재 값 |",
    "| 6 | `getByAltText` | 이미지 alt |",
    "| 7 (최후) | `getByTestId` | `data-testid` 속성 |",
    "",
    "### 쿼리 변형",
    "",
    "| 접두사 | 찾으면 | 못 찾으면 | 용도 |",
    "|--------|--------|----------|------|",
    "| `getBy` | 요소 반환 | throw | 존재해야 하는 요소 |",
    "| `queryBy` | 요소 반환 | `null` | 없어야 하는 요소 검증 |",
    "| `findBy` | Promise 반환 | throw | 비동기 요소 대기 |",
