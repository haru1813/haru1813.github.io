    "# 7. 트랜잭션 처리",
    "",
    "JdbcTemplate은 `@Transactional`로 트랜잭션을 관리합니다. `spring-boot-starter-jdbc`에 `DataSourceTransactionManager`가 자동 설정됩니다.",
    "",
    "```java",
    "@Service",
    "@RequiredArgsConstructor",
    "public class TransferService {",
    "",
    "    private final JdbcTemplate jdbcTemplate;",
    "",
    "    @Transactional",
    "    public void transfer(Long fromId, Long toId, int amount) {",
    "        int fromBalance = jdbcTemplate.queryForObject(",
    "                \"SELECT balance FROM accounts WHERE id = ?\", Integer.class, fromId);",
    "",
    "        if (fromBalance < amount) {",
    "            throw new RuntimeException(\"잔액 부족\");",
    "        }",
    "",
    "        jdbcTemplate.update(",
    "                \"UPDATE accounts SET balance = balance - ? WHERE id = ?\",",
    "                amount, fromId);",
    "",
    "        jdbcTemplate.update(",
    "                \"UPDATE accounts SET balance = balance + ? WHERE id = ?\",",
    "                amount, toId);",
    "    }",
    "}",
    "```",
    "",
