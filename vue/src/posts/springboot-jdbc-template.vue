    "# 2. NamedParameterJdbcTemplate",
    "",
    "`?` 대신 **이름 기반 파라미터** (`:name`)를 사용할 수 있습니다. SQL이 길어질수록 가독성이 크게 향상됩니다.",
    "",
    "```java",
    "package com.example.demo.repository;",
    "",
    "import com.example.demo.domain.User;",
    "import lombok.RequiredArgsConstructor;",
    "import org.springframework.jdbc.core.RowMapper;",
    "import org.springframework.jdbc.core.namedparam.BeanPropertySqlParameterSource;",
    "import org.springframework.jdbc.core.namedparam.MapSqlParameterSource;",
    "import org.springframework.jdbc.core.namedparam.NamedParameterJdbcTemplate;",
    "import org.springframework.jdbc.support.GeneratedKeyHolder;",
    "import org.springframework.jdbc.support.KeyHolder;",
    "import org.springframework.stereotype.Repository;",
    "",
    "import java.util.List;",
    "import java.util.Map;",
    "import java.util.Optional;",
    "",
    "@Repository",
    "@RequiredArgsConstructor",
    "public class UserNamedRepository {",
    "",
    "    private final NamedParameterJdbcTemplate namedTemplate;",
    "",
    "    private final RowMapper<User> userRowMapper = (rs, rowNum) -> User.builder()",
    "            .id(rs.getLong(\"id\"))",
    "            .name(rs.getString(\"name\"))",
    "            .email(rs.getString(\"email\"))",
    "            .age(rs.getInt(\"age\"))",
    "            .department(rs.getString(\"department\"))",
    "            .createdAt(rs.getTimestamp(\"created_at\").toLocalDateTime())",
    "            .updatedAt(rs.getTimestamp(\"updated_at\").toLocalDateTime())",
    "            .build();",
    "",
    "    // MapSqlParameterSource 사용",
    "    public Optional<User> findById(Long id) {",
    "        String sql = \"SELECT * FROM users WHERE id = :id\";",
    "        MapSqlParameterSource params = new MapSqlParameterSource(\"id\", id);",
    "        List<User> results = namedTemplate.query(sql, params, userRowMapper);",
    "        return results.stream().findFirst();",
    "    }",
    "",
    "    // Map 사용",
    "    public List<User> findByDepartmentAndMinAge(String department, int minAge) {",
    "        String sql = \"SELECT * FROM users WHERE department = :dept AND age >= :minAge\";",
    "        Map<String, Object> params = Map.of(",
    "                \"dept\", department,",
    "                \"minAge\", minAge",
    "        );",
    "        return namedTemplate.query(sql, params, userRowMapper);",
    "    }",
    "",
    "    // BeanPropertySqlParameterSource 사용 (객체 필드 → 파라미터 자동 매핑)",
    "    public User save(User user) {",
    "        String sql = \"INSERT INTO users (name, email, age, department) \" +",
    "                     \"VALUES (:name, :email, :age, :department)\";",
    "",
    "        BeanPropertySqlParameterSource params = new BeanPropertySqlParameterSource(user);",
    "        KeyHolder keyHolder = new GeneratedKeyHolder();",
    "",
    "        namedTemplate.update(sql, params, keyHolder);",
    "        user.setId(keyHolder.getKey().longValue());",
    "        return user;",
    "    }",
    "",
    "    public int update(User user) {",
    "        String sql = \"UPDATE users SET name = :name, email = :email, \" +",
    "                     \"age = :age, department = :department, \" +",
    "                     \"updated_at = CURRENT_TIMESTAMP WHERE id = :id\";",
    "        return namedTemplate.update(sql, new BeanPropertySqlParameterSource(user));",
    "    }",
    "}",
    "```",
    "",
    "### 파라미터 소스 비교",
    "",
    "| 방식 | 설명 | 사용 시점 |",
    "|------|------|---------|",
    "| `MapSqlParameterSource` | `.addValue(\"name\", value)` | 개별 파라미터 지정 |",
    "| `Map<String, Object>` | `Map.of(\"name\", value)` | 간단한 파라미터 |",
    "| `BeanPropertySqlParameterSource` | 객체 필드 → 파라미터 자동 | **객체 전체를 전달** |",
    "",
    "### `?` vs `:name` 비교",
    "",
    "```java",
    "// ❌ 위치 기반 (? → 순서 중요, 파라미터 많으면 혼동)",
    "jdbcTemplate.update(",
    "    \"INSERT INTO users (name, email, age, dept) VALUES (?, ?, ?, ?)\",",
    "    user.getName(), user.getEmail(), user.getAge(), user.getDept()",
    ");",
    "",
    "// ✅ 이름 기반 (:name → 순서 무관, 가독성 좋음)",
    "namedTemplate.update(",
    "    \"INSERT INTO users (name, email, age, dept) VALUES (:name, :email, :age, :dept)\",",
    "    new BeanPropertySqlParameterSource(user)",
    ");",
    "```",
