    "",
    "동의어는 테이블, 뷰, 시퀀스 등의 객체에 **별칭(별명)**을 부여하는 기능입니다. 다른 사용자의 객체에 접근할 때 `스키마.객체명` 대신 짧은 이름을 사용할 수 있습니다.",
