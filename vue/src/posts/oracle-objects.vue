    "SELECT *",
    "  FROM (SELECT ROWNUM AS RN, T.*",
    "          FROM (SELECT EMPNO, ENAME, SAL",
    "                  FROM EMP",
    "                 ORDER BY SAL DESC) T",
    "         WHERE ROWNUM <= 10)  -- 끝 행번호",
