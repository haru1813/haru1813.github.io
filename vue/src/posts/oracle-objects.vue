    "## 4. ROWNUM",
    "",
    "ROWNUM은 쿼리 결과에 **행 번호를 부여**하는 Oracle의 가상 컬럼(Pseudo Column)입니다. WHERE 절이 처리된 후, ORDER BY 이전에 번호가 매겨집니다.",
    "",
    "### 기본 사용법",
    "",
    "```sql",
    "-- 각 행에 번호 부여",
    "SELECT ROWNUM, EMPNO, ENAME, SAL",
    "  FROM EMP;",
    "",
    "-- 상위 5건만 조회",
    "SELECT ROWNUM, EMPNO, ENAME, SAL",
    "  FROM EMP",
    " WHERE ROWNUM <= 5;",
    "```",
    "",
    "### TOP-N 쿼리",
    "",
    "ROWNUM은 ORDER BY보다 먼저 실행되므로, 정렬 후 상위 N건을 구하려면 **서브쿼리**를 사용해야 합니다.",
    "",
    "```sql",
    "-- 급여 상위 3명 조회 (올바른 방법)",
    "SELECT ROWNUM, EMPNO, ENAME, SAL",
    "  FROM (SELECT EMPNO, ENAME, SAL",
    "          FROM EMP",
    "         ORDER BY SAL DESC)",
    " WHERE ROWNUM <= 3;",
    "```",
    "",
    "### ROWNUM 주의사항",
    "",
    "```sql",
    "-- ❌ 잘못된 사용: ROWNUM > 1 은 결과가 없음",
    "SELECT * FROM EMP WHERE ROWNUM > 1;",
    "",
    "-- ROWNUM은 1부터 순차적으로 부여되므로",
    "-- 첫 번째 행이 조건에 실패하면 다음 행도 계속 실패함",
    "",
    "-- ✅ 올바른 방법: 서브쿼리와 별칭 사용",
    "SELECT *",
    "  FROM (SELECT ROWNUM AS RN, E.*",
    "          FROM EMP E)",
    " WHERE RN BETWEEN 3 AND 5;",
    "```",
    "",
