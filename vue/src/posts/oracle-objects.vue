    "## 5. 시퀀스 (SEQUENCE)",
    "",
    "시퀀스는 **자동으로 번호를 생성**하는 Oracle 객체입니다. 주로 기본 키(PK)에 고유한 번호를 부여할 때 사용합니다.",
    "",
    "### 시퀀스 생성",
    "",
    "```sql",
    "-- 기본 시퀀스 생성",
    "CREATE SEQUENCE seq_emp_id;",
    "",
    "-- 옵션을 지정한 시퀀스 생성",
    "CREATE SEQUENCE seq_board_no",
    "    START WITH 1         -- 시작 값",
    "    INCREMENT BY 1       -- 증가 값",
    "    MINVALUE 1           -- 최솟값",
    "    MAXVALUE 99999       -- 최댓값",
    "    NOCYCLE              -- 최댓값 도달 시 순환하지 않음",
    "    NOCACHE;             -- 캐시 사용 안 함",
    "```",
    "",
    "### 시퀀스 옵션 설명",
    "",
    "| 옵션 | 설명 |",
    "|------|------|",
    "| **START WITH n** | 시작 번호 (기본값: 1) |",
    "| **INCREMENT BY n** | 증가 폭 (기본값: 1) |",
    "| **MAXVALUE n / NOMAXVALUE** | 최댓값 설정 / 무제한 |",
    "| **MINVALUE n / NOMINVALUE** | 최솟값 설정 / 무제한 |",
    "| **CYCLE / NOCYCLE** | 최댓값 도달 시 순환 여부 |",
    "| **CACHE n / NOCACHE** | 메모리에 미리 생성할 번호 개수 (기본: 20) |",
    "",
    "### NEXTVAL과 CURRVAL",
    "",
    "| 함수 | 설명 |",
    "|------|------|",
    "| **NEXTVAL** | 다음 번호를 생성하고 반환 |",
    "| **CURRVAL** | 현재 세션에서 마지막으로 생성된 번호 반환 |",
    "",
    "```sql",
    "-- 다음 번호 생성",
    "SELECT seq_board_no.NEXTVAL FROM DUAL;",
    "",
    "-- 현재 번호 확인 (NEXTVAL을 한 번 이상 호출한 후 사용 가능)",
    "SELECT seq_board_no.CURRVAL FROM DUAL;",
    "```",
    "",
    "### 시퀀스를 INSERT에 활용",
    "",
    "```sql",
    "-- 게시판 테이블 생성",
    "CREATE TABLE BOARD (",
    "    BOARD_NO NUMBER PRIMARY KEY,",
    "    TITLE    VARCHAR2(100),",
    "    CONTENT  VARCHAR2(4000),",
    "    REG_DATE DATE DEFAULT SYSDATE",
    ");",
    "",
    "-- 시퀀스를 사용한 데이터 삽입",
    "INSERT INTO BOARD (BOARD_NO, TITLE, CONTENT)",
    "VALUES (seq_board_no.NEXTVAL, '첫 번째 글', '안녕하세요!');",
    "",
    "INSERT INTO BOARD (BOARD_NO, TITLE, CONTENT)",
    "VALUES (seq_board_no.NEXTVAL, '두 번째 글', '반갑습니다!');",
    "",
    "SELECT * FROM BOARD;",
    "```",
    "",
    "### 시퀀스 수정 및 삭제",
    "",
    "```sql",
    "-- 시퀀스 수정 (START WITH는 변경 불가)",
    "ALTER SEQUENCE seq_board_no",
    "    INCREMENT BY 5",
    "    MAXVALUE 999999;",
    "",
    "-- 시퀀스 삭제",
    "DROP SEQUENCE seq_board_no;",
    "",
    "-- 시퀀스 정보 조회",
    "SELECT SEQUENCE_NAME, MIN_VALUE, MAX_VALUE,",
    "       INCREMENT_BY, LAST_NUMBER",
    "  FROM USER_SEQUENCES;",
    "```",
