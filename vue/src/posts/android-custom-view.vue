    "## 간단한 그림판 만들기",
    "",
    "Canvas와 Touch 이벤트를 결합한 실전 예제입니다.",
    "",
    "```java",
    "public class DrawingView extends View {",
    "",
    "    private Paint drawPaint;",
    "    private Path drawPath;",
    "    private List<PathData> paths = new ArrayList<>();",
    "    private int currentColor = Color.BLACK;",
    "    private float strokeWidth = 8f;",
    "",
    "    static class PathData {",
    "        Path path;",
    "        Paint paint;",
    "",
    "        PathData(Path path, Paint paint) {",
    "            this.path = path;",
    "            this.paint = paint;",
    "        }",
    "    }",
    "",
    "    public DrawingView(Context context, AttributeSet attrs) {",
    "        super(context, attrs);",
    "        init();",
    "    }",
    "",
    "    private void init() {",
    "        drawPath = new Path();",
    "        drawPaint = createPaint();",
    "    }",
    "",
    "    private Paint createPaint() {",
    "        Paint paint = new Paint(Paint.ANTI_ALIAS_FLAG);",
    "        paint.setColor(currentColor);",
    "        paint.setStyle(Paint.Style.STROKE);",
    "        paint.setStrokeWidth(strokeWidth);",
    "        paint.setStrokeCap(Paint.Cap.ROUND);",
    "        paint.setStrokeJoin(Paint.Join.ROUND);",
    "        return paint;",
    "    }",
    "",
    "    @Override",
    "    protected void onDraw(Canvas canvas) {",
    "        super.onDraw(canvas);",
    "        canvas.drawColor(Color.WHITE);",
    "",
    "        for (PathData pd : paths) {",
    "            canvas.drawPath(pd.path, pd.paint);",
    "        }",
    "        canvas.drawPath(drawPath, drawPaint);",
    "    }",
    "",
    "    @Override",
    "    public boolean onTouchEvent(MotionEvent event) {",
    "        float x = event.getX();",
    "        float y = event.getY();",
    "",
    "        switch (event.getAction()) {",
    "            case MotionEvent.ACTION_DOWN:",
    "                drawPath.moveTo(x, y);",
    "                return true;",
    "",
    "            case MotionEvent.ACTION_MOVE:",
    "                drawPath.lineTo(x, y);",
    "                break;",
    "",
    "            case MotionEvent.ACTION_UP:",
    "                paths.add(new PathData(drawPath, drawPaint));",
    "                drawPath = new Path();",
    "                drawPaint = createPaint();",
    "                break;",
    "",
    "            default:",
    "                return false;",
    "        }",
    "",
    "        invalidate();",
    "        return true;",
    "    }",
    "",
    "    public void setDrawColor(int color) {",
    "        currentColor = color;",
    "        drawPaint.setColor(color);",
    "    }",
    "",
    "    public void clearCanvas() {",
    "        paths.clear();",
    "        drawPath.reset();",
    "        invalidate();",
    "    }",
    "}",
    "```",
