    "## DynamicObject로 커스텀 dynamic 객체 만들기",
    "",
    "`DynamicObject`를 상속하면 동적 멤버 접근 시 동작을 직접 제어할 수 있습니다.",
    "",
    "```csharp",
    "using System.Dynamic;",
    "",
    "class FlexibleObject : DynamicObject",
    "{",
    "    private Dictionary<string, object> _members = new();",
    "",
    "    // 프로퍼티 읽기 시 호출",
    "    public override bool TryGetMember(GetMemberBinder binder, out object result)",
    "    {",
    "        if (_members.TryGetValue(binder.Name, out result))",
    "            return true;",
    "",
    "        result = $\"[{binder.Name}은(는) 정의되지 않음]\";",
    "        return true;",
    "    }",
    "",
    "    // 프로퍼티 쓰기 시 호출",
    "    public override bool TrySetMember(SetMemberBinder binder, object value)",
    "    {",
    "        _members[binder.Name] = value;",
    "        Console.WriteLine($\"  → {binder.Name} = {value} 설정됨\");",
    "        return true;",
    "    }",
    "",
    "    // 메서드 호출 시 호출",
    "    public override bool TryInvokeMember(",
    "        InvokeMemberBinder binder, object[] args, out object result)",
    "    {",
    "        if (_members.TryGetValue(binder.Name, out object member) && member is Delegate del)",
    "        {",
    "            result = del.DynamicInvoke(args);",
    "            return true;",
    "        }",
    "        result = null;",
    "        return false;",
    "    }",
    "}",
    "",
    "dynamic flex = new FlexibleObject();",
    "flex.Name = \"하루\";         // → Name = 하루 설정됨",
    "flex.Score = 95;            // → Score = 95 설정됨",
    "Console.WriteLine(flex.Name);    // 하루",
    "Console.WriteLine(flex.Unknown); // [Unknown은(는) 정의되지 않음]",
    "```",
    "",
