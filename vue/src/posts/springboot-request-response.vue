    "## 15) Service",
    "",
    "```java",
    "package com.example.demo.service;",
    "",
    "import com.example.demo.dto.MemberCreateForm;",
    "import com.example.demo.dto.MemberUpdateForm;",
    "import com.example.demo.entity.Member;",
    "import com.example.demo.repository.MemberRepository;",
    "import lombok.RequiredArgsConstructor;",
    "import org.springframework.stereotype.Service;",
    "import org.springframework.web.multipart.MultipartFile;",
    "",
    "import java.io.IOException;",
    "import java.nio.file.*;",
    "import java.util.List;",
    "import java.util.UUID;",
    "",
    "@Service",
    "@RequiredArgsConstructor",
    "public class MemberService {",
    "",
    "    private final MemberRepository memberRepository;",
    "    private static final String UPLOAD_DIR = \"uploads/\";",
    "",
    "    public Member create(MemberCreateForm form) {",
    "        Member member = new Member();",
    "        member.setName(form.getName());",
    "        member.setEmail(form.getEmail());",
    "        member.setAge(form.getAge());",
    "",
    "        if (form.getProfileImage() != null && !form.getProfileImage().isEmpty()) {",
    "            String savedPath = saveFile(form.getProfileImage());",
    "            member.setProfileImagePath(savedPath);",
    "        }",
    "",
    "        return memberRepository.save(member);",
    "    }",
    "",
    "    public Member update(MemberUpdateForm form) {",
    "        Member member = memberRepository.findById(form.getId())",
    "                .orElseThrow(() -> new IllegalArgumentException(\"회원을 찾을 수 없습니다\"));",
    "        member.setName(form.getName());",
    "        member.setEmail(form.getEmail());",
    "        member.setAge(form.getAge());",
    "        return memberRepository.save(member);",
    "    }",
    "",
    "    public Member findById(Long id) {",
    "        return memberRepository.findById(id)",
    "                .orElseThrow(() -> new IllegalArgumentException(\"회원을 찾을 수 없습니다\"));",
    "    }",
    "",
    "    public List<Member> findAll() {",
    "        return memberRepository.findAll();",
    "    }",
    "",
    "    public void delete(Long id) {",
    "        memberRepository.deleteById(id);",
    "    }",
    "",
    "    private String saveFile(MultipartFile file) {",
    "        try {",
    "            Path uploadPath = Paths.get(UPLOAD_DIR);",
    "            if (!Files.exists(uploadPath)) {",
    "                Files.createDirectories(uploadPath);",
    "            }",
    "            String filename = UUID.randomUUID() + \"_\" + file.getOriginalFilename();",
    "            Path filePath = uploadPath.resolve(filename);",
    "            Files.copy(file.getInputStream(), filePath, StandardCopyOption.REPLACE_EXISTING);",
    "            return filename;",
    "        } catch (IOException e) {",
    "            throw new RuntimeException(\"파일 저장 실패\", e);",
    "        }",
    "    }",
    "}",
    "```",
