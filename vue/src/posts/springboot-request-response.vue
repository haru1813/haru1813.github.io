    "| 파라미터 | `getParameter()` O | `getParameter()` O (상속) |",
    "| Body 읽기 | `getInputStream()` O | `getInputStream()` O (상속) |",
    "| 헤더 | X | `getHeader()` O |",
    "| 쿠키 | X | `getCookies()` O |",
    "| 세션 | X | `getSession()` O |",
    "| HTTP 메서드 | X | `getMethod()` O |",
    "| URI/URL | X | `getRequestURI()` O |",
    "| 실무 사용 | 거의 안 씀 | **대부분 이것을 사용** |",
