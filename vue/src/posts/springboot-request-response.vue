    "## 요청·응답 흐름 전체 다이어그램",
    "",
    "```mermaid",
    "sequenceDiagram",
    "    participant P as Postman",
    "    participant F as DispatcherServlet",
    "    participant C as Controller",
    "    participant V as Validation",
    "    participant S as Service",
    "    participant J as Jackson",
    "",
    "    P->>F: HTTP 요청 (JSON Body + Headers)",
    "    F->>J: JSON → Java 객체 역직렬화",
    "    J->>C: @RequestBody 파라미터 바인딩",
    "    C->>V: @Valid 검증",
    "    alt 검증 실패",
    "        V-->>C: BindingResult 에러",
    "        C-->>P: 400 Bad Request (에러 응답)",
    "    else 검증 성공",
    "        C->>S: 비즈니스 로직 호출",
    "        S-->>C: 결과 반환",
    "        C->>J: ResponseEntity → JSON 직렬화",
    "        J-->>P: HTTP 응답 (JSON Body + Status)",
    "    end",
    "```",
