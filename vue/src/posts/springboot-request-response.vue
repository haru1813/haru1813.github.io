    "## 7) MultipartFile (파일 업로드)",
    "`multipart/form-data` 형식의 파일 업로드를 처리합니다.",
    "",
    "```java",
    "@RestController",
    "@RequestMapping(\"/api/files\")",
    "public class FileUploadController {",
    "",
    "    // ─── 단일 파일 + 텍스트 필드 ───",
    "    @PostMapping(value = \"/upload\", consumes = MediaType.MULTIPART_FORM_DATA_VALUE)",
    "    public ResponseEntity<Map<String, Object>> upload(",
    "            @RequestParam(\"file\") MultipartFile file,",
    "            @RequestParam(value = \"description\", required = false) String desc) {",
    "        if (file.isEmpty()) {",
    "            return ResponseEntity.badRequest()",
    "                    .body(Map.of(\"error\", \"파일이 비어있습니다\"));",
    "        }",
    "        String savedPath = fileService.save(file);",
    "        return ResponseEntity.ok(Map.of(",
    "                \"savedPath\", savedPath,",
    "                \"originalName\", file.getOriginalFilename(),",
    "                \"size\", file.getSize(),",
    "                \"contentType\", file.getContentType(),",
    "                \"description\", desc != null ? desc : \"\"));",
    "    }",
    "",
    "    // ─── 다중 파일 업로드 ───",
    "    @PostMapping(\"/upload-multi\")",
    "    public ResponseEntity<Map<String, Object>> uploadMulti(",
    "            @RequestParam(\"files\") MultipartFile[] files) {",
    "        List<String> fileNames = new ArrayList<>();",
    "        long totalSize = 0;",
    "        for (MultipartFile f : files) {",
    "            fileService.save(f);",
    "            fileNames.add(f.getOriginalFilename());",
    "            totalSize += f.getSize();",
    "        }",
    "        return ResponseEntity.ok(Map.of(",
    "                \"fileCount\", files.length,",
    "                \"fileNames\", fileNames,",
    "                \"totalSize\", totalSize));",
    "    }",
    "",
    "    // ─── 파일 + JSON 데이터 동시에 ───",
