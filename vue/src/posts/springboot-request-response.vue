    "## 16) Controller - 전체 흐름의 핵심",
    "",
    "컨트롤러는 **요청을 받고**, **서비스를 호출**하고, **Model에 데이터를 담아** Thymeleaf 뷰로 전달합니다.",
    "",
    "```mermaid",
    "sequenceDiagram",
    "    participant B as 브라우저",
    "    participant C as Controller",
    "    participant S as Service",
    "    participant T as Thymeleaf",
    "    B->>C: HTTP 요청 (GET /members)",
    "    C->>S: findAll() 호출",
    "    S-->>C: List<Member> 반환",
    "    C->>C: model.addAttribute(\"members\", list)",
    "    C->>T: return \"member/list\"",
    "    T-->>B: HTML 렌더링 응답",
    "```",
    "",
    "### MemberController.java 전체 코드",
    "",
    "```java",
    "package com.example.demo.controller;",
    "",
    "import com.example.demo.dto.MemberCreateForm;",
    "import com.example.demo.dto.MemberUpdateForm;",
    "import com.example.demo.entity.Member;",
    "import com.example.demo.service.MemberService;",
    "import jakarta.validation.Valid;",
    "import lombok.RequiredArgsConstructor;",
    "import org.springframework.stereotype.Controller;",
    "import org.springframework.ui.Model;",
    "import org.springframework.validation.BindingResult;",
    "import org.springframework.web.bind.annotation.*;",
    "import org.springframework.web.servlet.mvc.support.RedirectAttributes;",
    "",
    "@Controller",
    "@RequestMapping(\"/members\")",
    "@RequiredArgsConstructor",
    "public class MemberController {",
    "",
    "    private final MemberService memberService;",
    "",
    "    // ──────────────────────────────────────────────",
    "    // 1. 회원 목록 (GET /members)",
    "    // ──────────────────────────────────────────────",
    "    @GetMapping",
    "    public String list(Model model) {",
    "        model.addAttribute(\"members\", memberService.findAll());",
    "        return \"member/list\";",
    "    }",
    "",
    "    // ──────────────────────────────────────────────",
    "    // 2. 회원 상세 (GET /members/{id})",
    "    // ──────────────────────────────────────────────",
    "    @GetMapping(\"/{id}\")",
    "    public String detail(@PathVariable Long id, Model model) {",
    "        model.addAttribute(\"member\", memberService.findById(id));",
    "        return \"member/detail\";",
    "    }",
    "",
    "    // ──────────────────────────────────────────────",
    "    // 3. 등록 폼 (GET /members/new)",
    "    // ──────────────────────────────────────────────",
    "    @GetMapping(\"/new\")",
    "    public String createForm(Model model) {",
    "        model.addAttribute(\"form\", new MemberCreateForm());",
    "        return \"member/create\";",
    "    }",
    "",
    "    // ──────────────────────────────────────────────",
    "    // 4. 등록 처리 (POST /members/new)",
    "    // ──────────────────────────────────────────────",
    "    @PostMapping(\"/new\")",
    "    public String create(",
    "            @Valid @ModelAttribute(\"form\") MemberCreateForm form,",
    "            BindingResult bindingResult,",
    "            RedirectAttributes ra) {",
    "",
    "        if (bindingResult.hasErrors()) {",
    "            return \"member/create\";",
    "        }",
    "",
    "        Member member = memberService.create(form);",
    "        ra.addFlashAttribute(\"message\", member.getName() + \"님이 등록되었습니다!\");",
    "        return \"redirect:/members\";",
    "    }",
    "",
    "    // ──────────────────────────────────────────────",
    "    // 5. 수정 폼 (GET /members/{id}/edit)",
    "    // ──────────────────────────────────────────────",
    "    @GetMapping(\"/{id}/edit\")",
    "    public String editForm(@PathVariable Long id, Model model) {",
    "        Member member = memberService.findById(id);",
    "",
    "        MemberUpdateForm form = new MemberUpdateForm();",
    "        form.setId(member.getId());",
    "        form.setName(member.getName());",
    "        form.setEmail(member.getEmail());",
    "        form.setAge(member.getAge());",
    "",
    "        model.addAttribute(\"form\", form);",
    "        return \"member/edit\";",
    "    }",
    "",
    "    // ──────────────────────────────────────────────",
    "    // 6. 수정 처리 (POST /members/{id}/edit)",
    "    // ──────────────────────────────────────────────",
    "    @PostMapping(\"/{id}/edit\")",
    "    public String edit(",
    "            @PathVariable Long id,",
    "            @Valid @ModelAttribute(\"form\") MemberUpdateForm form,",
    "            BindingResult bindingResult,",
    "            RedirectAttributes ra) {",
    "",
    "        if (bindingResult.hasErrors()) {",
    "            return \"member/edit\";",
    "        }",
    "",
    "        form.setId(id);",
    "        memberService.update(form);",
    "        ra.addFlashAttribute(\"message\", \"회원 정보가 수정되었습니다!\");",
    "        return \"redirect:/members/\" + id;",
    "    }",
    "",
    "    // ──────────────────────────────────────────────",
    "    // 7. 삭제 처리 (POST /members/{id}/delete)",
    "    // ──────────────────────────────────────────────",
    "    @PostMapping(\"/{id}/delete\")",
    "    public String delete(@PathVariable Long id, RedirectAttributes ra) {",
    "        memberService.delete(id);",
    "        ra.addFlashAttribute(\"message\", \"회원이 삭제되었습니다.\");",
    "        return \"redirect:/members\";",
    "    }",
    "}",
    "```",
    "",
    "### 컨트롤러 핵심 포인트 정리",
    "",
    "| 패턴 | 사용된 곳 | 설명 |",
    "|------|-----------|------|",
    "| `Model.addAttribute()` | 목록, 상세, 폼 | Controller → Thymeleaf 데이터 전달 |",
    "| `@ModelAttribute` | 등록/수정 POST | Thymeleaf 폼 → Controller 데이터 바인딩 |",
    "| `@Valid + BindingResult` | 등록/수정 POST | 유효성 검증 후 에러가 있으면 폼으로 되돌림 |",
    "| `RedirectAttributes` | 등록/수정/삭제 POST | 리다이렉트 후 1회성 메시지 전달 |",
    "| `return \"member/list\"` | 모든 핸들러 | 뷰 이름 → `templates/member/list.html` 렌더링 |",
    "| `return \"redirect:/members\"` | POST 후 | PRG(Post-Redirect-Get) 패턴 |",
