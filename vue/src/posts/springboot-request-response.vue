    "## 17) Thymeleaf 템플릿 - Controller와 데이터 주고받기",
    "",
    "### 공통 레이아웃 (layout/default.html)",
    "모든 페이지에서 재사용하는 헤더·푸터 구조입니다. `th:fragment`로 정의하고 `th:replace`로 삽입합니다.",
    "",
    "```html",
    "<!DOCTYPE html>",
    "<html xmlns:th=\"http://www.thymeleaf.org\">",
    "<head>",
    "    <meta charset=\"UTF-8\">",
    "    <meta name=\"viewport\" content=\"width=device-width, initial-scale=1.0\">",
    "    <title th:text=\"${pageTitle} ?: '회원 관리'\">회원 관리</title>",
    "    <link rel=\"stylesheet\" th:href=\"@{/css/style.css}\">",
    "</head>",
    "<body>",
    "",
    "<header th:fragment=\"header\">",
    "    <nav>",
    "        <a th:href=\"@{/members}\">회원 목록</a>",
    "        <a th:href=\"@{/members/new}\">회원 등록</a>",
    "    </nav>",
    "</header>",
    "",
    "<!-- 플래시 메시지 출력 (RedirectAttributes에서 전달) -->",
    "<div th:if=\"${message}\" class=\"alert-success\"",
    "     th:text=\"${message}\">",
    "</div>",
    "",
    "<main th:fragment=\"content\">",
    "    <p>페이지 내용이 여기에 들어갑니다.</p>",
    "</main>",
    "",
    "<footer th:fragment=\"footer\">",
    "    <p>&copy; 2026 회원 관리 시스템</p>",
    "</footer>",
    "",
    "</body>",
    "</html>",
    "```",
    "",
    "> **`th:fragment`**: 재사용 가능한 HTML 조각을 정의합니다. 다른 페이지에서 `th:replace=\"~{layout/default :: header}\"`로 삽입할 수 있습니다.",
    "",
