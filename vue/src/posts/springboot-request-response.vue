    "### ServletRequest vs HttpServletRequest 비교",
    "",
    "| 비교 | `ServletRequest` | `HttpServletRequest` |",
    "|------|-----------------|---------------------|",
    "| 계층 | 최상위 인터페이스 | HTTP 전용 하위 인터페이스 |",
