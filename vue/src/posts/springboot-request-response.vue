    "## 3) HttpEntity, RequestEntity",
    "`HttpEntity`는 **헤더 + 바디**를 하나의 객체로 묶어서 받습니다. `RequestEntity`는 여기에 **HTTP 메서드 + URL** 정보까지 포함합니다.",
    "",
    "```java",
    "@RestController",
    "@RequestMapping(\"/api/entity\")",
    "public class HttpEntityController {",
    "",
    "    // ─── HttpEntity: 헤더 + 바디 동시에 받기 ───",
    "    @PostMapping(\"/with-headers\")",
    "    public ResponseEntity<Map<String, Object>> withHeaders(",
    "            HttpEntity<CreateUserRequest> entity) {",
    "        HttpHeaders headers = entity.getHeaders();",
    "        CreateUserRequest body = entity.getBody();",
    "        String auth = headers.getFirst(\"Authorization\");",
    "        String contentType = headers.getFirst(\"Content-Type\");",
    "",
    "        Map<String, Object> result = new LinkedHashMap<>();",
    "        result.put(\"authorization\", auth);",
    "        result.put(\"contentType\", contentType);",
    "        result.put(\"receivedName\", body.getName());",
    "        result.put(\"receivedEmail\", body.getEmail());",
    "        return ResponseEntity.ok(result);",
    "    }",
    "",
    "    // ─── RequestEntity: 메서드 + URL + 헤더 + 바디 ───",
    "    @PostMapping(\"/full-info\")",
    "    public ResponseEntity<Map<String, Object>> fullInfo(",
    "            RequestEntity<CreateUserRequest> request) {",
    "        HttpMethod method = request.getMethod();",
    "        URI url = request.getUrl();",
    "        HttpHeaders headers = request.getHeaders();",
    "        CreateUserRequest body = request.getBody();",
    "",
    "        Map<String, Object> result = new LinkedHashMap<>();",
    "        result.put(\"method\", method.name());",
    "        result.put(\"url\", url.toString());",
    "        result.put(\"host\", headers.getFirst(\"Host\"));",
    "        result.put(\"customHeader\", headers.getFirst(\"X-Custom\"));",
    "        result.put(\"body\", body);",
    "        return ResponseEntity.ok(result);",
    "    }",
    "}",
