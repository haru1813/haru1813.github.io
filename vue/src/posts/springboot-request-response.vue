    "### 회원 목록 페이지 (member/list.html)",
    "Controller에서 `model.addAttribute(\"members\", list)`로 전달한 데이터를 **`th:each`**로 반복 출력합니다.",
    "",
    "```html",
    "<!DOCTYPE html>",
    "<html xmlns:th=\"http://www.thymeleaf.org\">",
    "<head>",
    "    <meta charset=\"UTF-8\">",
    "    <title>회원 목록</title>",
    "    <link rel=\"stylesheet\" th:href=\"@{/css/style.css}\">",
    "</head>",
    "<body>",
    "",
    "<header th:replace=\"~{layout/default :: header}\"></header>",
    "",
    "<!-- 플래시 메시지 (등록/삭제 후 리다이렉트 시 표시) -->",
    "<div th:if=\"${message}\" class=\"alert-success\" th:text=\"${message}\"></div>",
    "",
    "<h1>회원 목록</h1>",
    "",
    "<!-- 데이터가 없을 때 -->",
    "<p th:if=\"${#lists.isEmpty(members)}\">등록된 회원이 없습니다.</p>",
    "",
    "<!-- 데이터가 있을 때 -->",
    "<table th:unless=\"${#lists.isEmpty(members)}\">",
    "    <thead>",
    "        <tr>",
    "            <th>ID</th>",
    "            <th>이름</th>",
