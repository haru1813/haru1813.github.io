    "## 10) 요청 파라미터 vs 바디 정리",
    "",
    "| 구분 | 받는 쪽 | 용도 |",
    "|------|---------|------|",
    "| 쿼리(?key=val) | @RequestParam | GET, 조회 조건 |",
    "| 경로(/users/1) | @PathVariable | 리소스 식별자 |",
    "| 요청 바디(JSON) | @RequestBody | POST, PUT, PATCH 바디 |",
    "| 폼 데이터 | @RequestParam 또는 @ModelAttribute | form submit |",
    "| multipart | @RequestParam + MultipartFile | 파일 업로드 |",
    "",
    "## 11) RequestEntity/ResponseEntity vs @RequestBody/@ResponseBody 차이",
    "",
    "이름이 비슷해서 혼동하기 쉬운 네 가지를 명확하게 구분합니다.",
    "",
    "### 한 줄 정리",
    "",
    "| 구분 | 역할 | 핵심 |",
    "|------|------|------|",
    "| `@RequestBody` | 요청 **Body(JSON)**를 객체로 변환 | **어노테이션** (파라미터에 붙임) |",
    "| `@ResponseBody` | 반환 객체를 응답 **Body(JSON)**로 변환 | **어노테이션** (메서드에 붙임) |",
    "| `RequestEntity` | 요청 **전체**(메서드+URL+헤더+바디) 래핑 | **클래스** (파라미터 타입) |",
    "| `ResponseEntity` | 응답 **전체**(상태코드+헤더+바디) 래핑 | **클래스** (반환 타입) |",
    "",
    "### @RequestBody vs RequestEntity",
    "",
    "둘 다 **요청 데이터를 받는** 데 사용하지만, 받을 수 있는 범위가 다릅니다.",
    "",
    "```java",
    "// @RequestBody: Body만 받음",
    "@PostMapping(\"/v1\")",
    "public String v1(@RequestBody CreateUserRequest body) {",
    "    // body.getName(), body.getEmail() → Body 데이터만 접근 가능",
    "    // 헤더가 필요하면 @RequestHeader를 별도로 추가해야 함",
    "    return \"ok\";",
    "}",
    "",
    "// RequestEntity: Body + 헤더 + 메서드 + URL 전부",
    "@PostMapping(\"/v2\")",
    "public String v2(RequestEntity<CreateUserRequest> request) {",
    "    CreateUserRequest body = request.getBody();        // Body",
    "    String auth = request.getHeaders().getFirst(\"Authorization\"); // 헤더",
    "    HttpMethod method = request.getMethod();            // HTTP 메서드",
    "    URI url = request.getUrl();                         // URL",
    "    return \"ok\";",
    "}",
    "```",
    "",
    "| 비교 | `@RequestBody` | `RequestEntity<T>` |",
    "|------|---------------|-------------------|",
    "| 종류 | 어노테이션 | 클래스 |",
    "| Body 접근 | O | O (`getBody()`) |",
    "| 헤더 접근 | X (별도 `@RequestHeader` 필요) | O (`getHeaders()`) |",
    "| HTTP 메서드 | X | O (`getMethod()`) |",
    "| URL 정보 | X | O (`getUrl()`) |",
    "| 사용 빈도 | **가장 많이 사용** | 헤더+바디 동시 필요 시 |",
    "",
    "### @ResponseBody vs ResponseEntity",
    "",
    "둘 다 **응답을 만드는** 데 사용하지만, 제어할 수 있는 범위가 다릅니다.",
    "",
    "```java",
    "// @ResponseBody: Body만 제어 (상태코드 항상 200)",
    "@GetMapping(\"/v1\")",
    "@ResponseBody",
    "public UserResponse v1() {",
    "    return userService.findById(1L);",
    "    // Jackson이 JSON으로 변환 → 200 OK 고정",
    "    // 상태코드 변경 불가, 헤더 추가 불가",
    "}",
    "",
    "// ResponseEntity: 상태코드 + 헤더 + Body 전부 제어",
    "@GetMapping(\"/v2\")",
    "public ResponseEntity<UserResponse> v2() {",
    "    UserResponse user = userService.findById(1L);",
    "    return ResponseEntity",
    "            .status(HttpStatus.OK)              // 상태코드",
    "            .header(\"X-Custom\", \"value\")         // 커스텀 헤더",
    "            .cacheControl(CacheControl.maxAge(60, TimeUnit.SECONDS))",
    "            .body(user);                          // Body",
    "}",
    "```",
    "",
    "| 비교 | `@ResponseBody` | `ResponseEntity<T>` |",
    "|------|-----------------|---------------------|",
    "| 종류 | 어노테이션 | 클래스 |",
    "| Body → JSON | O (Jackson 자동 변환) | O (Jackson 자동 변환) |",
    "| 상태코드 제어 | X (항상 200) | O (`status()`, `created()`, `noContent()`) |",
    "| 헤더 추가 | X | O (`header()`, `cacheControl()`) |",
    "| Body 없는 응답 | X | O (`.build()`) |",
    "| 사용 빈도 | `@RestController` 사용 시 암묵적 | **가장 많이 사용** |",
    "",
    "### 실전에서 가장 많이 쓰는 조합",
    "",
    "```java",
    "@RestController  // @ResponseBody가 모든 메서드에 자동 적용",
    "@RequestMapping(\"/api/users\")",
    "public class UserController {",
    "",
    "    // ✅ 가장 일반적인 패턴: @RequestBody + ResponseEntity",
    "    @PostMapping",
    "    public ResponseEntity<UserResponse> create(",
    "            @RequestBody CreateUserRequest request) {  // 요청: Body만 필요",
    "        UserResponse user = userService.create(request);",
    "        return ResponseEntity.status(HttpStatus.CREATED) // 응답: 상태코드 제어",
    "                .body(user);",
    "    }",
    "",
    "    // ✅ 단순 조회: ResponseEntity로 감쌀 필요 없으면 바로 반환도 OK",
    "    @GetMapping(\"/{id}\")",
    "    public UserResponse findById(@PathVariable Long id) {",
    "        return userService.findById(id); // 200 OK + JSON 자동 변환",
    "    }",
    "}",
    "```",
    "",
