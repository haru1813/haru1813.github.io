    "# 5. 웹 요청 매핑",
    "",
    "## HTTP 메서드 매핑",
    "",
    "| 어노테이션 | HTTP 메서드 | 용도 |",
    "|-----------|-----------|------|",
    "| `@RequestMapping` | 모든 메서드 | 클래스 레벨 공통 경로 |",
    "| `@GetMapping` | GET | 조회 |",
    "| `@PostMapping` | POST | 생성 |",
    "| `@PutMapping` | PUT | 전체 수정 |",
    "| `@PatchMapping` | PATCH | 부분 수정 |",
    "| `@DeleteMapping` | DELETE | 삭제 |",
    "",
    "## 요청 파라미터 어노테이션",
    "",
    "```java",
    "@RestController",
    "@RequestMapping(\"/api/users\")",
    "@RequiredArgsConstructor",
    "public class UserController {",
    "",
    "    private final UserService userService;",
    "",
    "    // @PathVariable - URL 경로 변수",
    "    @GetMapping(\"/{id}\")",
    "    public ResponseEntity<UserResponse> findById(@PathVariable Long id) {",
    "        return ResponseEntity.ok(userService.findById(id));",
    "    }",
    "",
    "    // @RequestParam - 쿼리 파라미터",
    "    @GetMapping",
    "    public ResponseEntity<Page<UserResponse>> findAll(",
    "            @RequestParam(defaultValue = \"0\") int page,",
    "            @RequestParam(defaultValue = \"10\") int size,",
    "            @RequestParam(required = false) String keyword) {",
    "        return ResponseEntity.ok(userService.findAll(page, size, keyword));",
    "    }",
    "",
    "    // @RequestBody - JSON 요청 본문",
    "    @PostMapping",
    "    public ResponseEntity<UserResponse> create(",
    "            @Valid @RequestBody CreateUserRequest request) {",
    "        return ResponseEntity.status(HttpStatus.CREATED)",
    "                .body(userService.create(request));",
    "    }",
    "",
    "    // @RequestHeader - HTTP 헤더",
    "    @GetMapping(\"/me\")",
    "    public ResponseEntity<UserResponse> me(",
    "            @RequestHeader(\"Authorization\") String token) {",
    "        return ResponseEntity.ok(userService.findByToken(token));",
    "    }",
    "",
    "    // @CookieValue - 쿠키",
    "    @GetMapping(\"/session\")",
    "    public ResponseEntity<String> session(",
    "            @CookieValue(name = \"sessionId\", required = false) String sessionId) {",
    "        return ResponseEntity.ok(sessionId);",
    "    }",
    "}",
    "```",
    "",
    "### 요청 파라미터 정리",
    "",
    "| 어노테이션 | 소스 | 예시 |",
    "|-----------|------|------|",
    "| `@PathVariable` | URL 경로 | `/users/{id}` |",
    "| `@RequestParam` | 쿼리 스트링 | `/users?page=0&size=10` |",
    "| `@RequestBody` | 요청 본문 (JSON) | `{ \"name\": \"홍길동\" }` |",
    "| `@RequestHeader` | HTTP 헤더 | `Authorization: Bearer ...` |",
    "| `@CookieValue` | 쿠키 | `Cookie: sessionId=abc` |",
    "| `@ModelAttribute` | 폼 데이터 / 쿼리 → 객체 바인딩 | `name=홍길동&age=25` |",
    "",
    "## 응답 관련",
    "",
    "| 어노테이션 | 역할 |",
    "|-----------|------|",
    "| `@ResponseBody` | 반환값을 JSON으로 직렬화 (`@RestController`에 포함) |",
    "| `@ResponseStatus` | HTTP 상태 코드 지정 |",
    "| `ResponseEntity<T>` | 상태코드 + 헤더 + 바디 직접 제어 (어노테이션 아님) |",
