    "",
    "```java",
    "// DTO",
    "@Getter @Setter",
    "public class CreateUserRequest {",
    "",
    "    @NotBlank(message = \"이름은 필수입니다\")",
    "    @Size(min = 2, max = 50, message = \"이름은 2~50자\")",
    "    private String name;",
    "",
    "    @NotBlank(message = \"이메일은 필수입니다\")",
    "    @Email(message = \"이메일 형식이 올바르지 않습니다\")",
    "    private String email;",
    "",
    "    @NotNull(message = \"나이는 필수입니다\")",
    "    @Min(value = 0, message = \"나이는 0 이상\")",
    "    @Max(value = 150, message = \"나이는 150 이하\")",
    "    private Integer age;",
    "",
    "    @Pattern(regexp = \"^010-\\\\d{4}-\\\\d{4}$\", message = \"전화번호 형식: 010-XXXX-XXXX\")",
    "    private String phone;",
    "}",
    "```",
    "",
    "### Validation 어노테이션 표",
    "",
    "| 어노테이션 | 대상 | 설명 |",
    "|-----------|------|------|",
    "| `@NotNull` | 모든 타입 | null 불가 |",
    "| `@NotBlank` | String | null, \"\", \" \" 불가 |",
    "| `@NotEmpty` | String, Collection | null, 빈 값 불가 |",
    "| `@Size(min, max)` | String, Collection | 길이/크기 범위 |",
    "| `@Min` / `@Max` | 숫자 | 최소/최대 값 |",
    "| `@Email` | String | 이메일 형식 |",
    "| `@Pattern` | String | 정규식 매칭 |",
    "| `@Positive` | 숫자 | 양수만 |",
    "| `@PositiveOrZero` | 숫자 | 0 이상 |",
    "| `@Past` / `@Future` | 날짜 | 과거/미래 날짜 |",
    "",
    "### @Valid vs @Validated",
    "",
    "| 비교 | `@Valid` | `@Validated` |",
    "|------|---------|-------------|",
    "| 제공 | Jakarta Bean Validation | Spring |",
    "| 위치 | 메서드 파라미터, 필드 | 클래스, 메서드 파라미터 |",
    "| 그룹 검증 | X | **O** |",
    "| 중첩 검증 | **O** (내부 `@Valid`) | O |",
    "",
