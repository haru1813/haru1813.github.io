    "# 7. 트랜잭션",
    "",
    "## @Transactional",
    "",
    "```java",
    "@Service",
    "@RequiredArgsConstructor",
    "@Transactional(readOnly = true)  // 클래스 레벨: 기본 읽기 전용",
    "public class UserService {",
    "",
    "    private final UserRepository userRepository;",
    "",
    "    // readOnly = true 상속 → SELECT 최적화",
    "    public User findById(Long id) {",
    "        return userRepository.findById(id).orElseThrow();",
    "    }",
    "",
    "    @Transactional  // 쓰기 메서드에만 재정의",
    "    public User create(CreateUserRequest request) {",
    "        return userRepository.save(request.toEntity());",
    "    }",
    "",
    "    @Transactional(rollbackFor = Exception.class)  // Checked 예외도 롤백",
    "    public void transfer(Long fromId, Long toId, int amount) {",
    "        // ...",
    "    }",
    "}",
    "```",
    "",
    "### @Transactional 주요 속성",
    "",
    "| 속성 | 설명 | 기본값 |",
    "|------|------|--------|",
    "| `readOnly` | 읽기 전용 (Flush 생략, DB 최적화) | `false` |",
    "| `rollbackFor` | 특정 예외에서 롤백 | `RuntimeException` |",
    "| `noRollbackFor` | 특정 예외에서 롤백 안 함 | - |",
    "| `propagation` | 트랜잭션 전파 전략 | `REQUIRED` |",
    "| `isolation` | 격리 수준 | `DEFAULT` |",
    "| `timeout` | 타임아웃 (초) | -1 (무제한) |",
    "",
    "### Propagation (전파 전략)",
    "",
    "| 전파 | 설명 |",
    "|------|------|",
    "| `REQUIRED` | 기존 트랜잭션 참여, 없으면 새로 생성 (기본값) |",
    "| `REQUIRES_NEW` | 항상 새 트랜잭션 생성 (기존은 일시 중단) |",
    "| `MANDATORY` | 반드시 기존 트랜잭션 내에서 실행 |",
    "| `SUPPORTS` | 있으면 참여, 없으면 비트랜잭션 |",
    "| `NOT_SUPPORTED` | 트랜잭션 없이 실행 |",
    "| `NEVER` | 트랜잭션이 있으면 예외 |",
    "",
