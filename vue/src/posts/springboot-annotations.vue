    "## @ConditionalOn* (조건부 Bean 등록)",
    "",
    "특정 **조건이 충족될 때만** Bean을 등록합니다. Spring Boot의 Auto Configuration이 이 메커니즘으로 동작합니다.",
    "",
    "| 어노테이션 | 조건 | 예시 |",
    "|-----------|------|------|",
    "| `@ConditionalOnProperty` | 설정값이 특정 값일 때 | 기능 On/Off |",
    "| `@ConditionalOnClass` | classpath에 클래스가 있을 때 | 라이브러리 존재 여부 |",
    "| `@ConditionalOnMissingBean` | 해당 Bean이 없을 때 | 기본 구현 제공 |",
    "| `@ConditionalOnBean` | 해당 Bean이 있을 때 | 의존 Bean 존재 여부 |",
    "| `@ConditionalOnMissingClass` | classpath에 클래스가 없을 때 | 대체 구현 |",
    "| `@ConditionalOnExpression` | SpEL 표현식이 true일 때 | 복합 조건 |",
    "| `@ConditionalOnWebApplication` | 웹 애플리케이션일 때 | 웹 전용 Bean |",
    "",
    "### @ConditionalOnProperty",
    "",
    "```yaml",
    "# application.yml",
    "app:",
    "  cache:",
    "    enabled: true",
    "  notification:",
    "    sms:",
    "      enabled: false",
    "```",
    "",
    "```java",
    "@Bean",
    "@ConditionalOnProperty(name = \"app.cache.enabled\", havingValue = \"true\")",
    "public CacheManager cacheManager() {",
    "    return new ConcurrentMapCacheManager(\"products\", \"users\");",
    "}",
    "",
    "@Bean",
    "@ConditionalOnProperty(",
    "        name = \"app.notification.sms.enabled\",",
    "        havingValue = \"true\",",
    "        matchIfMissing = false  // 설정이 없으면 등록 안 함",
    ")",
    "public SmsService smsService() {",
    "    return new TwilioSmsService();",
    "}",
    "```",
    "",
    "### @ConditionalOnMissingBean",
    "",
    "사용자가 직접 Bean을 등록하지 않았을 때만 **기본 구현을 제공**합니다.",
    "",
    "```java",
    "@Configuration",
    "public class SecurityAutoConfig {",
    "",
    "    @Bean",
    "    @ConditionalOnMissingBean(PasswordEncoder.class)",
    "    public PasswordEncoder defaultPasswordEncoder() {",
    "        return new BCryptPasswordEncoder();  // 기본 제공",
    "    }",
    "}",
    "",
    "// 사용자가 직접 Bean을 등록하면 위 기본 Bean은 생성되지 않음",
    "@Configuration",
    "public class MySecurityConfig {",
    "    @Bean",
    "    public PasswordEncoder passwordEncoder() {",
    "        return new Argon2PasswordEncoder();  // 커스텀 → 이것만 등록됨",
    "    }",
    "}",
    "```",
    "",
    "### @ConditionalOnClass",
    "",
    "classpath에 특정 라이브러리가 있을 때만 Bean을 등록합니다.",
    "",
    "```java",
    "@Configuration",
    "@ConditionalOnClass(RedisConnectionFactory.class)  // Redis 라이브러리가 있을 때만",
    "public class RedisAutoConfig {",
    "",
    "    @Bean",
    "    public RedisCacheManager cacheManager(RedisConnectionFactory factory) {",
    "        return RedisCacheManager.builder(factory).build();",
    "    }",
    "}",
    "```",
    "",
    "> Spring Boot의 Auto Configuration은 대부분 `@ConditionalOnClass` + `@ConditionalOnMissingBean` 조합으로 동작합니다. 라이브러리가 있으면 자동 설정하되, 사용자가 직접 등록하면 양보합니다.",
    "",
    "---",
    "",
    "### 설정 어노테이션 정리",
    "",
    "| 어노테이션 | 역할 | 사용 시점 |",
    "|-----------|------|---------|",
    "| `@Configuration` | 설정 클래스 선언 | Bean 수동 등록 시 |",
    "| `@Bean` | 메서드 반환값을 Bean 등록 | 외부 라이브러리 객체 |",
    "| `@ConfigurationProperties` | 설정값 → 객체 바인딩 | 구조화된 설정 그룹 |",
    "| `@EnableConfigurationProperties` | `@ConfigurationProperties` 활성화 | 메인 클래스 |",
    "| `@Profile` | 환경별 Bean 활성화 | dev/test/prod 분리 |",
    "| `@PropertySource` | 추가 설정 파일 로드 | 별도 .properties 파일 |",
    "| `@ConditionalOnProperty` | 설정값 조건 | 기능 토글 |",
    "| `@ConditionalOnClass` | 클래스 존재 조건 | 라이브러리 의존 |",
    "| `@ConditionalOnMissingBean` | Bean 미존재 조건 | 기본 구현 제공 |",
