    "",
    "## @Valid & 검증 어노테이션",
    "",
    "```java",
    "// Controller",
    "@PostMapping",
    "public ResponseEntity<UserResponse> create(",
    "        @Valid @RequestBody CreateUserRequest request) {",
    "    return ResponseEntity.status(HttpStatus.CREATED)",
    "            .body(userService.create(request));",
