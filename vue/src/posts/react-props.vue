    "## ...props (나머지 Props 패턴)",
    "",
    "구조 분해에서 **필요한 props만 꺼내고 나머지를 `...rest`로 모아서** 하위 요소에 전달하는 패턴입니다. 래퍼 컴포넌트나 UI 라이브러리 제작 시 핵심적으로 사용됩니다.",
