    "### 실전 패턴 3 — 컴포넌트 감싸기 (Props 포워딩)",
    "",
    "기존 컴포넌트를 감싸면서 일부 props만 추가/변경하고 나머지는 그대로 전달합니다.",
    "",
    "```tsx",
    "import type { ComponentPropsWithoutRef } from 'react';",
    "",
    "// 기존 Input 컴포넌트의 모든 props를 받되, icon만 추가",
    "interface IconInputProps extends ComponentPropsWithoutRef<'input'> {",
    "  icon: string;",
    "}",
    "",
    "function IconInput({ icon, ...rest }: IconInputProps) {",
    "  return (",
    "    <div style={{ position: 'relative' }}>",
    "      <span style={{ position: 'absolute', left: '10px', top: '50%', transform: 'translateY(-50%)' }}>",
    "        {icon}",
    "      </span>",
    "      <input {...rest} style={{ paddingLeft: '36px', padding: '8px 8px 8px 36px', ...rest.style }} />",
    "    </div>",
    "  );",
    "}",
    "",
    "// 사용",
    "<IconInput icon=\"🔍\" placeholder=\"검색...\" onChange={(e) => setQuery(e.target.value)} />",
    "<IconInput icon=\"📧\" type=\"email\" placeholder=\"이메일\" required />",
    "```",
    "",
    "### ...props 동작 원리 정리",
    "",
    "```tsx",
    "// 부모가 전달한 모든 props",
    "<Card title=\"제목\" className=\"my-card\" id=\"card-1\" onClick={fn} />",
    "",
    "// 자식에서 구조 분해",
    "function Card({ title, ...rest }) {",
    "  // title = \"제목\"",
    "  // rest  = { className: \"my-card\", id: \"card-1\", onClick: fn }",
    "",
    "  return (",
    "    <div {...rest}>       {/* className, id, onClick이 <div>에 전달됨 */}",
    "      <h2>{title}</h2>",
    "    </div>",
    "  );",
    "}",
    "```",
    "",
    "| 문법 | 위치 | 역할 |",
    "|------|------|------|",
    "| `{...obj}` | JSX에서 | 객체의 모든 속성을 **props로 펼쳐서 전달** |",
    "| `...rest` | 매개변수에서 | 명시하지 않은 **나머지 props를 모아서 수집** |",
    "| `{...rest}` | 자식 JSX에서 | 수집한 나머지를 **하위 요소에 그대로 전달** |",
    "",
    "### 자주 사용하는 HTML 속성 타입",
    "",
    "| 타입 | 대상 | 사용 예 |",
    "|------|------|---------|",
    "| `ComponentPropsWithoutRef<'input'>` | `<input>` 래퍼 | type, placeholder, onChange... |",
    "| `ComponentPropsWithoutRef<'button'>` | `<button>` 래퍼 | onClick, disabled, type... |",
    "| `ComponentPropsWithoutRef<'a'>` | `<a>` 래퍼 | href, target, rel... |",
    "| `ComponentPropsWithoutRef<'div'>` | `<div>` 래퍼 | className, id, style... |",
    "| `ComponentPropsWithRef<'input'>` | ref 포함 | ref 전달이 필요할 때 |",
    "",
