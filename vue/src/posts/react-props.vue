    "## React.FC 타입 심층 분석",
    "",
    "`React.FC`(또는 `React.FunctionComponent`)는 함수 컴포넌트의 **타입을 지정**하는 TypeScript 제네릭 타입입니다.",
    "",
    "### React.FC 기본 사용법",
    "",
    "```tsx",
    "import type { FC } from 'react';",
    "",
    "interface GreetingProps {",
    "  name: string;",
    "  age: number;",
    "}",
    "",
    "// React.FC를 사용하는 방식",
    "const Greeting: FC<GreetingProps> = ({ name, age }) => {",
    "  return (",
    "    <div>",
    "      <h1>안녕하세요, {name}님!</h1>",
    "      <p>나이: {age}세</p>",
    "    </div>",
    "  );",
    "};",
    "```",
    "",
    "### React.FC를 사용하지 않는 방식 (권장)",
    "",
    "```tsx",
    "interface GreetingProps {",
    "  name: string;",
    "  age: number;",
    "}",
    "",
    "// 함수 매개변수에 직접 타입 지정",
    "function Greeting({ name, age }: GreetingProps) {",
    "  return (",
    "    <div>",
    "      <h1>안녕하세요, {name}님!</h1>",
    "      <p>나이: {age}세</p>",
    "    </div>",
    "  );",
    "}",
    "",
    "// 화살표 함수도 동일",
    "const Greeting2 = ({ name, age }: GreetingProps) => {",
    "  return <p>{name}, {age}세</p>;",
    "};",
    "```",
    "",
    "### React.FC의 내부 구조",
    "",
    "React 18 이전의 `React.FC` 타입 정의를 단순화하면 다음과 같습니다.",
    "",
    "```tsx",
    "type FC<P = {}> = (props: P & { children?: ReactNode }) => ReactElement | null;",
    "```",
    "",
    "즉 `FC<Props>`는:",
    "- Props를 받아서 `ReactElement | null`을 반환하는 함수 타입",
    "- **React 18 이전**: `children`을 자동으로 포함 → 의도하지 않은 children 전달 가능",
    "- **React 18 이후**: `children` 자동 포함이 **제거됨** → 명시적으로 선언해야 함",
    "",
    "### React.FC를 사용할 때와 하지 않을 때",
    "",
    "| 비교 항목 | `React.FC<Props>` | 직접 타입 지정 |",
    "|-----------|-------------------|---------------|",
    "| 반환 타입 | 자동 추론 (`ReactElement \\| null`) | 자동 추론 (더 유연) |",
    "| children | React 18+에서는 명시 필요 | 항상 명시 필요 |",
    "| 제네릭 컴포넌트 | ❌ 사용 불가 | ✅ 가능 |",
    "| 기본값 (defaultProps) | 타입 추론 안 됨 | 정상 동작 |",
    "| 함수 선언식 | ❌ (화살표 함수만) | ✅ 둘 다 가능 |",
    "| 코드 간결성 | 약간 장황 | 간결 |",
    "",
    "### React.FC의 문제점 — 제네릭 컴포넌트",
    "",
    "제네릭 컴포넌트를 만들 때 `React.FC`는 사용할 수 없습니다.",
    "",
    "```tsx",
    "// ❌ FC로는 제네릭 컴포넌트를 만들 수 없음",
    "// const List: FC<ListProps<T>> = ... // T를 어디에 선언?",
    "",
    "// ✅ 직접 타입 지정하면 제네릭 가능",
    "interface ListProps<T> {",
    "  items: T[];",
    "  renderItem: (item: T) => ReactNode;",
    "}",
    "",
    "function List<T>({ items, renderItem }: ListProps<T>) {",
    "  return (",
    "    <ul>",
    "      {items.map((item, i) => (",
    "        <li key={i}>{renderItem(item)}</li>",
    "      ))}",
    "    </ul>",
    "  );",
    "}",
    "",
    "// 사용 — T가 자동 추론됨",
    "function App() {",
    "  return (",
    "    <div>",
    "      <List",
    "        items={['React', 'Vue', 'Angular']}",
    "        renderItem={(item) => <strong>{item}</strong>}",
    "      />",
    "      <List",
    "        items={[1, 2, 3]}",
    "        renderItem={(n) => <span>{n * 10}</span>}",
    "      />",
    "    </div>",
    "  );",
    "}",
    "```",
    "",
    "### React.FC의 문제점 — defaultProps 타입 추론",
    "",
    "```tsx",
    "// ❌ FC + defaultProps → 타입 추론 불일치",
    "interface ButtonProps {",
    "  text?: string;",
    "  color?: string;",
    "}",
    "",
    "const Button: FC<ButtonProps> = ({ text, color }) => {",
    "  // text와 color가 여전히 string | undefined로 추론됨",
    "  return <button style={{ color }}>{text}</button>;",
    "};",
    "Button.defaultProps = { text: '클릭', color: 'blue' };",
    "",
    "// ✅ 구조 분해 기본값이면 정확히 string으로 추론됨",
    "function Button2({ text = '클릭', color = 'blue' }: ButtonProps) {",
    "  // text: string, color: string (undefined 아님!)",
    "  return <button style={{ color }}>{text}</button>;",
    "}",
    "```",
    "",
    "### React.FC의 장점",
    "",
    "그럼에도 `React.FC`를 사용하는 이유가 있습니다.",
    "",
    "```tsx",
    "// 1. 반환 타입을 명시적으로 제한 — 실수 방지",
    "const Good: FC<Props> = ({ name }) => {",
    "  return <div>{name}</div>;  // ✅ ReactElement 반환",
    "};",
    "",
    "// FC 없이는 문자열, 숫자 등도 반환 가능 (의도하지 않을 수 있음)",
    "function MaybeWrong({ name }: Props) {",
    "  return name;  // string 반환 — 에러 아님, 하지만 의도한 건가?",
    "}",
    "",
    "// 2. displayName 타입 지원",
    "const MyComponent: FC<Props> = (props) => <div />;",
    "MyComponent.displayName = 'MyComponent';  // ✅ 타입 안전",
    "```",
    "",
    "### 결론 — 어떤 방식을 쓸까?",
    "",
    "```tsx",
    "// ✅ 권장: 직접 타입 지정 (React 공식 문서도 이 방식 사용)",
    "interface CardProps {",
    "  title: string;",
    "  children: ReactNode;",
    "}",
    "",
    "function Card({ title, children }: CardProps) {",
    "  return (",
    "    <div>",
    "      <h2>{title}</h2>",
    "      {children}",
    "    </div>",
    "  );",
    "}",
    "",
    "// ⚠️ FC도 사용 가능 — 팀 컨벤션에 따라 결정",
    "const Card2: FC<CardProps> = ({ title, children }) => {",
    "  return (",
    "    <div>",
    "      <h2>{title}</h2>",
    "      {children}",
    "    </div>",
    "  );",
    "};",
    "```",
    "",
