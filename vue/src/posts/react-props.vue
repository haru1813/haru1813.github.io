    "  return (",
    "    <button",
    "      {...rest}",
    "      style={{",
    "        background: colors[variant],",
    "        color: variant === 'ghost' ? '#333' : 'white',",
    "        padding: paddings[size],",
    "        border: variant === 'ghost' ? '1px solid #ddd' : 'none',",
    "        borderRadius: '8px',",
    "        cursor: 'pointer',",
    "        fontSize: '14px',",
    "        ...rest.style,",
    "      }}",
    "    >",
    "      {children}",
    "    </button>",
    "  );",
    "}",
    "",
    "// 사용 — onClick, disabled, type 등 모든 button 속성이 그대로 전달됨",
    "function App() {",
    "  return (",
    "    <div style={{ display: 'flex', gap: '8px' }}>",
    "      <Button onClick={() => alert('저장!')}>저장</Button>",
    "      <Button variant=\"danger\" onClick={() => confirm('삭제?')}>삭제</Button>",
    "      <Button variant=\"ghost\" size=\"sm\" disabled>비활성</Button>",
    "      <Button type=\"submit\" form=\"myForm\">제출</Button>",
    "    </div>",
    "  );",
    "}",
    "```",
