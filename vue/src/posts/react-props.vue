    "#### TypeScript",
    "```tsx",
    "import type { ComponentPropsWithoutRef } from 'react';",
    "",
    "// ComponentPropsWithoutRef<'input'>은 <input>의 모든 HTML 속성 타입",
    "interface InputProps extends ComponentPropsWithoutRef<'input'> {",
    "  label?: string;",
    "  error?: string;",
    "}",
    "",
    "function Input({ label, error, ...rest }: InputProps) {",
    "  return (",
    "    <div style={{ marginBottom: '16px' }}>",
    "      {label && <label style={{ display: 'block', marginBottom: '4px', fontWeight: 'bold' }}>{label}</label>}",
    "      <input",
    "        {...rest}",
    "        style={{",
    "          padding: '8px 12px',",
    "          border: `2px solid ${error ? '#e74c3c' : '#ddd'}`,",
    "          borderRadius: '6px',",
    "          width: '100%',",
    "          boxSizing: 'border-box',",
    "        }}",
    "      />",
    "      {error && <p style={{ color: '#e74c3c', fontSize: '12px', marginTop: '4px' }}>{error}</p>}",
    "    </div>",
    "  );",
    "}",
    "",
    "// 사용 — type, placeholder, required, onChange 등 모든 input 속성 가능",
    "function App() {",
    "  return (",
    "    <form>",
    "      <Input label=\"이메일\" type=\"email\" placeholder=\"이메일 입력\" required />",
    "      <Input label=\"비밀번호\" type=\"password\" placeholder=\"비밀번호\" minLength={8} />",
