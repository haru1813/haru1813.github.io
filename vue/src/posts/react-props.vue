    "## Render Props 패턴",
    "",
    "함수를 props로 전달하여 **렌더링 로직을 외부에서 결정**하는 고급 패턴입니다.",
    "",
    "### JavaScript",
    "```jsx",
    "import { useState, useEffect } from 'react';",
    "",
    "function MouseTracker({ render }) {",
    "  const [position, setPosition] = useState({ x: 0, y: 0 });",
    "",
    "  useEffect(() => {",
    "    const handleMove = (e) => setPosition({ x: e.clientX, y: e.clientY });",
    "    window.addEventListener('mousemove', handleMove);",
    "    return () => window.removeEventListener('mousemove', handleMove);",
    "  }, []);",
    "",
    "  return render(position);",
    "}",
    "",
    "function App() {",
    "  return (",
    "    <div>",
    "      <MouseTracker render={({ x, y }) => (",
    "        <p>마우스 위치: ({x}, {y})</p>",
    "      )} />",
    "      <MouseTracker render={({ x, y }) => (",
    "        <div style={{",
    "          position: 'fixed', left: x, top: y,",
    "          width: '20px', height: '20px',",
    "          background: 'red', borderRadius: '50%',",
    "          transform: 'translate(-50%, -50%)',",
    "          pointerEvents: 'none',",
    "        }} />",
    "      )} />",
    "    </div>",
    "  );",
    "}",
    "```",
    "",
    "### TypeScript",
    "```tsx",
    "import { useState, useEffect, type ReactNode } from 'react';",
    "",
    "interface Position {",
    "  x: number;",
    "  y: number;",
    "}",
    "",
    "interface MouseTrackerProps {",
    "  render: (position: Position) => ReactNode;",
    "}",
    "",
    "function MouseTracker({ render }: MouseTrackerProps) {",
    "  const [position, setPosition] = useState<Position>({ x: 0, y: 0 });",
    "",
    "  useEffect(() => {",
    "    const handleMove = (e: MouseEvent): void => {",
    "      setPosition({ x: e.clientX, y: e.clientY });",
    "    };",
    "    window.addEventListener('mousemove', handleMove);",
    "    return () => window.removeEventListener('mousemove', handleMove);",
    "  }, []);",
    "",
    "  return <>{render(position)}</>;",
    "}",
    "```",
    "",
    "> Render Props는 커스텀 Hook이 등장하기 전에 많이 쓰였습니다. 현재는 Hook으로 대체하는 경우가 많지만, 렌더링 제어가 필요한 라이브러리에서 여전히 사용됩니다.",
