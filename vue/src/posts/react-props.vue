    "|------|----------|",
    "| 일반 컴포넌트 | **직접 타입 지정** (간결, 유연) |",
    "| 제네릭 컴포넌트 | **직접 타입 지정** (FC 불가) |",
    "| 팀에서 FC 컨벤션 사용 시 | **FC 사용** (일관성) |",
