    "React 컴포넌트는 **함수 선언**(`function`), **화살표 함수**(`=>`), **클래스**(`class extends Component`) 3가지로 작성합니다. 현대 React에서는 함수형 + Hooks(`useState`, `useEffect`)가 표준이며, 클래스 컴포넌트는 Error Boundary나 레거시 코드에서만 사용합니다. TypeScript에서는 함수형은 `React.FC<Props>` 또는 반환 타입 `: JSX.Element`, 클래스는 `Component<Props, State>` 제네릭으로 타입을 지정합니다."
