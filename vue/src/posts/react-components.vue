    "### 생명주기 메서드 (Lifecycle)",
    "",
    "클래스 컴포넌트만의 특징으로 **생명주기 메서드**가 있습니다.",
    "",
    "**JavaScript**",
    "```jsx",
    "import React, { Component } from 'react';",
    "",
    "class Timer extends Component {",
    "  constructor(props) {",
    "    super(props);",
    "    this.state = { seconds: 0 };",
    "    this.intervalId = null;",
    "  }",
    "",
    "  // 마운트 직후 (DOM에 렌더링된 후)",
    "  componentDidMount() {",
    "    console.log('Timer 마운트');",
    "    this.intervalId = setInterval(() => {",
    "      this.setState((prev) => ({ seconds: prev.seconds + 1 }));",
    "    }, 1000);",
    "  }",
    "",
    "  // state 또는 props 변경 시",
    "  componentDidUpdate(prevProps, prevState) {",
    "    if (prevState.seconds !== this.state.seconds) {",
    "      document.title = `${this.state.seconds}초 경과`;",
    "    }",
    "  }",
    "",
    "  // 언마운트 직전 (정리 작업)",
    "  componentWillUnmount() {",
    "    console.log('Timer 언마운트');",
    "    clearInterval(this.intervalId);",
    "  }",
    "",
    "  render() {",
    "    const min = String(Math.floor(this.state.seconds / 60)).padStart(2, '0');",
    "    const sec = String(this.state.seconds % 60).padStart(2, '0');",
    "    return <h1 style={{ fontFamily: 'monospace' }}>{min}:{sec}</h1>;",
    "  }",
    "}",
    "```",
    "",
    "**TypeScript**",
    "```tsx",
    "import React, { Component } from 'react';",
    "",
    "interface TimerState {",
    "  seconds: number;",
    "}",
    "",
    "class Timer extends Component<{}, TimerState> {",
    "  private intervalId: ReturnType<typeof setInterval> | null = null;",
    "",
    "  constructor(props: {}) {",
    "    super(props);",
    "    this.state = { seconds: 0 };",
    "  }",
    "",
    "  componentDidMount(): void {",
    "    this.intervalId = setInterval(() => {",
    "      this.setState((prev) => ({ seconds: prev.seconds + 1 }));",
    "    }, 1000);",
    "  }",
    "",
    "  componentDidUpdate(_prevProps: {}, prevState: TimerState): void {",
    "    if (prevState.seconds !== this.state.seconds) {",
    "      document.title = `${this.state.seconds}초 경과`;",
    "    }",
    "  }",
    "",
    "  componentWillUnmount(): void {",
    "    if (this.intervalId) clearInterval(this.intervalId);",
    "  }",
    "",
    "  render(): JSX.Element {",
    "    const min = String(Math.floor(this.state.seconds / 60)).padStart(2, '0');",
    "    const sec = String(this.state.seconds % 60).padStart(2, '0');",
    "    return <h1 style={{ fontFamily: 'monospace' }}>{min}:{sec}</h1>;",
    "  }",
    "}",
    "```",
    "",
    "| 생명주기 메서드 | 호출 시점 | 함수형 대응 |",
    "|-----------------|----------|-------------|",
    "| `constructor()` | 생성 시 | `useState()` 초기값 |",
    "| `componentDidMount()` | 마운트 후 | `useEffect(() => {}, [])` |",
    "| `componentDidUpdate()` | 업데이트 후 | `useEffect(() => {}, [dep])` |",
    "| `componentWillUnmount()` | 언마운트 전 | `useEffect`의 `return` |",
    "| `shouldComponentUpdate()` | 리렌더링 여부 결정 | `React.memo()` |",
