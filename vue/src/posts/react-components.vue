    "function LikeButton() {",
    "  const [likes, setLikes] = useState(0);",
    "  return <button onClick={() => setLikes(likes + 1)}>좋아요 {likes}</button>;",
    "}",
    "```",
    "",
    "**화살표 함수**",
    "```jsx",
    "import { useState } from 'react';",
    "",
    "const LikeButton = () => {",
    "  const [likes, setLikes] = useState(0);",
    "  return <button onClick={() => setLikes(likes + 1)}>좋아요 {likes}</button>;",
    "};",
    "```",
    "",
    "**클래스**",
    "```jsx",
    "import React, { Component } from 'react';",
    "",
    "class LikeButton extends Component {",
    "  constructor(props) {",
    "    super(props);",
    "    this.state = { likes: 0 };",
    "  }",
    "",
    "  render() {",
    "    return (",
    "      <button onClick={() => this.setState({ likes: this.state.likes + 1 })}>",
    "        좋아요 {this.state.likes}",
    "      </button>",
    "    );",
    "  }",
    "}",
    "```",
    "",
    "### 비교표",
    "",
    "| 비교 | 함수 선언 | 화살표 함수 | 클래스 |",
    "|------|----------|------------|--------|",
    "| 문법 | `function Comp() {}` | `const Comp = () => {}` | `class Comp extends Component` |",
    "| 상태 관리 | `useState()` Hook | `useState()` Hook | `this.state` / `this.setState()` |",
    "| 생명주기 | `useEffect()` | `useEffect()` | `componentDidMount()` 등 |",
    "| `this` 바인딩 | 없음 | 없음 | 필요 (화살표 메서드 또는 `.bind()`) |",
    "| 호이스팅 | ✅ (선언 전에 사용 가능) | ❌ | ❌ |",
    "| 코드 양 | 보통 | 적음 (간결) | 많음 |",
    "| 타입 지정 (TS) | `: JSX.Element` 반환 | `React.FC<Props>` | `Component<Props, State>` |",
    "| 권장 여부 | ✅ 권장 | ✅ 권장 | ⚠️ 레거시 |",
    "",
    "### 언제 무엇을 쓸까?",
    "",
