    "## 실전 예제: 전체 CRUD 캐시 적용",
    "",
    "```java",
    "@Service",
    "@RequiredArgsConstructor",
    "@Slf4j",
    "public class ProductService {",
    "",
    "    private final ProductRepository productRepository;",
    "",
    "    // 조회: 캐시 있으면 캐시 반환, 없으면 DB 조회 후 캐시 저장",
    "    @Cacheable(value = \"products\", key = \"#id\", unless = \"#result == null\")",
    "    public Product findById(Long id) {",
    "        log.info(\"[CACHE MISS] DB 조회: id={}\", id);",
    "        return productRepository.findById(id).orElse(null);",
    "    }",
    "",
    "    // 목록 조회: 카테고리+페이지 조합 키로 캐싱",
    "    @Cacheable(value = \"productList\",",
    "               key = \"#category + ':page:' + #page\",",
    "               condition = \"#page <= 10\")",
    "    public List<Product> findByCategory(String category, int page) {",
    "        log.info(\"[CACHE MISS] 목록 조회: category={}, page={}\", category, page);",
    "        return productRepository.findByCategory(category, page);",
    "    }",
    "",
    "    // 생성: 목록 캐시 전체 삭제 (목록이 바뀌므로)",
    "    @CacheEvict(value = \"productList\", allEntries = true)",
    "    public Product create(CreateProductRequest request) {",
    "        log.info(\"상품 생성 → productList 캐시 전체 삭제\");",
    "        return productRepository.save(request.toEntity());",
    "    }",
    "",
    "    // 수정: 해당 상품 캐시 갱신 + 목록 캐시 삭제",
    "    @Caching(",
    "        put = @CachePut(value = \"products\", key = \"#id\"),",
    "        evict = @CacheEvict(value = \"productList\", allEntries = true)",
    "    )",
    "    public Product update(Long id, UpdateProductRequest request) {",
    "        log.info(\"상품 수정 → products 캐시 갱신 + productList 캐시 삭제\");",
    "        Product product = productRepository.findById(id).orElseThrow();",
    "        product.setName(request.getName());",
    "        product.setPrice(request.getPrice());",
    "        return productRepository.save(product);",
    "    }",
    "",
    "    // 삭제: 해당 상품 캐시 삭제 + 목록 캐시 삭제",
    "    @Caching(evict = {",
    "        @CacheEvict(value = \"products\", key = \"#id\"),",
    "        @CacheEvict(value = \"productList\", allEntries = true)",
    "    })",
    "    public void delete(Long id) {",
    "        log.info(\"상품 삭제 → products, productList 캐시 삭제\");",
    "        productRepository.deleteById(id);",
    "    }",
    "}",
    "```",
