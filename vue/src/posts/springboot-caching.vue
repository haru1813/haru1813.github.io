    "    public void setHash(String key, String field, Object value) {",
    "        redisTemplate.opsForHash().put(key, field, value);",
    "    }",
    "",
    "    public Object getHash(String key, String field) {",
    "        return redisTemplate.opsForHash().get(key, field);",
    "    }",
    "",
