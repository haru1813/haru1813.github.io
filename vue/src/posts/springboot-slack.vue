    "import org.springframework.stereotype.Service;",
    "",
    "import jakarta.annotation.PostConstruct;",
    "import jakarta.annotation.PreDestroy;",
    "import java.util.List;",
    "",
    "import static com.slack.api.model.block.Blocks.*;",
    "import static com.slack.api.model.block.composition.BlockCompositions.*;",
    "",
    "@Service",
    "@Slf4j",
    "public class SlackSdkService {",
    "",
    "    @Value(\"${slack.bot.token}\")",
    "    private String botToken;",
    "",
    "    @Value(\"${slack.bot.channel}\")",
    "    private String defaultChannel;",
    "",
    "    private Slack slack;",
    "    private MethodsClient methods;",
    "",
    "    @PostConstruct",
    "    public void init() {",
    "        slack = Slack.getInstance();",
    "        methods = slack.methods(botToken);",
    "    }",
    "",
    "    @PreDestroy",
    "    public void destroy() throws Exception {",
    "        slack.close();",
    "    }",
    "",
    "    public void sendMessage(String message) {",
    "        try {",
    "            ChatPostMessageResponse response = methods.chatPostMessage(req -> req",
    "                    .channel(defaultChannel)",
    "                    .text(message)",
    "            );",
    "",
    "            if (response.isOk()) {",
    "                log.info(\"Slack SDK 메시지 전송 성공\");",
    "            } else {",
    "                log.error(\"Slack SDK 메시지 전송 실패: {}\", response.getError());",
    "            }",
    "        } catch (Exception e) {",
    "            log.error(\"Slack SDK 예외: {}\", e.getMessage());",
    "        }",
    "    }",
    "",
    "    public void sendBlockMessage(String headerText, String bodyText, String color) {",
    "        try {",
    "            methods.chatPostMessage(req -> req",
    "                    .channel(defaultChannel)",
    "                    .blocks(List.of(",
    "                            header(h -> h.text(plainText(headerText))),",
    "                            section(s -> s.text(markdownText(bodyText))),",
    "                            divider(),",
    "                            context(List.of(markdownText(\":clock1: \" + java.time.LocalDateTime.now())))",
    "                    ))",
    "            );",
    "        } catch (Exception e) {",
    "            log.error(\"Slack Block 메시지 전송 실패: {}\", e.getMessage());",
    "        }",
    "    }",
    "}",
    "```",
