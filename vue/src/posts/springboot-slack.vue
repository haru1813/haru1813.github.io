    "# 2. Slack Bot API (Web API)",
    "",
    "Webhook보다 더 많은 기능이 필요할 때 Slack Bot을 사용합니다.",
    "",
    "## Bot Token 발급",
    "",
    "1. [Slack API](https://api.slack.com/apps) → **Create New App** → **From scratch**",
    "2. 왼쪽 메뉴 **OAuth & Permissions** → **Scopes** → Bot Token Scopes 추가:",
    "",
    "| Scope | 용도 |",
    "|-------|------|",
    "| `chat:write` | 메시지 전송 |",
    "| `chat:write.public` | 참여하지 않은 public 채널에도 전송 |",
    "| `files:write` | 파일 업로드 |",
    "| `channels:read` | 채널 목록 조회 |",
    "",
    "3. **Install to Workspace** → Bot User OAuth Token 복사",
    "",
    "```",
    "xoxb-your-slack-bot-token",
    "```",
    "",
    "4. Bot을 알림 채널에 초대: 채널에서 `/invite @앱이름`",
    "",
    "## application.yml",
    "",
    "```yaml",
    "slack:",
    "  bot:",
    "    token: xoxb-your-slack-bot-token",
    "    channel: C0123456789  # 채널 ID (채널 우클릭 → 채널 세부정보에서 확인)",
    "```",
    "",
    "## SlackBotService",
    "",
    "```java",
    "package com.example.demo.service;",
    "",
    "import lombok.extern.slf4j.Slf4j;",
    "import org.springframework.beans.factory.annotation.Value;",
    "import org.springframework.http.*;",
    "import org.springframework.stereotype.Service;",
    "import org.springframework.web.client.RestTemplate;",
    "",
    "import java.util.HashMap;",
    "import java.util.Map;",
    "",
    "@Service",
    "@Slf4j",
    "public class SlackBotService {",
    "",
    "    private static final String SLACK_API_URL = \"https://slack.com/api/chat.postMessage\";",
    "",
    "    @Value(\"${slack.bot.token}\")",
    "    private String botToken;",
    "",
    "    @Value(\"${slack.bot.channel}\")",
    "    private String defaultChannel;",
    "",
    "    private final RestTemplate restTemplate = new RestTemplate();",
    "",
    "    public void sendMessage(String channel, String message) {",
    "        Map<String, Object> payload = new HashMap<>();",
    "        payload.put(\"channel\", channel);",
    "        payload.put(\"text\", message);",
    "",
    "        HttpHeaders headers = new HttpHeaders();",
    "        headers.setContentType(MediaType.APPLICATION_JSON);",
    "        headers.setBearerAuth(botToken);",
    "",
    "        HttpEntity<Map<String, Object>> request = new HttpEntity<>(payload, headers);",
    "",
    "        try {",
    "            ResponseEntity<Map> response = restTemplate.postForEntity(",
    "                    SLACK_API_URL, request, Map.class);",
    "",
    "            Map body = response.getBody();",
    "            if (body != null && Boolean.TRUE.equals(body.get(\"ok\"))) {",
    "                log.info(\"Slack Bot 메시지 전송 성공: channel={}\", channel);",
    "            } else {",
    "                log.error(\"Slack Bot 메시지 전송 실패: {}\", body != null ? body.get(\"error\") : \"unknown\");",
    "            }",
    "        } catch (Exception e) {",
    "            log.error(\"Slack Bot 메시지 전송 예외: {}\", e.getMessage());",
    "        }",
    "    }",
    "",
    "    public void sendMessage(String message) {",
    "        sendMessage(defaultChannel, message);",
    "    }",
    "}",
    "```",
    "",
    "### Webhook vs Bot API 비교",
    "",
    "| 비교 | Incoming Webhook | Bot API (chat.postMessage) |",
    "|------|:---:|:---:|",
    "| 인증 | Webhook URL (고정) | Bot Token (Bearer) |",
    "| 채널 | Webhook 생성 시 고정 | 파라미터로 자유롭게 지정 |",
    "| 여러 채널 전송 | Webhook 여러 개 필요 | 하나의 Token으로 가능 |",
    "| 파일 업로드 | 불가 | 가능 (files.upload) |",
    "| 메시지 수정/삭제 | 불가 | 가능 (chat.update/delete) |",
    "| 스레드 답글 | 불가 | 가능 (thread_ts) |",
    "| 설정 난이도 | 매우 쉬움 | 약간 복잡 |",
