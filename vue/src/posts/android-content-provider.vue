    "## ContentProvider vs 직접 DB 접근 비교",
    "",
    "| 비교 항목 | ContentProvider | 직접 DB 접근 |",
    "|-----------|----------------|-------------|",
    "| 앱 간 공유 | 가능 (IPC 지원) | 불가능 (같은 앱만) |",
    "| 접근 방식 | Content URI | 직접 SQLiteDatabase 참조 |",
    "| 보안 | 권한 기반 세밀한 제어 | 앱 내부 샌드박스에 의존 |",
    "| 추상화 | 저장 방식 숨김 (DB, 파일, 네트워크) | DB 구조 노출 |",
    "| CursorLoader | 지원 (UI 자동 갱신) | 별도 구현 필요 |",
    "| 성능 | IPC 오버헤드 있음 | 직접 접근으로 빠름 |",
    "| 복잡도 | 구현 코드 많음 | 상대적으로 간단 |",
    "| 사용 시기 | 다른 앱에 데이터 제공 시 | 앱 내부 전용 데이터 |",
