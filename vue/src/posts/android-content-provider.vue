    "        +delete() int",
    "        +getType() String",
    "    }",
    "    class MyContentProvider {",
    "        -SQLiteOpenHelper dbHelper",
    "        -UriMatcher uriMatcher",
    "        +onCreate() boolean",
    "        +query() Cursor",
    "        +insert() Uri",
    "        +update() int",
    "        +delete() int",
    "        +getType() String",
    "    }",
    "    ContentProvider <|-- MyContentProvider",
    "```",
    "",
    "### UriMatcher 설정",
    "",
    "UriMatcher는 들어온 URI를 분석하여 어떤 데이터에 접근할지 결정합니다.",
    "",
    "```java",
    "public class BookProvider extends ContentProvider {",
    "",
    "    public static final String AUTHORITY = \"com.example.myapp.bookprovider\";",
    "    public static final Uri CONTENT_URI =",
    "        Uri.parse(\"content://\" + AUTHORITY + \"/books\");",
    "",
    "    private static final int BOOKS = 1;      // 전체 목록",
    "    private static final int BOOK_ID = 2;     // 개별 항목",
    "",
    "    private static final UriMatcher uriMatcher = new UriMatcher(UriMatcher.NO_MATCH);",
    "",
    "    static {",
    "        uriMatcher.addURI(AUTHORITY, \"books\", BOOKS);",
    "        uriMatcher.addURI(AUTHORITY, \"books/#\", BOOK_ID);",
    "    }",
    "",
    "    private BookDatabaseHelper dbHelper;",
    "",
    "    @Override",
    "    public boolean onCreate() {",
    "        dbHelper = new BookDatabaseHelper(getContext());",
    "        return true;",
    "    }",
    "",
    "    @Override",
    "    public Cursor query(Uri uri, String[] projection,",
    "                        String selection, String[] selectionArgs, String sortOrder) {",
    "        SQLiteDatabase db = dbHelper.getReadableDatabase();",
    "        Cursor cursor;",
    "",
    "        switch (uriMatcher.match(uri)) {",
    "            case BOOKS:",
    "                cursor = db.query(\"books\", projection,",
    "                    selection, selectionArgs, null, null, sortOrder);",
    "                break;",
    "            case BOOK_ID:",
    "                String id = uri.getLastPathSegment();",
    "                cursor = db.query(\"books\", projection,",
    "                    \"_id = ?\", new String[]{id}, null, null, sortOrder);",
    "                break;",
    "            default:",
    "                throw new IllegalArgumentException(\"알 수 없는 URI: \" + uri);",
    "        }",
    "",
    "        cursor.setNotificationUri(getContext().getContentResolver(), uri);",
    "        return cursor;",
    "    }",
    "",
    "    @Override",
    "    public Uri insert(Uri uri, ContentValues values) {",
    "        if (uriMatcher.match(uri) != BOOKS) {",
    "            throw new IllegalArgumentException(\"잘못된 URI: \" + uri);",
    "        }",
    "",
    "        SQLiteDatabase db = dbHelper.getWritableDatabase();",
    "        long id = db.insert(\"books\", null, values);",
    "",
    "        if (id == -1) {",
    "            throw new SQLException(\"삽입 실패: \" + uri);",
    "        }",
    "",
    "        getContext().getContentResolver().notifyChange(uri, null);",
    "        return ContentUris.withAppendedId(CONTENT_URI, id);",
    "    }",
    "",
    "    @Override",
    "    public int update(Uri uri, ContentValues values,",
    "                      String selection, String[] selectionArgs) {",
    "        SQLiteDatabase db = dbHelper.getWritableDatabase();",
    "        int rowsUpdated;",
    "",
    "        switch (uriMatcher.match(uri)) {",
    "            case BOOKS:",
    "                rowsUpdated = db.update(\"books\", values, selection, selectionArgs);",
    "                break;",
    "            case BOOK_ID:",
    "                String id = uri.getLastPathSegment();",
    "                rowsUpdated = db.update(\"books\", values,",
    "                    \"_id = ?\", new String[]{id});",
    "                break;",
    "            default:",
    "                throw new IllegalArgumentException(\"알 수 없는 URI: \" + uri);",
    "        }",
    "",
    "        if (rowsUpdated > 0) {",
    "            getContext().getContentResolver().notifyChange(uri, null);",
    "        }",
    "        return rowsUpdated;",
    "    }",
    "",
    "    @Override",
    "    public int delete(Uri uri, String selection, String[] selectionArgs) {",
    "        SQLiteDatabase db = dbHelper.getWritableDatabase();",
    "        int rowsDeleted;",
    "",
    "        switch (uriMatcher.match(uri)) {",
    "            case BOOKS:",
    "                rowsDeleted = db.delete(\"books\", selection, selectionArgs);",
    "                break;",
    "            case BOOK_ID:",
    "                String id = uri.getLastPathSegment();",
    "                rowsDeleted = db.delete(\"books\", \"_id = ?\", new String[]{id});",
    "                break;",
    "            default:",
    "                throw new IllegalArgumentException(\"알 수 없는 URI: \" + uri);",
    "        }",
    "",
    "        if (rowsDeleted > 0) {",
    "            getContext().getContentResolver().notifyChange(uri, null);",
    "        }",
    "        return rowsDeleted;",
    "    }",
    "",
    "    @Override",
    "    public String getType(Uri uri) {",
    "        switch (uriMatcher.match(uri)) {",
    "            case BOOKS:",
    "                return \"vnd.android.cursor.dir/vnd.\" + AUTHORITY + \".books\";",
    "            case BOOK_ID:",
    "                return \"vnd.android.cursor.item/vnd.\" + AUTHORITY + \".books\";",
    "            default:",
    "                throw new IllegalArgumentException(\"알 수 없는 URI: \" + uri);",
    "        }",
    "    }",
    "}",
    "```",
