    "## Entity ↔ DTO 변환 방법",
    "",
    "### 방법 1: 수동 변환 (가장 기본)",
    "",
    "```java",
    "// DTO → Entity (Request DTO에 toEntity 메서드)",
    "public User toEntity() {",
    "    return User.builder()",
    "            .email(this.email)",
    "            .name(this.name)",
    "            .build();",
    "}",
    "",
    "// Entity → DTO (Response DTO에 from 정적 메서드)",
    "public static UserResponse from(User user) {",
    "    return UserResponse.builder()",
    "            .id(user.getId())",
    "            .email(user.getEmail())",
    "            .name(user.getName())",
    "            .build();",
    "}",
    "```",
    "",
    "| 장점 | 단점 |",
    "|------|------|",
    "| 명시적, 디버깅 쉬움 | 필드가 많으면 코드가 길어짐 |",
    "| 의존성 없음 | 변환 로직 반복 |",
    "| **가장 권장됨** | - |",
    "",
    "### 방법 2: MapStruct (자동 매핑, 컴파일 타임)",
    "",
    "**MapStruct**는 컴파일 시점에 변환 코드를 **자동 생성**하는 라이브러리입니다. 리플렉션을 사용하지 않아 성능이 좋습니다.",
    "",
    "#### 의존성",
    "",
    "```groovy",
    "dependencies {",
    "    implementation 'org.mapstruct:mapstruct:1.6.3'",
    "    annotationProcessor 'org.mapstruct:mapstruct-processor:1.6.3'",
    "    // Lombok과 함께 사용 시",
    "    annotationProcessor 'org.projectlombok:lombok-mapstruct-binding:0.2.0'",
    "}",
    "```",
    "",
    "#### Mapper 정의",
    "",
    "```java",
    "@Mapper(componentModel = \"spring\")",
    "public interface UserMapper {",
    "",
    "    UserResponse toResponse(User user);",
    "",
    "    @Mapping(target = \"id\", ignore = true)",
    "    @Mapping(target = \"role\", constant = \"USER\")",
    "    @Mapping(target = \"createdAt\", ignore = true)",
    "    User toEntity(CreateUserRequest request);",
    "",
    "    List<UserListResponse> toListResponse(List<User> users);",
    "",
    "    @Mapping(target = \"teamName\", source = \"team.name\")",
    "    MemberResponse toMemberResponse(Member member);",
    "}",
    "```",
    "",
    "#### 사용법",
    "",
    "```java",
    "@Service",
    "@RequiredArgsConstructor",
    "public class UserService {",
    "",
    "    private final UserRepository userRepository;",
    "    private final UserMapper userMapper;",
    "",
    "    public UserResponse create(CreateUserRequest request) {",
    "        User user = userMapper.toEntity(request);",
    "        User saved = userRepository.save(user);",
    "        return userMapper.toResponse(saved);",
    "    }",
    "}",
    "```",
    "",
    "| 장점 | 단점 |",
    "|------|------|",
    "| 코드 자동 생성 (반복 제거) | 설정 필요 (의존성, 어노테이션) |",
    "| **컴파일 타임 체크** (타입 안전) | 복잡한 매핑은 커스텀 필요 |",
    "| 리플렉션 없음 (성능 좋음) | 빌드 시 생성 코드 확인 필요 |",
    "",
    "### 방법 3: ModelMapper (런타임 리플렉션)",
    "",
    "**ModelMapper**는 런타임 리플렉션을 사용하여 필드명이 같은 객체끼리 자동으로 매핑해주는 라이브러리입니다. 설정이 간단하지만, 컴파일 시점에 오류를 잡을 수 없고 성능이 떨어져 실무에서는 수동 변환이나 MapStruct를 더 권장합니다.",
    "",
    "```java",
    "ModelMapper modelMapper = new ModelMapper();",
    "UserResponse response = modelMapper.map(user, UserResponse.class);",
    "```",
    "",
    "> 📌 ModelMapper의 매칭 전략, 커스텀 매핑, 장단점 비교 등 상세한 내용은 '스프링부트 다양한 라이브러리' 글을 참고하세요.",
    "",
    "### 변환 방법 비교",
    "",
    "| 방법 | 성능 | 타입 안전 | 코드량 | 추천 |",
    "|------|------|---------|--------|------|",
    "| **수동 변환** | 최고 | 완벽 | 많음 | **기본 추천** |",
    "| **MapStruct** | 매우 좋음 | 컴파일 타임 | 적음 | **필드 많을 때 추천** |",
    "| ModelMapper | 느림 | 런타임 | 최소 | 비추천 |",
    "",
