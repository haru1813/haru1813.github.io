    "## Java Record로 DTO 만들기 (Java 16+)",
    "",
    "**Record**는 **불변 데이터 객체**를 위한 Java 문법입니다. DTO에 이상적입니다.",
    "",
    "### Record 기본",
    "",
    "```java",
    "// Lombok 없이도 getter, equals, hashCode, toString 자동 생성",
    "public record UserResponse(",
    "    Long id,",
    "    String email,",
    "    String name,",
    "    String role,",
    "    LocalDateTime createdAt",
    ") {",
    "    public static UserResponse from(User user) {",
    "        return new UserResponse(",
    "                user.getId(),",
    "                user.getEmail(),",
    "                user.getName(),",
    "                user.getRole(),",
    "                user.getCreatedAt()",
    "        );",
    "    }",
    "}",
    "```",
    "",
    "### Request DTO를 Record로",
    "",
    "```java",
    "public record CreateUserRequest(",
    "    @NotBlank(message = \"이메일은 필수입니다\")",
    "    @Email",
    "    String email,",
    "",
    "    @NotBlank(message = \"비밀번호는 필수입니다\")",
    "    @Size(min = 8, max = 20)",
    "    String password,",
    "",
    "    @NotBlank(message = \"이름은 필수입니다\")",
    "    String name",
    ") {",
    "    public User toEntity(String encodedPassword) {",
    "        return User.builder()",
    "                .email(email)",
    "                .password(encodedPassword)",
    "                .name(name)",
    "                .role(\"USER\")",
    "                .build();",
    "    }",
    "}",
    "```",
    "",
    "### Record의 장점",
    "",
    "| 항목 | Record | 일반 클래스 + Lombok |",
    "|------|--------|---------------------|",
    "| 불변성 | **기본 불변** (final 필드) | `@Setter` 실수로 넣을 수 있음 |",
    "| 보일러플레이트 | 거의 없음 | `@Getter @NoArgsConstructor @AllArgsConstructor` 필요 |",
