    "",
    "## 실전 예제 1: @ConditionalOnProperty",
    "",
    "설정 값에 따라 기능을 켜고 끕니다.",
    "",
    "### application.yml",
    "",
    "```yaml",
    "app:",
    "  cache:",
    "    enabled: true",
    "    type: redis",
