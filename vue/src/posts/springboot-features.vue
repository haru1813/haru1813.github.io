    "# 3. @ConditionalOn* 어노테이션",
    "",
    "## 개념",
    "",
    "`@ConditionalOn*`은 **특정 조건이 충족될 때만 빈을 등록**하는 어노테이션입니다. Spring Boot의 **Auto Configuration**이 동작하는 핵심 메커니즘입니다.",
    "",
    "```mermaid",
    "flowchart TD",
    "    A[\"@Configuration 클래스\"] --> B{\"@ConditionalOn* 조건 체크\"}",
    "    B -->|조건 충족| C[\"빈 등록\"]",
    "    B -->|조건 미충족| D[\"빈 등록 안 함 (무시)\"]",
    "```",
    "",
    "## 주요 @ConditionalOn* 어노테이션",
    "",
    "| 어노테이션 | 조건 | 예시 |",
    "|------------|------|------|",
    "| `@ConditionalOnProperty` | **설정 값**이 특정 값일 때 | `app.cache.enabled=true` |",
    "| `@ConditionalOnClass` | **클래스**가 클래스패스에 있을 때 | Redis 라이브러리 존재 여부 |",
    "| `@ConditionalOnMissingClass` | 클래스가 없을 때 | |",
    "| `@ConditionalOnBean` | **빈**이 이미 등록되어 있을 때 | DataSource 빈 존재 여부 |",
    "| `@ConditionalOnMissingBean` | **빈**이 없을 때 | 사용자 정의 빈이 없으면 기본 빈 등록 |",
    "| `@ConditionalOnWebApplication` | 웹 애플리케이션일 때 | |",
    "| `@ConditionalOnExpression` | **SpEL 표현식**이 true일 때 | 복합 조건 |",
    "",
