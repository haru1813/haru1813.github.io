    "import com.example.demo.service.MessageFormatter;",
    "import com.example.demo.service.NotificationService;",
    "import com.example.demo.service.StorageService;",
    "import lombok.RequiredArgsConstructor;",
    "import org.springframework.beans.factory.annotation.Autowired;",
    "import org.springframework.beans.factory.annotation.Value;",
    "import org.springframework.core.env.Environment;",
    "import org.springframework.web.bind.annotation.*;",
    "",
    "import java.util.LinkedHashMap;",
    "import java.util.Map;",
    "",
    "@RestController",
    "@RequestMapping(\"/api\")",
    "@RequiredArgsConstructor",
    "public class ConfigController {",
    "",
    "    private final AppProperties appProperties;",
    "    private final JwtProperties jwtProperties;",
    "    private final StorageService storageService;",
    "    private final MessageFormatter messageFormatter;",
    "    private final Environment environment;",
    "",
    "    @Autowired(required = false)",
    "    private NotificationService notificationService;",
    "",
    "    @Value(\"${custom.api.key:no-key}\")",
    "    private String customApiKey;",
    "",
    "    @Value(\"${custom.api.url:https://default.com}\")",
    "    private String customApiUrl;",
    "",
    "    @Value(\"${server.port:8080}\")",
    "    private int serverPort;",
    "",
    "    // ① @Value, @ConfigurationProperties, @PropertySource 테스트",
    "    @GetMapping(\"/config\")",
    "    public Map<String, Object> getConfig() {",
    "        Map<String, Object> result = new LinkedHashMap<>();",
    "        result.put(\"activeProfile\", environment.getActiveProfiles());",
    "        result.put(\"serverPort\", serverPort);",
    "        result.put(\"app\", Map.of(",
    "            \"name\", appProperties.getName(),",
    "            \"version\", appProperties.getVersion(),",
    "            \"maxRetry\", appProperties.getMaxRetry()",
    "        ));",
    "        result.put(\"jwt\", Map.of(",
    "            \"issuer\", jwtProperties.getIssuer(),",
    "            \"accessTokenExpiration\", jwtProperties.getAccessTokenExpiration()",
    "        ));",
    "        result.put(\"customProperty\", Map.of(",
    "            \"apiKey\", customApiKey,",
    "            \"apiUrl\", customApiUrl",
    "        ));",
    "        return result;",
    "    }",
    "",
    "    // ② @Profile 테스트",
    "    @GetMapping(\"/storage\")",
    "    public Map<String, String> testStorage(",
    "            @RequestParam(defaultValue = \"test.png\") String filename) {",
    "        return Map.of(",
    "            \"storageType\", storageService.getType(),",
    "            \"savedPath\", storageService.store(filename)",
    "        );",
    "    }",
    "",
    "    // ③ @ConditionalOnProperty 테스트",
    "    @GetMapping(\"/notification\")",
    "    public Map<String, Object> testNotification(",
    "            @RequestParam(defaultValue = \"테스트 메시지\") String message) {",
    "        Map<String, Object> result = new LinkedHashMap<>();",
    "        result.put(\"emailEnabled\", notificationService != null);",
    "        result.put(\"result\", notificationService != null",
    "            ? notificationService.send(message)",
    "            : \"NotificationService 빈이 등록되지 않음 (app.notification.email.enabled=false)\");",
    "        return result;",
    "    }",
    "",
    "    // ④ @ConditionalOnMissingBean 테스트",
    "    @GetMapping(\"/format\")",
    "    public Map<String, String> testFormat(",
    "            @RequestParam(defaultValue = \"Hello World\") String message) {",
    "        return Map.of(",
    "            \"formatterClass\", messageFormatter.getClass().getSimpleName(),",
    "            \"result\", messageFormatter.format(message)",
    "        );",
    "    }",
    "}",
    "```",
