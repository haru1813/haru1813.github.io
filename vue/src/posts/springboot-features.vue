    "",
    "import com.example.demo.config.AppProperties;",
    "import com.example.demo.config.JwtProperties;",
    "import lombok.RequiredArgsConstructor;",
    "import org.springframework.beans.factory.annotation.Value;",
    "import org.springframework.core.env.Environment;",
    "import org.springframework.web.bind.annotation.GetMapping;",
    "import org.springframework.web.bind.annotation.RestController;",
    "",
    "import java.util.Map;",
    "",
    "@RestController",
    "@RequiredArgsConstructor",
    "public class ConfigController {",
    "",
    "    private final AppProperties appProperties;",
    "    private final JwtProperties jwtProperties;",
    "    private final Environment environment;",
    "",
    "    @Value(\"${custom.api.key:no-key}\")",
    "    private String customApiKey;",
    "",
    "    @Value(\"${custom.api.url:https://default.com}\")",
    "    private String customApiUrl;",
    "",
    "    @Value(\"${server.port:8080}\")",
    "    private int serverPort;",
    "",
    "    @GetMapping(\"/api/config\")",
    "    public Map<String, Object> getConfig() {",
    "        return Map.of(",
    "            \"profile\", environment.getActiveProfiles(),",
    "            \"app\", Map.of(",
    "                \"name\", appProperties.getName(),",
    "                \"version\", appProperties.getVersion(),",
    "                \"maxRetry\", appProperties.getMaxRetry()",
    "            ),",
    "            \"jwt\", Map.of(",
    "                \"issuer\", jwtProperties.getIssuer(),",
    "                \"accessTokenExpiration\", jwtProperties.getAccessTokenExpiration()",
    "            ),",
    "            \"custom\", Map.of(",
    "                \"apiKey\", customApiKey,",
    "                \"apiUrl\", customApiUrl",
    "            ),",
    "            \"serverPort\", serverPort",
    "        );",
    "    }",
    "}",
    "```",
