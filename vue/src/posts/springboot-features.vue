    "## 실습에서 사용한 어노테이션 정리",
    "",
    "| 어노테이션 | 파일 | 역할 |",
    "|-----------|------|------|",
    "| `@SpringBootApplication` | DemoApplication | 앱 시작점 |",
    "| `@ConfigurationPropertiesScan` | DemoApplication | `@ConfigurationProperties` 자동 스캔 |",
    "| `@Configuration` | AppConfig | 설정 클래스 선언 |",
    "| `@Bean` | AppConfig | 메서드 반환값을 빈 등록 (RestTemplate) |",
    "| `@PropertySource` | AppConfig | 외부 설정 파일 로드 |",
    "| `@Value` | ConfigController | 단일 설정값 주입 |",
    "| `@ConfigurationProperties` | AppProperties, JwtProperties | 설정 그룹 → 객체 바인딩 |",
