    "# 5. Auto Configuration 원리",
    "",
    "## 개념",
    "",
    "**Auto Configuration**은 Spring Boot의 핵심 철학인 **\"설정보다 관습(Convention over Configuration)\"**을 구현하는 메커니즘입니다. 클래스패스의 라이브러리와 설정을 분석하여 **필요한 빈을 자동으로 등록**합니다.",
    "",
    "```mermaid",
    "flowchart TD",
    "    A[\"spring-boot-starter-web 추가\"] --> B[\"클래스패스에<br/>Tomcat, Jackson, Spring MVC 존재\"]",
    "    B --> C[\"Auto Configuration 동작\"]",
    "    C --> D[\"DispatcherServlet 자동 등록\"]",
    "    C --> E[\"내장 Tomcat 자동 설정\"]",
    "    C --> F[\"Jackson ObjectMapper 자동 등록\"]",
    "    C --> G[\"에러 핸들링 자동 설정\"]",
    "```",
    "",
    "> `spring-boot-starter-web`을 추가하기만 하면 별도 설정 없이 웹 서버가 동작하는 이유가 바로 Auto Configuration입니다.",
    "",
    "## 동작 원리",
    "",
    "### @SpringBootApplication 분해",
    "",
    "```java",
    "@SpringBootApplication",
    "// ↓ 이 어노테이션은 3가지를 포함합니다",
    "",
    "@SpringBootConfiguration    // @Configuration과 동일",
    "@EnableAutoConfiguration    // ★ 자동 설정 활성화",
    "@ComponentScan              // 컴포넌트 스캔",
    "public class DemoApplication { ... }",
    "```",
    "",
    "### @EnableAutoConfiguration 동작 과정",
    "",
    "```mermaid",
    "flowchart TD",
    "    A[\"@EnableAutoConfiguration\"] --> B[\"META-INF/spring/org.springframework.boot.autoconfigure.AutoConfiguration.imports 파일 로드\"]",
    "    B --> C[\"자동 설정 후보 클래스 목록 로드<br/>(수백 개)\"]",
    "    C --> D[\"각 클래스의 @ConditionalOn* 조건 평가\"]",
    "    D -->|조건 충족| E[\"빈 등록\"]",
    "    D -->|조건 미충족| F[\"무시\"]",
    "```",
    "",
    "### 실제 Auto Configuration 예시 (DataSource)",
    "",
    "Spring Boot의 `DataSourceAutoConfiguration` 내부를 단순화하면 다음과 같습니다.",
    "",
    "```java",
    "@AutoConfiguration",
    "@ConditionalOnClass(DataSource.class)    // DataSource 클래스가 있을 때만",
    "@EnableConfigurationProperties(DataSourceProperties.class)",
    "public class DataSourceAutoConfiguration {",
    "",
    "    @Bean",
    "    @ConditionalOnMissingBean  // 사용자가 직접 DataSource를 등록하지 않았을 때만",
    "    public DataSource dataSource(DataSourceProperties properties) {",
    "        return DataSourceBuilder.create()",
    "                .url(properties.getUrl())",
    "                .username(properties.getUsername())",
    "                .password(properties.getPassword())",
    "                .build();",
    "    }",
    "}",
    "```",
    "",
    "### 핵심 패턴",
    "",
    "```",
    "1. @ConditionalOnClass    → 라이브러리가 있는지 확인",
    "2. @ConditionalOnMissingBean → 사용자가 직접 등록하지 않았는지 확인",
    "3. @ConfigurationProperties → 설정 값 바인딩",
    "4. 기본값으로 빈 등록       → 설정 없이도 동작",
    "```",
    "",
    "> 이 패턴 덕분에 \"starter만 추가하면 동작\"하는 Spring Boot의 마법이 가능합니다.",
    "",
