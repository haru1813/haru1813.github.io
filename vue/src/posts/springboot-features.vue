    "## 정리",
    "",
    "| 기능 | 역할 | 핵심 코드 |",
    "|------|------|-----------|",
    "| `@ConfigurationProperties` | 설정 → 객체 바인딩 | `prefix = \"app.jwt\"` + `@Validated` |",
    "| `@Profile` | 환경별 설정/빈 분리 | `@Profile(\"dev\")`, `application-dev.yml` |",
    "| `@ConditionalOnProperty` | 설정 값 조건부 빈 | `havingValue = \"true\"` |",
    "| `@ConditionalOnClass` | 클래스 존재 조건부 빈 | 라이브러리 유무에 따른 분기 |",
    "| `@ConditionalOnMissingBean` | 빈 미존재 조건부 빈 | 기본값 제공 패턴 |",
    "| Actuator `/health` | 헬스 체크 | `HealthIndicator` 구현 |",
    "| Actuator `/metrics` | 메트릭 | `MeterRegistry`, `Counter` |",
    "| Auto Configuration | 자동 빈 등록 | `@AutoConfiguration` + `@ConditionalOn*` |",
    "",
    "| 상황 | 추천 기능 |",
    "|------|----------|",
    "| 설정 값을 안전하게 관리 | `@ConfigurationProperties` + `@Validated` |",
    "| dev/prod 환경 분리 | `@Profile` + `application-{profile}.yml` |",
    "| 기능 ON/OFF | `@ConditionalOnProperty` |",
    "| 라이브러리 유무 대응 | `@ConditionalOnClass` + `@ConditionalOnMissingBean` |",
    "| 운영 모니터링 | Actuator `health`, `metrics`, `loggers` |",
    "| 자동 설정 동작 확인 | `debug: true` (Conditions Report) |",
    "| 자동 설정 제외 | `@SpringBootApplication(exclude = ...)` |"
