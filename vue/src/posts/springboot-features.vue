    "## 커스텀 Auto Configuration 만들기",
    "",
    "자체 라이브러리에 Auto Configuration을 추가하는 방법입니다.",
    "",
    "### 1단계: Properties 클래스",
    "",
    "```java",
    "@Getter @Setter",
    "@ConfigurationProperties(prefix = \"my.greeting\")",
    "public class GreetingProperties {",
    "    private boolean enabled = true;",
    "    private String prefix = \"Hello\";",
    "    private String suffix = \"!\";",
    "}",
    "```",
    "",
    "### 2단계: 서비스 클래스",
    "",
    "```java",
    "@RequiredArgsConstructor",
    "public class GreetingService {",
    "",
    "    private final GreetingProperties properties;",
    "",
    "    public String greet(String name) {",
    "        return properties.getPrefix() + \", \" + name + properties.getSuffix();",
    "    }",
    "}",
    "```",
    "",
    "### 3단계: Auto Configuration 클래스",
    "",
    "```java",
    "@AutoConfiguration",
    "@ConditionalOnProperty(",
    "        name = \"my.greeting.enabled\",",
    "        havingValue = \"true\",",
    "        matchIfMissing = true",
    ")",
    "@EnableConfigurationProperties(GreetingProperties.class)",
    "public class GreetingAutoConfiguration {",
    "",
    "    @Bean",
    "    @ConditionalOnMissingBean",
    "    public GreetingService greetingService(GreetingProperties properties) {",
    "        return new GreetingService(properties);",
    "    }",
    "}",
    "```",
    "",
    "### 4단계: imports 파일 등록",
    "",
    "```",
    "src/main/resources/",
    "└── META-INF/spring/",
    "    └── org.springframework.boot.autoconfigure.AutoConfiguration.imports",
    "```",
    "",
    "```",
    "com.example.greeting.GreetingAutoConfiguration",
    "```",
    "",
    "### 사용 결과",
    "",
    "```yaml",
    "# application.yml",
    "my:",
    "  greeting:",
    "    prefix: 안녕하세요",
    "    suffix: 님!",
    "```",
    "",
    "```java",
    "@RestController",
    "@RequiredArgsConstructor",
    "public class HelloController {",
    "",
    "    private final GreetingService greetingService;",
    "",
    "    @GetMapping(\"/hello\")",
    "    public String hello(@RequestParam String name) {",
    "        return greetingService.greet(name);",
    "        // → \"안녕하세요, 홍길동님!\"",
    "    }",
    "}",
    "```",
